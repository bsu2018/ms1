         SUBROUTINE DRPRNT(KAR, DATDIR, MSWALL, TOKN, VALN )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:   DRPRNT(KAR, DATDIR, MSWALL )
C   ------------
C   HAHAEHE :   *** OPAOTKA PEKTB  #E:
C   -----------
C                    #E T0 TM KOUT  PPVENT
C
C            E:    T0 - BPEM HAAA BBOA;
C                    TM - BPEM KOHEA BBOA;
C                    KOUT - "KA KOUT -  TOK  PA COXPAHEMX
C                            BBOHX HAEH PEEH (A COXPAHEH
C                            HOUTT) BBOT HA EAT ";              0
C                      PPVENT - P = 0 HE EATAETC HOPMA B
C                                       MOMEHT KOMMTA;
C                               P = 1 - EATAETC;
C                                 ( MOAHE = 0 );
C
C
C
C                  P CEHOM PHT APAMETPOB PEKTB 
C                   CCKA "AKA BBOA" --> AOHET O
C                   COMMON/SERVZ6/  BOAET A "PON",
C                    + BOEHE MASPRT(106) <-> PPVENT;
C   METO:         OEBEH;
C   ------
C
C   APAMETP:     KAR - CMBOHA CTPOKA TEKCTA PEKTB #E;
C   ---------            (OCTABETC MOHTOPOM - CKAHEPOM );
C                  DATDIR - MACCB PHTX CE  KAR->(CTPOKA P.)
C                  MSWALL - CCOK COCTOH EMEHTOB E;
C   OPOPAMM:  1. GETSPS
C   ------------   2. COMMON PROF1, SERVZ6
C   PMEAHE:    1. B COMMON SERVZ6 KCPTC:
C   ----------        TBEGON, TENDON, KOUTON, SPISON
C                  2.  O MOAH T0=0.0, TM=666.0, KOUT=1;
C
CZZZ-------------------------------------------------------------------
         COMMON/PROF1/ MASPRT(200)
         COMMON/SERVZ6/ TBEGON, TENDON, KOUTON, SPISON(55)
         REAL TOKN(*),VALN(*)
         INTEGER SPISON
         DIMENSION KAR(*), DATDIR(*), MSWALL(*)
         NRDR = MASPRT(12)
         NWTR = MASPRT(13)
C--->  ECT  MSWALL
       IF ( MASPRT(74) .EQ. 1 ) GO TO 200
       WRITE(NWTR,201)
201    FORMAT(/' *** HET OCAH E - PEKTBA HOPPETC ***'/)
        RETURN
200    CONTINUE
C        ***************************
         KOUT = 1
         IPP = 0
C********READ(NRDR, PON)
       IF ( DATDIR(1) .EQ. 0.0 ) GO TO 100
C--> TO-TO ECT
       T0 = DATDIR(2)
       TM = DATDIR(3)
       KOUT = IFIX( DATDIR(4) )
       IPP = IFIX( DATDIR(5) )
       IF ( IPP .EQ. 0 ) GO  TO   101
       IPP = 1
       GO TO 101
100    CONTINUE
C-->   M O   A H  E
       T0 = 0.0
       TM = +666.0
       KOUT = 1
       IPP = 0
101    CONTINUE
C********WRITE(NWTR, PON)
         IF ( KOUT .LE. 0 ) KOUT = 1
         TBEGON = T0
         TENDON = TM
         KOUTON = KOUT
         WRITE(NWTR, 50) T0, TM, KOUT, IPP
50       FORMAT(/' *** #E ਭ:==> T0=',G12.5,' TM=',G12.5,
     =  ' KOUT=',I5, ' PPVENT=', I2, ' ***'/)
C--------
         CALL GETSPS(MSWALL, SPISON, IRC )
         GO TO (1, 2, 3 ), IRC
         CALL SUBERR(IRC)
3        CONTINUE
         WRITE(NWTR,10)
10       FORMAT(/' *** KOHE AA "RDR" - PAOTA PEPBAETC ***'/
     = ' *** [ DRPRNT ] ***'/)
         call STOPP(16)
C######################################################################
2        CONTINUE
C--->  SPIS CT....   CPOC PON....
         MASPRT(56) = 0
         MASPRT(106) = 0
         RETURN
C######################################################################
1        CONTINUE
C--> CCOK ECT.....  PON  "BEPHEM"
         MASPRT(56) = 1
         MASPRT(106) = IPP
         RETURN
         END

         SUBROUTINE DRout0(KAR, DAT, MSWALL, TOKN, VALLN )
CXXX----KOMEKC "MS1" -->
C   OPOPAMMA:   DRout0(KAR, DAT, MSWALL, TOKN, VALLN )
C   ------------
C   HAHAEHE:    *** COHTE  OPAOTK PEKTB # ***
C   -----------      #  T0 TM KOUT
C
C                  T0 - HAAHOE BPEM
C                  TM - KOHEHOE  BPEM
C                  KOUT - EPE CKOKO TOEK BBOT;
C
C
C   METO:         OEBEH;
C   -----
C   APAMETP:     KAR - CMBOHA CTPOKA TEKCTA PEKTB(A-KOD,80);
C   ---------      DAT - MACCB PHTX AHHX PEKTB;
C                  MSWALL - COBA COCTOH E;
C                  TOKN, VALLN - MACCB XPAHEH "TOEK";
C
C   OPOPAMM:  1. COMMON PROF1;
C   -------------  2. GETSPS, DRPR00, READ00 , PPNCOM
C
C   PMEAHE:    1. "MOAHE" --> TOKO  KOUT      ---->
C   ----------         1
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
C-->    1- AC NSPOOL
         EQUIVALENCE ( MASPRT(48), HOUTT )
C********EQUIVALENCE ( SETKA,  MASPRT(63) )
C
         EQUIVALENCE ( NWTR,   MASPRT(13) )
         EQUIVALENCE ( NWTRE,  MASPRT(14) )
C********EQUIVALENCE ( NRDR,   MASPRT(12) )
         INTEGER SPIS
         DIMENSION SPIS(91), MSWALL(*), TOKN(*), VALLN(*)
         DIMENSION KAR(*), DAT(*)
C-->  ECT  MSWALL ?
       IF ( MASPRT(74) .EQ. 1 ) GO TO 59
       WRITE(NWTR, 60)
60     FORMAT(/' *** HET OCAH E - PEKTBA HOPPETC ***'/)
       RETURN
59     CONTINUE
         T0 = 0.0
         TM = 666.0
         KOUT = 1
       KKK = IFIX( DAT(1) )
       IF ( KKK .LT. 2 ) GO TO 202
       T0 = DAT(2)
       TM = DAT(3)
       KOUT = IFIX( DAT(4) )
         IF ( KOUT .LE. 0 ) KOUT = 1
202     CONTINUE
         WRITE(NWTR, 203) T0, TM, KOUT
203      FORMAT(/' *** # ਭ:=> T0=', G12.5,
     = ' TM=',G12.5, ' KOUT=',I5, ' ***'/)
C----------------------------------------------------------------------
         CALL GETSPO( MSWALL, SPIS, IRC )
         GO TO ( 1,2,3), IRC
         CALL SUBERR(IRC)
3        CONTINUE
         WRITE(NWTR, 204)
204      FORMAT(/' *** KOHE AA HA "RDR" - PAOTA PEPBAETC ***'/)
         call STOPP(16)
C----------------------------------------------------------------------
2        CONTINUE
C-->  SPIS CT
         RETURN
C**********************************************************************
1        CONTINUE
C--> OOTOBKA K BBO...
       TIME = 0.0
C
       KTXX = 0
C
C                                             ???????????
C--> OTKPOEMC
C
       TIME = 0.0
       KTXX = 0
         KOD = 1
         CALL READ00( KOD, HOUTT, TOKN, VALLN)
         GO TO ( 1000, 2000, 3000 ), KOD
         CALL SUBERR(KOD)
1000     CONTINUE
C*** OKPEM ********************************************************
C---> KOHTPOPEM TM, B TIME --> MAX BPEM...
C        IF ( TIME .LT. TM ) WRITE(NWTR,205) TIME, TM, TIME
C205      FORMAT(' *** AHHE AOB HAKOEH OPEEEH TOKO O ',
C    = G12.5, ' CEK. ***'/
C    = ' *** APOEHA BEPXH PAHA BPEMEHHOO HTEPBAA TM=',
C    = G12.5, ' CEK. ***'/
C    = ' *** EPEOPEEETC HA HAEHE TM=', G12.5, ' CEK. ***')
C        IF ( TIME .LT. TM ) TM = TIME
C=*********************************************************************
C----------------------------------------------------------------------
       HOUTTX = HOUTT * 1.8E+4
       WRITE(NWTR, 5667 ) HOUTT, HOUTTX
5667   FORMAT(/' *** HAEH EPEMEHHX E OPEEEH C AOM',
     = 1X,G12.5, ' (', G12.5, ') ***'/)
C----------------------------------------------------------------------
C****CTPAXOBKA*********************************************************
       IF ( TM .LE. T0 ) WRITE(NWTR,5666) T0, TM
5666  FORMAT(//' *** HTEPBA HAEH [T0,TM]:=> ','[',G12.5,',',G12.5,
     =   '] ***'/ ' *** OPEEEH OOHO --> "# - ',
     =        ' HOPPETC ***'//)
       IF ( TM .LE. T0 ) RETURN
C**********************************************************************
C-->  OTKPT EAAT
       CALL PPNCOM(3,NWTR)
C
         KOD = 1
         CALL DRPR02(KOD, SPIS, TIME, TOKN, VALLN )
         KXX = 0
C-->  K EAT
206      CONTINUE
         KOD = 2
         CALL READ00(KOD, TIME, TOKN, VALLN )
         GO TO (207, 2000, 3000 ), KOD
         CALL SUBERR(KOD)
207      CONTINUE
C-->  HAA  TOKA
         IF ( TIME .LT. T0 ) GO TO 206
         IF ( TIME .GT. TM ) GO TO 208
C   TOKA HAA
         KOD = 2
         IF ( KXX .EQ. 0 )
     =     CALL DRPR02(KOD, SPIS, TIME, TOKN, VALLN )
         KTXX = KTXX + 1
         KXX = KXX + 1
         IF ( KXX .EQ. KOUT ) KXX = 0
         GO TO 206
208      CONTINUE
         KOD = 3
         CALL DRPR02(KOD, SPIS, TIME, TOKN, VALLN )
C===>          PEKTBA BOHEHA
         RETURN
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
2000     CONTINUE
C--->   O. KOHE, BCEO B AE KTXX TOEK
         WRITE(NWTR, 54) KTXX
54       FORMAT(/' *** OCTHT KOHE AOB HAKOEH    ***'/
     = ' *** B APOEHHOM AOOHE OT T0 O TM ***'/
     =          ' *** OHAPEHO BCEO ', I5, ' TOEK PEEH ***'/)
C
       GO TO 208
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
3000     CONTINUE
C-->  . KOHE
         WRITE(NWTR,55) KTXX,         TIME
55       FORMAT(/' *** A HAKOEH PEEH HE  AKPT ***'/
     = ' *** B APOEHHOM AOOHE OT T0 O TM ***'/
     = ' *** OHAPEHO BCEO ', I5, ' TOEK PEEH ***'/
     = ' *** OCEH TOKA OPEEEHA  T =',G12.5, ' CEK. ***'/
     = ' *** PAOTA PEPBAETC ***********************************'//)
         call STOPP(16)
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
         END

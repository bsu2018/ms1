           SUBROUTINE MSGCON(I)
c-------------------------------------------------------------
C  ВЫДача сообщения на консоль
c  используется ANSI.SYS
c-------------------------------------------------------------
       COMMON /PROF1/ MASPRT(200)
       EQUIVALENCE ( TIME, MASPRT(29) )
C
           IF ( I .EQ. 1 ) THEN
           WRITE(99,*)  ''
c          WRITE(99,*)   '[0;0HВнимание: создается контрольная точка длX
c    Xя времени модели ',TIME, ' cek.'
           WRITE(99,1)   TIME
1     format(            '[0;0HВнимание: создается контрольная точка длX
     Xя времени модели ',g12.5,' cek.')
           RETURN
           ENDIF
           IF ( I .EQ. 2 ) THEN
C          WRITE(99,*)  ''
c          WRITE(99,*)   '[0;0HВнимание: контрольная точка  создана  длX
c    Xя времени модели ',TIME, ' cek.'
           WRITE(99,2)   TIME
2     format(            '[0;0HВнимание: контрольная точка  создана  длX
     Xя времени модели ',g12.5, ' cek.')
           RETURN
           ENDIF
           END

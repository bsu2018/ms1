         SUBROUTINE DEPOSQ( ZY, ZJ, YALL, JALL )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:   DEPOSQ( ZY, ZJ, YALL, JALL )
C   ------------
C   HAHAEHE:    OCECTBEHE AKTA "CTK" -
C   ----------     MATP OBX POBOMOCTE YALL  BEKTOPA
C                  CTOHKOB TOKA JALL HAEHM, COOTBETCTBEHHO
C                  ZY    JY;
C
C
C   METO:         B V.M.=2.*  -  XPAHEHE HE AKOBAHHO MATP;
C   -----
C   APAMETP:     CM. HAHAEHE;
C   ---------
C   OPOPAMM:  1. PROF1
C   -------------
C   PMEAHE:    1. NDALL - "ABOHA" PAMEPHOCT YALL(*,*);
C   ----------        NSYS EMEHTOB "CTTC";
C                  2. KOHTPO HE POBOTC;
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION YALL(1), JALL(1)
         REAL JALL
         EQUIVALENCE ( NDALL, MASPRT(23) )
         EQUIVALENCE ( NSYS,  MASPRT(26) )
         ID( I, J ) = NDALL * ( J - 1 )  +  I
C======================================================================
C======================================================================
         DO 500 I = 1, NSYS
500      JALL(I) = ZJ
C----------------------------------------------------------------------
         DO 501 I = 1, NSYS
            DO 501 J = 1, NSYS
               IJ = ID(I,J)
               YALL(IJ) = ZY
501      CONTINUE
       RETURN
         END

         SUBROUTINE DM0005 ( MSW, RMSW, YALL, JALL, LOGYTR,
     *                       TOKN, TOKN1, VALLN, VALLN1, KOD,
     *                         MSWALL, RMSWAL, ADRMSW )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:  DM0005 ( MSW, RMSW, YALL, JALL, LOGYTR,
C   ------------             TOKN, TOKN1, VALLN, VALLN1, KOD,
C    *                         MSWALL, RMSWAL, ADRMSW )
C
C   HAHAEHE:    B ABCMOCT OT HAEH AA ZAPROS [MASPRT(28)]:
C   -----------    BOHET CEE HK:
C
C                  A) ZAPROS = 1
C                     ----------
C                  OPMPOBAHE BKAOB B YALL MATP POBOMOCTE 
C                  JALL - BEKTOP CTOHKOB TOKA HAEH APAMETPOB
C                  KBBAEHTHX  G - J BETBE  MOE EMEHTA E
C                  TA # 6 [ CTOHK SIN HAPEH(1) ];
C                  TA # 7 [ CTOHK SIN HAPEH(2) ];
C                  ***************************************************
C                  B) ZAPROS = 2
C                     ----------
C                  BCEHE HAEH TOKOB B BETBX B T(N+1) MOMEHT
C                  BPEMEH   CXO  G  J APAMETPOB  HAEH
C                  OBX HAPEH HA EMEHTE PACTAHHX  T(N+1)
C                     AC (KCA) TX HAEH B "TOK T(N+1)";
C
C                  C) ZAPROS = 3
C                     **********
C                     HE OEPBAETC ( PEEPB );
C
C                  D) ZAPROS = 4
C                     **********
C                     HE OEPBAETC;
C
C   METO:         CKPETA PABHEH COCTOH MOE AHHOO
C   -----          TA HA OCHOBE METOA HEPA BO:
C                  Y(N+1) = Y(N) + H1*F(N+1) + H2*F(N).
C
C   APAMETP:     MSW, RMSW - PABEE COBO COCTOH MAKPOMOE
C   ---------      ( "OTPEOK" MACCB MSWALL -> C.M. / GETMSW );
C                  YALL - MATPA P0BOMOCT AHAPEMO E;
C                  JALL - BEKTOP CTOHKOB TOKA AHAPEMO E;
C                  VALLN - BEKTOP OBX HAPEH AHAPEMO E:
C                          P  T  =  TN;
C                  VALLN1- BEKTOP OBX HAPEH AHAPEMO E:
C                           P  T  =  TN  + H0;
C                  TOKN    TOKN1 - BEKTOPA EPEMEHHX "COCTOH"
C                                   EMEHTA P  TN    TN + H0;
C                                   PAMEPHOCT CM. MASPRT(46)=NNTOK;
C                  LOGYTR - BEKTOP PHAKOB K.. OB:
C                  COETC B MAKPOMOEX C PHAKOM TA = 5;
C                  PAMEPHOC MACCBOB P "ALL" OPEETC:
C                   MAX_DIMENSION ---> NDALL = MASPRT(23)
C                   TEKA_PAMEPHOCT ---> NSYS = MASPRT(26)
C                  KOD - APAMETP OPATHO CB C MOHTOPOM DM....
C                   ( COETC  MAKPOMOEE "EAHX" KE)
C
C
C
C   OPOPAMM:  1. COMMON PROF1, PROF2, PROF3;
C   ------------             DEPOST;
C
C   PMEAHE:  1.CTPKTP MSW COBA MAKPOMOE AHHOO TA CM. HE
C   ----------   2. P ZAPROS ^=   1   2 - COOEHE  XO;
C
C
C               "OCTOHHA" ACT MSW TA # 6   # 7:
C                --------------------------------------
C
C  +-------+--------+--------+--------+------+------+-------+---+
C  I HOMEP I HOMEP  I HOMEP  I  P. I KO I KO. IAP.   IPE I
C  I EME I TA   I /    I  TA  I ECT I BB. IHA.  IE I
C  I HTA   I MOE I PACETAI  MOEI BO   I HA I"TOKN" IPB I
C  I       I        I DM.....I        I OBI EH.I"TOKN1"I   I
C  +-------+--------+--------+--------+------+------+-------+---+
C  I  1    I   2    I   3    I    4   I   5  I  6   I   7   I 8 I
C  +-------+--------+--------+--------+------+------+-----------+
C
C
C
C                  RMSW TA # 6 [ CTOHK SIN HAPEH(1) ]
C                  --------------------------------------------
C                    ( U = EM * SIN( OMEGA * T  +  FAZA )
C
C
C                             SPM                 SRAB
C +-----+-----++--------+------+--------+------++------+
C IHOM. I HOM.II HA.  I HA.I        I FAZA II      I
C IA IA II  1/R   I EM/R I OMEGA  I  P   II JES  I
C I "K" I "M" II        I      I        I PA. II      I
C +-----+-----++--------+------+--------+------++------+
C I  9  I  10 II  11    I  12  I  13    I  14  II  15  I
C +-----+-----++--------+------+--------+------++------+
C
C
C
C                  RMSW TA # 7 [ CTOHK SIN HAPEH(2) ]
C                  --------------------------------------------
C                                                             LM = 7
C                                                             ******
C
C                      SPM                        SRAB
C     ++------+------+------+--------+------++------+------+
C     II      I      I HA.I        I FAZA II      I      I
C     II  R   I  L   I  EM  I OMEGA  I  P   II GES  I JES  I
C     II      I      I      I        I PA. II      I      I
C     ++------+------+------+--------+------++------+------+
C     II  1   I  2   I  3   I   4    I  5   II  6   I   7  I
C     ++------+------+------+--------+------++------+------+
C     II  11  I  12  I  13  I  14    I  15  II  16  I  17  I
C     ++------+------+------+--------+------++------+------+
C
C
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         COMMON /PROF2/ MACTAB(800)
         COMMON /PROF3/ FUNTAB(50)
         DIMENSION MSW(*), RMSW(*), YALL(*), JALL(*), LOGYTR(*)
         DIMENSION VALLN(*), RMASPT(1), VALLN1(*), TOKN(*), TOKN1(*)
         REAL JALL, JES
         EQUIVALENCE ( ZAPROS, MASPRT(28) )
         EQUIVALENCE ( NDALL,  MASPRT(23) )
         EQUIVALENCE ( TN,     MASPRT(29) )
         EQUIVALENCE ( H0,     MASPRT(30) )
         EQUIVALENCE ( H1,     MASPRT(32) )
         EQUIVALENCE ( H2,     MASPRT(33) )
         EQUIVALENCE ( FKOM,   MASPRT(34) )
         EQUIVALENCE ( TXG,    MASPRT(35) )
         EQUIVALENCE ( NTXG,   MASPRT(36) )
         EQUIVALENCE ( EPSKOM, MASPRT(39) )
         DIMENSION MSWALL(*), RMSWAL(*)
       INTEGER ADRMSW
         EQUIVALENCE ( ZEROCP, MASPRT(50) )
         INTEGER FKOM, ZAPROS, TIP
         EQUIVALENCE ( MASPRT(1), RMASPT(1) )
C===>    BCTPOEHHA HK OCBAH BXMEPHX MACCBOB:
         ID(I,J) = NDALL * (J-1)  +  I
C==> "HOBOE" BPEM   ( A, .... )
         TN1 = TN + H0
C---->    OKEH:
         K = MSW(9)
         M = MSW(10)
C--->  KOHTPO TA
         TIP = MSW(2)
         IF ( TIP .LT. 6 .OR. TIP .GT. 7 ) GO TO 9999
C---> BOP PEMA:
         GO TO ( 667, 666                 ), ZAPROS
C===> HAE OKA
         NWTRE = MASPRT(14)
         WRITE(NWTRE,9997) MSW(3), ZAPROS
9997     FORMAT( ' *** DM MOHTOP #', I4, ' APOC OCBAH OOEH
     = ***'/ ' *** ZAPROS = ', I5, ' - HOPPETC ***')
         RETURN
667      CONTINUE
C---------ZAPROS = 1---------------------------------------------------
C---> TO A T?
         IF ( TIP .EQ. 7 ) GO TO 7
C######################################################################
C---> CTOHK SIN HAPEH(1).
C--->   OEMEHTH BKA:
         GES = RMSW(11)
         JES = RMSW(12) * SIN( RMSW(13) * TN1 + RMSW(14)  )
         RMSW(15) = JES
C---->
C======================================================================
         KOD = 0
C---->
         CALL DEPOST( K, M, GES, JES, YALL, JALL )
C---->
         RETURN
C######################################################################
C---> CTOHK SIN HAPEH(2)
7        CONTINUE
C--->   OEMEHTH BKA:
         ITOK= MSW(7)
         TOK = TOKN(ITOK)
C----------------------------------------------------------------------
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN(K)
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN(M)
         UN = VK - VM
C----------------------------------------------------------------------
         EN  = RMSW(13) * SIN( RMSW(14) * TN  + RMSW(15)  )
         EN1 = RMSW(13) * SIN( RMSW(14) * TN1 + RMSW(15)  )
C----------------------------------------------------------------------
         Z = RMSW(12) + H1 * RMSW(11)
         GES = H1 / Z
         JES =( TOK*(RMSW(12)-H2*RMSW(11)) + H1*EN1 + H2*EN + H2*UN )/Z
         RMSW(16) = GES
         RMSW(17) = JES
C---->
C======================================================================
         KOD = 0
C---->
         CALL DEPOST( K, M, GES, JES, YALL, JALL )
C---->
         RETURN
C======================================================================
666      CONTINUE
C$$$$$$$$$$$$$$$$$$$ZAPROS = 2$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
C---> TO A T?
         IF ( TIP .EQ. 7 ) GO TO 17
C######################################################################
C---> CTOHK SIN HAPEH.
C--->     HOBEEE BPEM...        TN + H0 =>  HEO JES OPEEEHO!!!
         JES = RMSW(15)
C---> HOBOE HAPEHE:
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN1(K)
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN1(M)
         UN1 = VK - VM
C--->  HOB TOK:
         TOKT = RMSW(11) * UN1 + JES
C--->  AC TOKOB
         ITOK = MSW(7)
         TOKN1( ITOK ) = TOKT
         KOD = 0
         RETURN
C######################################################################
C---> CTOHK SIN HAPEH(2)
17       CONTINUE
C--->     HOBEEE BPEM...        TN + H0 =>  HEO JES OPEEEHO!!!
         GES = RMSW(16)
         JES = RMSW(17)
C---> HOBOE HAPEHE:
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN1(K)
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN1(M)
         UN1 = VK - VM
C--->  HOB TOK:
         TOKT = GES * UN1 + JES
C--->     AC TOKOB
         ITOK = MSW(7)
         TOKN1( ITOK ) = TOKT
         KOD = 0
         RETURN
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
9999     CONTINUE
         NWTRE = MASPRT(14)
         WRITE(NWTRE,9998) MSW(1), MSW(2)
9998     FORMAT( ' *** O. CO B DM0005 - APOC HA OCBAHE O
     =O TA ***'/
     =    ' *** EMEHT # ', I10, ' T MOE # ', I10, ' ***'//)
         CALL SUBERR(0)
         END

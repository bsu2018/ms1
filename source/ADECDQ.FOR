       SUBROUTINE ADECDQ( KD, KQ, IER )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT    MA    1988 .)-----------
C   OPOPAMMA: ADECDQ( KD, KQ, IER )
C   -------------
C   HAHAEHE:  POPAMMA EPEBOT EOE BOHOE CO  KD ,
C   ----------   AAHHOE B "10" OPME B "8"  OPM.
C
C   METO:         EPEKOPOBKA.
C   -----
C
C   APAMETP:  BXOHE :
C  ----------      KD  - AAHHOE BOHOE CO B "10# OPME;
C
C               BXOHE :
C                  KQ  - PEOPAOBAHHOE BOHOE CO B "8"
C                        OPM ;
C                  IER - A OK:
C                        IER=0 - OOK HET;
C                        IER=1 - AAHHOE BOCMPHOE CO OE
C                                CA "177777", TO COOTBETCTBET
C                                MAKCMAHOM C 16-T PAPHOO

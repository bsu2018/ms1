C   ------------
C
C   PMEAHE:    1. KOHTPO HEPABHOCT AC BOCMPHOO
C   -----------       CA HE POBOTC.
C
CZZZ-------------------------------------------------------------------
       IER = 0
       IF( KD .GT. 65535  ) GO TO 9999
C--> PAOEM O PAPAM.
       KD5 = KD / 32768
       KDP = KD  - KD5 * 32768
       KD4 = KDP/ 4096
       KDP = KDP - KD4 * 4096
       KD3 = KDP/ 512
       KDP = KDP - KD3 * 512
       KD2 = KDP/ 64
       KDP = KDP - KD2 * 64
       KD1 = KDP/ 8
       KD0 = KDP - KD1 * 8
C--> PA O PAPAM, TEEP HAEM ECTHOE CO.
       KQ = KD0 + 10*KD1 + 100*KD2 + 1000*KD3 + 10000*KD4 + 100000*KD5
       RETURN
C----------------------------------------------------------------------
9999   CONTINUE
       IER = 1
       RETURN
       END

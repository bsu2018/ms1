         SUBROUTINE DM0021 ( MSW, RMSW, YALL, JALL, LOGYTR,
     *                       TOKN, TOKN1, VALLN, VALLN1, KOD,
     *                         MSWALL, RMSWAL, ADRMSW )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT   MA   1987 .)------------
C   OPOPAMMA:  DM0021 ( MSW, RMSW, YALL, JALL, LOGYTR,
C   -------------            TOKN, TOKN1, VALLN, VALLN1, KOD,
C                              MSWALL, RMSWAL, ADRMSW )
C
C   HAHAEHE:    MOHTOP:
C   -----------      MOE BEHT, P. C [MO.# 93,.T #6]
C                    MOE KA  , P. C [MO.#668,.T #6]
C                  ****************************************************
C
C                  B ABCMOCT OT HAEH AA ZAPROS [MASPRT(28)]:
C                  BOHET CEE HK:
C
C                  A) ZAPROS = 1
C                     ----------
C                 -  OPMPOBAHE BKAOB B YALL MATP POBOMOCTE
C                   HAEH "POBOMOCT" AKPTOO BEHT ( GVENT6 )
C                   [ B AHHO BEPC KOMEKCA ];
C                  - OPMPOBAHE BEKTOPA K.. OB "LOGYTR"
C                     HEHTHX OB --> COETC
C                     /  GETLOG --> OCECTBT "EMEHTAPH" BKA;
C
C                  1A) ZAPROS = 10
C                      ------------
C                      OPMPOBAHE "TEHEBO" CCTEM PABHEH
C                       ( OEMEHTH BKA )
C
C                  B) ZAPROS = 20
C                     ------------
C                  BCEHE HAEH TOKOB B BETBX B T(N+1) MOMEHT
C                  BPEMEH. P TOM TOK OTKPTX BEHTE:( K  M )
C                  <TOKN1>:= <VALLN1[K]> - <VALLN1[M]>;
C                   TOK AKPTX BEHTE = 0.0;
C                  AC TX TOKOB B MACCB TOKN1;
C                   !!! BOHT O MOKA PEEH VALLN1 !!!
C
C                  C) ZAPROS = 3
C                     -----------
C                     OBOTC AHA HA BOMOH CMEH COCTOH
C                     BEHT [ OTKPT <==> AKPT ]:
C                     P TOM: FKOM = 0 - COCTOHE HE MEHOC;
C                               FKOM = 1 - HA TEKEM AE COCTOHE
C                                         OHO "O" MEHTC B
C                                         B T = TX MOMEHT BPEMEH;
C                                         PEAHO B POPAMME OPEE-
C                                         ETC HX - A BOMOHO
C                                         KOMMTA, ATEM TX=TN+HX;
C                                         P HX < HXMIN [MASPRT(62)]
C                                         PHMAETC HX = HXMIN;
C                                         COCTOHE HX=0.0 BBAET
C                                         EAT COOEH;
C                                         POBEEH COTBETCTBE
C                                         MEHEH B MASPRT:
C                                          FKOM <== "1"
C                                         HXG = MIN( HX, HXG )
C                                         TXG = TN + HXG
C
C              ET: ICLOSE - MO TOKA "AKPT" BEHT;
C                    UOPEN  - MO HAPEH "OTKPT" BEHT;
C
C
C                  D) ZAPROS = 4
C                     **********
C                   - P TX_G  [MASPRT(35)] .EQ. HX[RMSW(15)] -
C                      KCPETC CMEHA COCTOH BEHT APAMETPOM
C                      KD [MSW(14)];
C                     PABEHCTBO POBEPETC C TO. O "EPSKOM".
C
C   METO:         PAHOBHOCT BOHOBOO AOPTMA  OPMPOBAH
C   -----          MACCBA LOGYTR --> EE / GETLOG;
C
C                  "TEHEBA" CCTEMA OPMPETC "MACTAPOBAHHA"
C                   TAK, TO: V[X] - V[Y] = <TOK    X-Y   BETB>;
C                   , OTMEEHHE B LOGYTR(*) = 0 - "AOBE"
C                    KAO HEHTHO P OTKPTX BEHTE;
C
C   APAMETP:     MSW, RMSW - PABEE COBO COCTOH MAKPOMOE
C   ---------      ( "OTPEOK" MACCB MSWALL -> C.M. / GETMSW );
C                  YALL - MATPA P0BOMOCT AHAPEMO E;
C                  JALL - BEKTOP CTOHKOB TOKA AHAPEMO E;
C                  VALLN - BEKTOP OBX HAPEH AHAPEMO E:
C                          P  T  =  TN;
C                  VALLN1- BEKTOP OBX HAPEH AHAPEMO E:
C                          P  TN + H0;
C                  TOKN   TOKN1 - HAEHE EPEMEHHX "COCTOH"
C                                  MOE P TN    TN + H0;
C                          ( PAMEPHOCT -> CM. MASPRT(46) = NNTOK )
C                  LOGYTR - BEKTOP PHAKOB K.. OB:
C                  COETC B MAKPOMOEX C PHAKOM TA = 6;
C                  PAMEPHOC MACCBOB P "ALL" OPEETC:
C                   MAX_DIMENSION ---> NDALL = MASPRT(23)
C                   TEKA_PAMEPHOCT ---> NSYS = MASPRT(26)
C                  KOD - APAMETP OPATHO CB C MOHTOPOM DM....
C
C
C
C
C   OPOPAMM:  1. COMMON PROF1, PROF2, PROF3;
C   ------------   2. GETLOG,  SEQSOS, DEPOST;
C
C   PMEAHE:  1.CTPKTP MSW COBA MAKPOMOE AHHOO TA CM. HE
C   ----------   2. P ZAPROS ^=1, 10,20,3,4 - BAA COOEH  XO
C                3. P FTEST = 1 -> EAT HA WTRE AKTA CMEH COCTOH
C
C               "OCTOHHA" ACT MSW TA # 93  # 668
C                ---------------------------------------
C
C  +-------+--------+--------+--------+------+------+-------+---+
C  I HOMEP I HOMEP  I HOMEP  I PHAKI KO I KO. IAP.   IPE I
C  I EME I TA   I /    I TA   I ECT I BB. IHA.  IE I
C  I HTA   I MOE I PACETAI MOE I BO   I HA I"TOKN" IPB I
C  I       I        I DM.....I        I OBI EH.I"TOKN1"I   I
C  +-------+--------+--------+--------+------+------+-------+---+
C  I  1    I   2    I   3    I    4   I   5  I  6   I   7   I 8 I
C  +-------+--------+--------+--------+------+------+-------+---+
C
C
C
C
C
C
C
C
C        RMSW TA # 93  # 668 [ BEHT(K), P. C ]
C        -----------------------------------------------------
C    SPM   I                SRAB
C +----+---+------+-----+---+------+----+--------+----+
C INELTINSMI ATOKMI KD  IHX I MCIU I HX I        I Q  I
C I    I   I      I     I   I      ICIU I PEEPB I    I
C I    I   I      I     I   I      I    I        I    I
C +----+---+------+-----+---+------+----+--------+----+
C I  1 I 2 I   3  I  4  I 5 I   6  I  7 I   8,9  I 10 I
C +----+---+------+-----+---+------+----+--------+----+
C I 11 I12 I  13  I 14  I15 I  16  I 17 I  18,19 I 20 I
C +----+---+------+-----+---+------+----+--------+----+
C
C      BEHT OTKPT -> KD=1;    BEHT AKPT ->  KD=0;
C      HX - A O BPEMEH, BPEM BOMOHOO EPEKEH;
C
C        MCIU    HXCIU - APAMETP "BOCTAHOBEHHOO" MPCA "C"
C
C  Q - A KD;
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         COMMON /PROF2/ MACTAB(800)
         COMMON /PROF3/ FUNTAB(50)
         DIMENSION MSW(*), RMSW(*), YALL(*), JALL(*), LOGYTR(*)
         DIMENSION VALLN(*), RMASPT(1), VALLN1(*), TOKN(*), TOKN1(*)
         REAL JALL, JRL, L, IRLT, IRLN
         DIMENSION MSWALL(*), RMSWAL(*)
         INTEGER ADRMSW , ATOKM
         EQUIVALENCE ( RMASPT(1),MASPRT(1) )
         EQUIVALENCE ( HXMIN , MASPRT(62) )
         EQUIVALENCE ( NWTRE , MASPRT(14) )
         EQUIVALENCE ( GVENT6, MASPRT(59) )
         EQUIVALENCE ( FTEST , MASPRT(60) )
         EQUIVALENCE ( OPEN6 , MASPRT(57) )
         INTEGER FTEST, OPEN6
         EQUIVALENCE ( ZAPROS, MASPRT(28) )
         EQUIVALENCE ( NDALL,  MASPRT(23) )
         EQUIVALENCE ( TN,     MASPRT(29) )
         EQUIVALENCE ( H0,     MASPRT(30) )
         EQUIVALENCE ( H1,     MASPRT(32) )
         EQUIVALENCE ( H2,     MASPRT(33) )
         EQUIVALENCE ( FKOM,   MASPRT(34) )
         EQUIVALENCE ( TXG,    MASPRT(35) )
         EQUIVALENCE ( HXG,    MASPRT(61) )
         EQUIVALENCE ( NTXG,   MASPRT(36) )
         EQUIVALENCE ( EPSKOM, MASPRT(39) )
         EQUIVALENCE ( ZEROCP, MASPRT(50) )
         INTEGER FKOM, ZAPROS,TIP
         EQUIVALENCE ( ICLOSE, MASPRT(77) )
         EQUIVALENCE ( UOPEN,  MASPRT(78) )
         REAL ICLOSE
         TIP = MSW(2)
C--------------------------------------------------------------------
C---->    OKEH:
C--------------------------------------------------------------------
         K = MSW(9)
         M = MSW(10)
C--->  KOHTPO TA
         IF ( TIP .NE. 93  .AND. TIP .NE. 668 ) GO TO 9999
C--------------------------------------------------------------------
C---> BOP PEMA:
C--------------------------------------------------------------------
         IF ( ZAPROS .EQ. 1 ) GO TO 667
         IF ( ZAPROS .EQ. 10 ) GO TO 6670
         IF ( ZAPROS .EQ. 20 ) GO TO 6660
         IF ( ZAPROS .EQ. 3 ) GO TO 600
         IF ( ZAPROS .EQ. 4 ) GO TO 500
C--------------------------------------------------------------------
C===> HAE OKA
         NWTRE = MASPRT(14)
         WRITE(NWTRE,9997) MSW(3), ZAPROS
9997     FORMAT( ' *** DM MOHTOP #', I4, ' APOC OCBAH OOEH
     = ***'/ ' *** ZAPROS = ', I5, ' - HOPPETC ***')
         RETURN
667      CONTINUE
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
C---------------Z A P R O S = 1----------------------------------------
         KOD = 0
C---> "CPOC" HAEH TX - BOMOHOO BPEMEH EPEKEH.....
         RMSW(15) =  H0
         MSW(20) = 0
         KD = MSW(14)
C--->    EC "K"  "M" HEHTH --->  BBAT GETLOG.....
         IF ( KD .EQ. 0 ) GO TO 6607
C--->    ECT "KOHTAKT"  [ KD = 1 ]
         CALL GETLOG_( K, M, LOGYTR )
         RETURN
6607     CONTINUE
C--------------------------------------------------------------------
C===> BEHT(K) AKPT ==> KD=0 <=="MAKET" OEH MAO POBOMOCT
C---> EC NVENT6 = 0.0, TO MAKET HE HEH...
         IF ( GVENT6 .EQ. 0.0 ) RETURN
         G = GVENT6
C--------------------------------------------------------------------
C--->   OEMEHTH BKA:
         CALL DEPOST( K, M, G, 0.0, YALL, JALL )
         RETURN
C======================================================================
C$$$$$$$ ZAPROS = 10 $$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
C--->OPMPOBAHE "TEHEBO"CCTEM  P OTKPTX BEHTE(KE)
6670     CONTINUE
         KOD = 0
         KD = MSW(14)
         IF ( KD .EQ. 0 ) RETURN
C----------------------------------------------------------------------
         G = 1.0
C----------------------------------------------------------------------
C--->  BPOCT "AOBE"     HEHTHX P...
         IF ( K .EQ. 0 ) GO TO 6671
         IF ( LOGYTR(K) .EQ. -1 ) K = 0
6671     CONTINUE
         IF ( M .EQ. 0 ) GO TO 6672
         IF ( LOGYTR(M) .EQ. -1 ) M = 0
6672     CONTINUE
C======================================================================
C--->  BKABAEM....
         CALL DEPOST( K, M, G, 0.0, YALL, JALL )
         RETURN
C======================================================================
C$$$$$$ZAPROS = 20 $$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
C===> PACTAT TOK BEHTE !!! BOHT O MOKA VALLN1 !!!!
6660     CONTINUE
         KOD = 0
         ITOK = MSW(7)
         KD = MSW(14)
         IF ( KD .EQ. 0 ) GO TO 6661
C--->  BEHT(K) OTKPT, BPOCT "AOBE"  O AHHM LOGYTR..
         IF ( K .EQ. 0 ) GO TO 6662
         IF ( LOGYTR(K) .EQ. -1 ) K = 0
6662     CONTINUE
         IF ( M .EQ. 0 ) GO TO 6663
         IF ( LOGYTR(M) .EQ. -1 ) M = 0
6663     CONTINUE
         TK = 0.0
         IF ( K .NE. 0 ) TK = VALLN1(K)
         TM = 0.0
         IF ( M .NE. 0 ) TM = VALLN1(M)
         TOKN1(ITOK) = TK - TM
         RETURN
6661     CONTINUE
C--->  BEHT (K)  AKPT....
         TOKN1(ITOK)  =  0.0
         RETURN
C======================================================================
600      CONTINUE
C---- Z A P R O S = 3 -----TOKN, TOKN1, VALLN, VALLN1 OPEEEH-------
C-----------------------------------------!!! VALLN1 MOPOBAHO !!!
C-->>  OPEET CHA "C", AOMHT MCIU   HXCIU....
         ITOK = MSW(7)
         ATOKM = MSW(13)
         MCIU  = IFIX ( TOKN1(ATOKM) )
         HXCIU = TOKN1( ATOKM + 6 )
         MSW(16) = MCIU
         TOKXX = 0.0
         IF ( MCIU .EQ. 01  .OR. MCIU .EQ. 11 ) TOKXX = 1.0
         TOKN1( ITOK+1 ) = TOKXX
         RMSW(17) = HXCIU
         IF( TIP .EQ. 668 ) GO TO 6015
C--------------------------------------------------------------------
C----OPEEM TEK.  PE. COCTOH (?)----------------------------
C===>    HOBOE HAPEHE  TOK
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN1(K)
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN1(M)
         UT = VK - VM
         TVT  = TOKN1( ITOK )
C--> A PAHE O...
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN(K)
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN(M)
         UN = VK - VM
         TVN  = TOKN( ITOK )
C======================================================================
C----AMEMC COCTOHM----------------------------------------------
6015     CONTINUE
         KD = MSW(14)
         IF ( KD .EQ. 0 ) GO TO 601
         IF ( TIP .EQ. 668 ) GO TO 6016
C----------------------------------------------------------------------
C---> BEHT OTKPT (KD=1), HE XOET  OH AKPTC?
C  KOPPEKTPOBKA TVN  T 93
C----------------------------------------------------------------------
       IF ( TVN .LT. 0.0  .AND. TVN .LE. -ICLOSE ) TVN = 0.0
       IF ( TVT .LT. -ICLOSE ) GO TO 602
C----------------------------------------------------------------------
C---> HAE COCTOHE BEHT HE MEHOC....
         KOD = 0
         RETURN
6016     CONTINUE
C----------------------------------------------------------------------
C---> K OTKPT (KD=1), HE XOET  OH AKPTC?
C----------------------------------------------------------------------
       MCIU = MSW(16)
       IF ( MCIU .EQ. 10 .OR. MCIU .EQ. 00 ) GO TO 602
C----------------------------------------------------------------------
C---> HAE COCTOHE KA HE MEHOC....
         KOD = 0
         RETURN
602      CONTINUE
C--->  BEHT(K)   #AKPC#  B TEEH BPEMEH OT TN O TN+H0....
C      KOA TO MOO CTC?   ( HX - PEAHO BOHEHH A )....
C !!!! CPABEBO P TVN = (-ICLOSE) !!!
         IF ( TIP .EQ.  93 ) HX = H0 * ( (-ICLOSE - TVN) /(TVT - TVN))
         IF ( TIP .EQ. 668 ) HX = RMSW(17)
C!!!-------------------------------------------------------------------
         IF ( HX .LT. 0.0 ) WRITE (NWTRE, 604 ) MSW(1), KD, TN, H0, HX
604   FORMAT(' *** EMEHT # ', I5, ' MOE "BEHT(K)->COMHEH ?'
     = , ' ***'/' *** KD = ', I2, ' TN = ', E12.5, ' H0 = ', E12.5,
     =  ' HX = ', E12.5, ' ***')
C!!!-------------------------------------------------------------------
         IF ( HX .LT. HXMIN ) HX = HXMIN
         RMSW(15) = HX
C--->  EC TX .LT. TX_G  ==> TO B TX_G  <=== TX...
         HXG  =  AMIN1( HX, HXG )
C                ****************
         TXG = TN + HXG
C====>  OTMETT TO B "FKOM"....
         FKOM = 1
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
         MSW(20) = -1
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
         KOD = 0
         RETURN
C----------------------------------------------------------------------
601      CONTINUE
         KOD = 0
         IF ( TIP .EQ. 668 ) GO TO 6017
C===> BEHT AKPT (KD=0), HE EAET  OH OTKPTC?....
         IF( UT .LE. UOPEN ) GO TO 6030
C======================================================================
C--->  TO EAET "C" ???????????????
         MCIU = MSW(16)
         IF ( MCIU .EQ. 01 ) GO TO 603
C-->  MOET T M ABHO "XOEM" ?
         IF ( MCIU .EQ. 11 ) GO TO 603
C======================================================================
C@@@@@@-->> OT 06.01.87. <<-@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
C-->  OPAOTKA KOHA....
       IF ( MCIU .EQ. 00 ) GO TO 6030
C-->   MCIU = 10  -->  C "KOHAET"
       HX = 0.0
       IF ( UN .GT. UOPEN ) GO TO 606
       HX = +H0 * ( (UOPEN - UN) / (UT-UN) )
       HXCIU = RMSW(17)
       IF ( HX .LE. HXCIU ) GO TO 606
6030   CONTINUE
C@@@@@@--> OT 06.01.87. <<-- KOHE-@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
C===>  HET, HE BOEA,  HE A --> COCTOHE HE MEHETC...
         KOD = 0
         RETURN
6017     CONTINUE
C===> K AKPT (KD=0), HE EAET  OH OTKPTC?....
C-> OTKPOETC P MCIU => 01    11
         MCIU = MSW(16)
         IF ( MCIU .EQ. 01  .OR.  MCIU .EQ. 11 ) GO TO 603
C===>  HET, HE BOEA, COCTOHE HE MEHETC...
         KOD = 0
         RETURN
603      CONTINUE
C======================================================================
C===>   BEHT(K) B TEEH TN  <-->  TN+H0 OEH OTKPTC ...
C       KOA OBC "C" ???????????
         HXCIU = RMSW(17)
         HX = HXCIU
         IF ( TIP .EQ. 668  ) GO TO 606
         IF ( UN .GT. UOPEN ) GO TO 606
C--->  UN    UT  "PAHOO" HAKA...
C   HX  ->  "A" "ECTECTBEHHO" KOMTA
         HX  =    + H0 * (  ( UOPEN -  UN ) /(UT - UN)  )
C-->  KOA EM "C" PAPEO...
         IF ( HXCIU .GT. HX ) HX = HXCIU
606      CONTINUE
C!!!-------------------------------------------------------------------
         IF ( HX .LT. 0.0 ) WRITE (NWTRE, 605 ) MSW(1), KD, TN, H0, HX,
     =                                 MCIU
605   FORMAT(' *** EMEHT # ', I5, ' MOE "BEHT(K)->COMHEH ?'
     = , ' ***'/' *** KD = ', I2, ' TN = ', E12.5, ' H0 = ', E12.5,
     =  ' HX = ', E12.5, ' MCIU = ', I2, ' ***')
C!!!-------------------------------------------------------------------
         IF ( HX  .LT. HXMIN ) HX  = HXMIN
         RMSW(15) = HX
         HXG =  AMIN1( HX, HXG )
C               ****************
         TXG = TN + HXG
         FKOM = 1
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
         MSW(20) = -1
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
         KOD = 0
         RETURN
C----------------------------------------------------------------------
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
500      CONTINUE
         KOD = 0
C===> KCA CMEH COCTOH MOE.................ZAPROS = 4
C---->  HET  HEOXOMOCT POBECT CMEH COCTOH BEHT(KA)?
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
        IF ( MSW(20) .NE. -1 ) RETURN
C@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
C       EC TXG   "PMEPHO" PABHO TX=MOE, TO CMEHA HHA!
         HX   = RMSW(15)
C        TX - TXG == HX - HXG
         EPST = ABS( HX   - HXG )
         IF ( EPST .GT. EPSKOM ) RETURN
C===> HAE CMEHM COCTOHE....
         KD = MSW(14)
         KD = KD + 1
         IF ( KD. EQ. 2 ) KD = 0
         MSW(14) = KD
C---->  EC BEHT (K)  AKPC, AKCPEM EO HOMEP.....
         IF ( KD .EQ. 0 ) NTXG = MSW(1)
C!!!-------------------------------------------------------------------
         IF ( FTEST .GE. 1 ) WRITE(NWTRE,501) MSW(1), KD, TN, TXG
501     FORMAT(' ***FTEST_1: B -TE #',I5, ' KD CTAO =',I2,
     =          ' TN = ', E12.5, ' TXG = ', E12.5, ' ***')
C!!!-------------------------------------------------------------------
         RETURN
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
9999     CONTINUE
         NWTRE = MASPRT(14)
         WRITE(NWTRE,9998) MSW(1), MSW(2)
9998     FORMAT( ' *** O. CO B DM0021 - APOC HA OCBAHE O
     =O TA ***'/
     =    ' *** EMEHT # ', I10, ' T MOE # ', I10, ' ***'//)
         CALL SUBERR(0)
          RETURN
         END

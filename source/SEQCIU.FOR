       SUBROUTINE SEQCIU( TXK, TXA, CXK, CXA, IKN, IKN1, IAN, IAN1,
     *                    IFXK, IFXA, EYYN, EYYN1 )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA: SEQCIU( TXK, TXA, CXK, CXA, IKN, IKN1, IAN, IAN1,
C  --------------         IFXK, IFXA, EYYN, EYYN1 )
C
C   HAHAEHE : PABEHE PAEHO PAOTO BEHTHX P,
C   -----------   *** BEPTKAHOE PABEHE ***
C
C   METO:  OOHAEHE  IH(T) = IA(T) - IK(T);
C   -----
C              P  ABS( IH(TN) ) <= IYSTAV ---> "PAPEEH":
C                      EYYN1 > 0  OHA "A" PA;
C                      EYYN1 < 0  OHA "K" PA;
C                      EYYN1 = 0  OHA "A" PA  "K" PA;
C
C              P ABS( IH(TN+H0) ) <= IYSTAV ---> "PET":
C                      EYYN1 > 0  HE OHA "A" PA;
C                      EYYN1 < 0  HE OHA "K" PA;
C                      EYYN1 = 0  HE OHA "A" PA  "K" PA;
C                                 (????????????????????????????????)
C
C  !!!  TXA/K MEHETC B CAE ECTBTEHO CMEH CXA/K   !!!
C
C   APAMETP: CXK - A APEEH ( =0 ) / PAPEEH ( =1 ) "OA"
C   ---------        "C" HA K-P;
C              TXK - BPEM, HAHA C KOTOPOO ECTBET CXK A;
C              IFXK - A PAPEEH MEHEH HAEH TX B RMSW_50
C                     OCAH BEHT HA HAEHE TXK ( P =1 MOHO );
C                     A-P  --> AHAOHO;
C              EYYN, EYYN1 - HAEH PABX CHAOB B TN, TN+H0;
C              IKN, IKN1 - TOK K-P B TN, TN+H0;
C              IAN, IAN1 - TOK A-P B TN, TN+H0;
C                                                                      ;
C
C
C   OPOPAMM:   PROF1;
C   ------------
C   PMEAHE:    IYSTAV  MASPRT(105);
C   ----------
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         EQUIVALENCE ( TN,     MASPRT(29) )
         EQUIVALENCE ( H0,     MASPRT(30) )
         EQUIVALENCE ( NWTRE,  MASPRT(14) )
         EQUIVALENCE ( ICLOSE, MASPRT(78) )
         EQUIVALENCE ( IYSTAV, MASPRT(105) )
         REAL ICLOSE, IH, IH1
C----------------------------------------------------------------------
       REAL IYSTAV,IKN, IKN1, IAN, IAN1
       INTEGER CXA, CXK
C
       IFXK = 0
       IFXA = 0
       IH=IAN-IKN
       IH1=IAN1-IKN1
C**********************************************************************
       AIH1 = ABS( IH1 )
       AIH  = ABS( IH  )
C----------------------------------------------------------------------
       IF ( AIH .LE. IYSTAV ) GO TO 1
10     CONTINUE
       IF ( AIH1 .LE. IYSTAV ) GO TO 100
C--->  H EO HE MEHT B TOM CAE....
20     CONTINUE
       RETURN
C----------------------------------------------------------------------
1      CONTINUE
C-->  "PAPEEH" COACHO HAKA EYYN1......
       IF ( EYYN1 ) 2,3,4
C******
2      CONTINUE
C-->   < 0  OEH K.
       IF ( CXK .EQ. 1 ) GO TO 10
       CXK = 1
       IFXK = 1
       TXK = TN + H0
       GO TO 10
3      CONTINUE
C   = 0,  OEH  K  A..
       IF ( CXK .EQ. 1 ) GO TO 30
       CXK = 1
       IFXK = 1
       TXK = TN + H0
30     CONTINUE
       IF ( CXA .EQ. 1 ) GO TO 10
       CXA = 1
       IFXA = 1
       TXA = TN + H0
       GO TO 10
4      CONTINUE
C  >  0  OEH A....
      IF ( CXA .EQ. 1 ) GO TO 10
      CXA = 1
      IFXA = 1
      TXA = TN + H0
      GO TO 10
C----------------------------------------------------------------------
100    CONTINUE
C-->  APET  COACHO EYYN1......
       IF ( EYYN1 ) 5, 6, 7
5      CONTINUE
C  < 0  HE OEH A
       IF ( CXA .EQ. 0 ) GO TO 20
       CXA = 0
       IFXA = 1
       TXA = TN + H0
       GO TO 20
6      CONTINUE
C   = 0  HE OEH H K H A ( ????)
C@@@@@GO TO 20
       IF ( CXA .EQ . 0 ) GO TO  60
       CXA = 0
       IFXA = 1
       TXA = TN + H0
60     CONTINUE
       IF ( CXK .EQ. 0 ) GO TO 20
       CXK = 0
       IFXK = 1
       TXK = TN + H0
       GO TO 20
7      CONTINUE
C  > 0 HE OEH K...
       IF ( CXK .EQ. 0 ) GO TO 20
       CXK = 0
       IFXK = 1
       TXK = TN + H0
       GO TO 20
C**********************************************************************
C**********************************************************************
       END

      SUBROUTINE EQMAS(M1,I1,M2,I2,N,KODS)
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C    OPOPAMMA:  EQMAS(M1,I1,M2,I2,N,KODS)
C    ------------
C      HAHAEHE: OPOPAMMA CPABHEH N-EMEHTOB MACCBOB M1  M2,
C      ----------   HAHA  C EMEHTOB I1  I2 COOTBETCTBEHHO
C
C      METO:             OEBEH;
C      -----
C      APAMETP:   M1 - MACCB
C      ---------    M2 - MACCB
C                   I1 - HOMEP EPBOO EMEHTA MACCBA M1 C KOTOPOO
C                        HAHAETC CPABHEHE
C                   I2 - -//- MACCBA M2 -//-
C                   N - KOECTBO EMEHTOB CPABHEH
C                   KOD - KO CPABHEH:
C                         KOD=1 CPABHEHE CEHO
C                         KOD=0 CPABHEHE HE CEHO
C                                         **
C      OPOPAMM:  HET
C      ------------
C
C      PMEAHE:
C      -----------
CZZZ-------------------------------------------------------------------
      DIMENSION M1(1),M2(1)
      KODS=1
      DO 1 K=1,N
      K1=K-1
      IF (M1(I1+K1).EQ.M2(I2+K1)) GO TO 1
      KODS=0
      GO TO 2
    1 CONTINUE
    2 RETURN
      END

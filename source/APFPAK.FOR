       SUBROUTINE APFPAK ( FUN, IDKOD, FCAR )
CXXX ---KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C      OPOPAMMA: APFPAK ( FUN, IDKOD, FCAR )
C      ------------
C      HAHAEHE: POBOT KOPOBAHE 4-X EMEHTOB MACCBA FCAR
C      ----------  B CAT OPMAT - B OHO CO IDKOD  OPATHO;
C      METO:      PECTABEHE CA O ECTHM AAM;
C      ------
C      APAMETP: FUN - =1  O FCAR COAETC IDKOD,
C      ---------        =2  O IDKOD COAETC FCAR;
C                 IDKOD - P EHTKATOPA ( CHTE  4-X );
C                 FCAR  - MACCB, PAMEPHOCT 4, C EMEHTAM B F-KODE;
C                  BCE APAMETP B INTEGER*4; FCAR(I) <= 99;
C
C      OPOPAMM: HET; (SUBERR, COMMON: PRINTN)
C
CZZZ-------------------------------------------------------------------
C!!!---------------
         COMMON/PRINTN/ NW6
         DIMENSION FCAR(*)
         INTEGER FUN, FCAR
C!!!---------------
         DATA KP1 / 1 00 00 00 /,
     *        KP2 /    1 00 00 /,
     *        KP3 /       1 00 /
         GO TO ( 1, 2 ), FUN
C!!!--------------
         WRITE(NW6,10) FUN
10       FORMAT(' ***** CO P OPAEH K APFPAK, FUN=',I10)
         CALL SUBERR(1)
C!!!--------------
C     O FCAR  EAEM IDKOD
1        IDKOD = KP1 * FCAR(1) +
     *           KP2 * FCAR(2) +
     *           KP3 * FCAR(3) +
     *                 FCAR(4)
         RETURN
C   O  IDKOD  EAEM FCAR(1...4)
2        FCAR(1) = IDKOD/KP1
         IX1 = IDKOD - FCAR(1)*KP1
         FCAR(2) = IX1/KP2
         IX2 = IX1 - FCAR(2)*KP2
         FCAR(3) = IX2/KP3
         FCAR(4) =  IX2 - FCAR(3)*KP3
         RETURN
         END

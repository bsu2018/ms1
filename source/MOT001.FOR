	 SUBROUTINE MOT001(NDIM,N,A,X)					
CXXXX	 MOT001    -----------------------------------------------------
C    OPOPAMMA:  MOT001(NDIM,N,A,X)					
C    ------------							
C    HAHAEHE:    BCEHE MATP X, OPATHO MATP A;		
C		  P PAOTE MATPA A PAPAETC.			
C									
C    METO:	CKEHE E BOPA BEEO EMEHTA,		
C    ------	 A CTAETC HE BPOEHHO, KOHTPO8 HE POBOTC,	
C		 (  MEH8EH B. OEPA )			
C									
C    APAMETP: NDIM - ABOHA PAMEPHTCT8 MACCBOB A  X;		
C    ---------								
C		N - PAMEPHOCT8 MATP A  X;				
C		A - CXOHA MATPA ( PAPAETC ! );			
C		X - BCEMA, E OPATHA;				
C									
C    BBAEME OPOPAMM:	 HET.					
C    ------------------------						
C									
CZZZZ	 MOT001    ____________________________________________________ 
	  DIMENSION A(NDIM,NDIM),X(NDIM,NDIM)				
	  REAL*4 A,X,T,T1						
C	  INTEGER*4 I,J,K,N,NDIM					
C									
C  COAT8 EHH B X						
C									
	  DO 1 I=1,N							
	  DO 1 J=1,N							
	  X(I,J)=0.0							
	  IF ( I .EQ. J ) X(I,J)=1.0					
1	  CONTINUE							
C									
C  CKEH, BE BCEA AOHA8H				
C									
	  DO 10 K=1,N							
C									
C---									
C									
	  T = A(K,K)							
C---									
	  A(K,K)=1.0							
C									
C---									
C	 HOPMPOBKA CTPOK K						
C									
	  DO 11 J=1,N							
	  X(K,J)=X(K,J)/T						
	  IF ( J .LE. K ) GO TO 11					
	  A(K,J)=A(K,J)/T						
11	  CONTINUE							
C									
C  MEHEHE CTPOK							
C									
	  DO 12 I=1,N							
	  IF ( I .EQ. K) GO TO 12					
	  T1=A(I,K)							
	  A(I,K)=0.0							
	  DO 13 J=1,N							
	  X(I,J)=X(I,J)-X(K,J)*T1					
	  IF ( J .LE. K ) GO TO 13					
	  A(I,J)=A(I,J)-A(K,J)*T1					
13	  CONTINUE							
C---------								
12	  CONTINUE							
C--------								
10	  CONTINUE							
C--------								
	  RETURN							
	  END								
	  END								

       SUBROUTINE ADECQD( KQ, KD, KQ0, KQ1, KQ2, KQ3, KQ4, KQ5, IER )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT    MA    1988 .)-----------
C   OPOPAMMA: ADECQD( KQ, KD, KQ0, KQ1, KQ2, KQ3, KQ4, KQ5, IER )
C   -------------
C   HAHAEHE:  POPAMMA EPEBOT EOE BOHOE CO  KQ ,
C   ----------   AAHHOE B BOCMPHO OPME B ECTH OPM
C                C OHOBPEMEHHO OPAPHO PABKO AAHHOO
C                BOCMPHOO CA.
C
C   METO:         EPEKOPOBKA.
C   -----
C
C   APAMETP:  BXOHE :
C  ----------      KQ  - AAHHOE BOHOE CO B BOCMPHO OPME;
C
C               BXOHE :
C                  KD  - PEOPAOBAHHOE BOHOE CO B ECTH
C                        OPM ;
C                  KQ0 - PAP "0" BOCMPHOO CA ;
C                  KQ1 - PAP "1" BOCMPHOO CA ;
C                  KQ2 - PAP "2" BOCMPHOO CA ;
C                  KQ3 - PAP "3" BOCMPHOO CA ;
C                  KQ4 - PAP "4" BOCMPHOO CA ;
C                  KQ5 - PAP "5" BOCMPHOO CA ;
C                  IER - A OK:
C                        IER=0 - OOK HET;
C                        IER=1 - AAHHOE BOCMPHOE CO OE
C                                CA "177777", TO COOTBETCTBET
C                                MAKCMAHOM C 16-T PAPHOO
C                                BOHOO CA .
C
C   OPOPAMM:  1. HET.
C   ------------
C
C   PMEAHE:    1. KOHTPO HEPABHOCT AC BOCMPHOO
C   -----------       CA HE POBOTC.
C
CZZZ-------------------------------------------------------------------
       IER = 0
       IF( KQ .GT. 177777 ) GO TO 9999
C--> PAOEM O PAPAM.
       KQ5 = KQ / 100000
       KQP = KQ  - KQ5 * 100000
       KQ4 = KQP/ 10000
       KQP = KQP - KQ4 * 10000
       KQ3 = KQP/ 1000
       KQP = KQP - KQ3 * 1000
       KQ2 = KQP/ 100
       KQP = KQP - KQ2 * 100
       KQ1 = KQP/ 10
       KQ0 = KQP - KQ1 * 10
C--> PA O PAPAM, TEEP HAEM ECTHOE CO.
       KD = KQ0 + 8*KQ1 + 64*KQ2 + 512*KQ3 + 4096*KQ4 + 32768*KQ5
       RETURN
C----------------------------------------------------------------------
9999   CONTINUE
       IER = 1
       RETURN
       END

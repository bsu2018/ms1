         SUBROUTINE FM0003( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:  FM0003( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C   HAHAEHE:    HACTPOKA MAKPOMOE:
C  ------------
C                  T     M MOE     OOHAEH B AHEEM
C
C                  80       "O - RL"                D_RL
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOE.
C
C   METO:         PAMETKA OE RMSW O MOE "RL _ O";
C   -----
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO HA HAEH L(0)  L(@) HE POBOTC;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C                  RMSW TA # 80 [ RL _ O ]
C                  --------------------------------
C                                                             LM = 10
C                                                             *******
C
C                    SPM                      SRAB
C          ++---+---+---+--++----+---+-------+------+--------+----+---+
C          II R I L I R I L II G I J I PEE  I COCT.I BPEM  I PEEPB I
C          II 0 I 0 I @ I @ II R I R I  PB   I TEK. I EPEK.I        I
C          II   I   I   I   II L I L I       I "KD" I "TX"[*]I        I
C          ++---+---+---+---++---+---+-------+------+--------+--------+
C          II 1 I 2 I 3 I 4 II 5 I 6 I   7   I  8   I   9    I   10   I
C          ++---+---+---+---++---+---+-------+------+--------+--------+
C          II 11I 12I 13I 14II 15I 16I   17  I  18  I   19   I   20   I
C          ++---+---+---+---++---+---+-------+------+--------+----+---+
C
C  0 - O OTKPT(KD=1) ; @ - O AKPT (KD=0) ;
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
         INTEGER BA0T, TIP
         TIP = BA0T(2)
         NEL = BA0T(1)
         NWTR = MASPRT(13)
         IF ( TIP .EQ. 80 ) GO TO 1
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(14)
         WRITE(NWTRE, 500) TIP, NEL
500      FORMAT(' *** O. CO B FM0003: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C######################################################################
C######################################################################
1        CONTINUE
C---> OHEHE BCEX OE RMSW  OA
         LM = 10
         DO 100 I=1,LM
100      RMSW(I) = 0.0
C===> PHT CXO. APAMETP:
         RMSW(1) = PAR0T(1)
         RMSW(2) = PAR0T(2)
         RMSW(3) = PAR0T(3)
         RMSW(4) = PAR0T(4)
C---->
         IF ( RMSW(1) .EQ. 0.0 ) WRITE(NWTR, 2) NEL
         IF ( RMSW(1) .EQ. 0.0 ) RMSW(1) = 1.0E-32
2        FORMAT(' *** COPOTBEHE "OTKPTOO" EMEHTA # ', I4,
     = ' PABHO 0.0 ***'/' *** PHMAETC PABHM 1.0E-32 [OM] ***')
C---->
         IF ( RMSW(3) .EQ. 0.0 ) WRITE(NWTR, 3) NEL
         IF ( RMSW(3) .EQ. 0.0 ) RMSW(3) = 1.0E+32
3        FORMAT(' *** COPOTBEHE "AKPTOO" EMEHTA # ', I4,
     = ' PABHO 0.0 ***'/' *** PHMAETC PABHM 1.0E+32 [OM] ***')
         RETURN
         END

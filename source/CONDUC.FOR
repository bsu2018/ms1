       SUBROUTINE CONDUC (G11, G12, G22, G1, G2, G111, G222)
CXXX---- "MS1" --> 1.1  (OT  1989.)------------------
C    
C    ------------
C     :     
C    ----------    -  
C                 G11 ,G12 ,G22  G1, G2
C                   G11, G12, G22
C                   G111,G222
C
C                    FM0035
C
CZZZ----------------------------------------------------------------------
       GZ = G22 + G1
       G11 = G11 - (G12**2) / GZ
       G12 = - G12*G2/ GZ
       G22 = G1 - (G2**2)/ GZ
       G111 = G12/ GZ
       G222 = G2 /GZ
       RETURN
       END

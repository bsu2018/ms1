C   14                                  CMMA IHA**2+IHB**2+IHC**2
C   15 - 20                             P E  E P B
C    20 +I          (I OT 1 O NTOKOB)  TEKE HAEH HTEPAHX
C                                       CMM  BCEH ECTBX
C                                       HAEH TOKOB ;
C    20+NTOKOB+I    (I OT 1 O NTOKOB)  TO E  HAPEH ;
C    20+2*NTOKOB+I  (I OT 1 O NTOKOB)  TO E  AKTBHX MOHOCTE ;
C    ------------------------------------------------------------------
C

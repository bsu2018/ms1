C         -----------------
C    ------------------------------------------------------------------
C    HOMEP EMEHTA                     HAEHE
C    ------------------------------------------------------------------
C    1                                  CTAHAPTHE APAMETP PSUB5
C    2                                         =  "  =  "  =
C    2+I           ( I OT 1 O NTOKOB)  HAEH TOKOB EMEHTOB

        SUBROUTINE PSUB11( NEL, URMAS, STMAS, DGMAS, INTMAS )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT APE  1988 .)------------
C   OPOPAMMA: PSUB11( NEL, URMAS, STMAS, DGMAS, INTMAS )
C   -------------
C   HAHAEHE:  / OOBATE  MOEPOBAH
C   ----------   HEHEHX EMEHTOB
C
C                  ( MOE #17 )
C   BAPAHT:    BOMOH BA BAPAHTA MOE:
C   ---------  1.PHAK MOE = 0, MOE HEHA,
C                O BEHAM, EPEABAEMM OOBATEEM 
C                HAEHM TOKA   HAPEH HEHEHOO EMEHTA
C                O MOMEHTA BPEMEH TN BKTEHO B OPOPAMME
C                OPEETC HAEH G,J-APAMETPOB MOE  T=TN+1
C              2.PHAK MOE = 1, MOE TEPAOHHA,
C                O BEHAM, EPEABAEMM OOBATEEM 
C                HAEHM TOKA   HAPEH HEHEHOO EMEHTA
C                O MOMEHTA BPEMEH TN+1 BKTEHO B OPOPAMME
C                OPEETC HAEH G,J-APAMETPOB B MOMEHT TN+1
C                TEPAOHHM COCOOM
C   METO:     OEBEH.
C   -----
C  APAMETP:   NEL - HOMEP EMEHTA,  KOTOPOO PSUB PAOTAET;
C  ---------    URMAS - MACCB AHHX OOBATE:
C                       1-E COBO - KOECTBO APAMETPOB B CCKE
C                       ( ACAHO EM COM B BEECTBEHH MACCB);
C                       2-E COBO - PHAK MOE ( CM.BAPAHT );
C                       3-E COBO - MOMEHT BKEH MOE  T0;
C                       4-E COBO - KO BA HEHEHOCT ( K )
C                       5-E COBO  AEE  - KOHCTAHT AHATECKX
C                           BPAEH, OPEEEMX KOOM ( K )
C----------------------------------------------------------------------
C              STMAS - MACCB AHHX, EPEABAEMX B PSUB:
C
C      COBO           OOHAEHE   -   HAEHE
C      1          TN - BPEM N - O AA PACETA;
C      2          H0 - A HTEPPOBAH;
C      3          I  - HAEHE TOKA HEHEHOO EMEHTA ATPEO-
C                      BAHHOO  OOBATEEM,  MOMEHTA BPEMEH TN;
C      4          U  - HAEHE HAPEH HEHEHOO EMEHTA, ATPE-
C                      OBAHHOO OOBATEEM,  MOMEHTA BPEMEH TN;
C----------------------------------------------------------------------
C              DGMAS(1) - PXOT HOMEP APOCA,
C                         EC DGMAS(1)=1, TO TO EPB BXO B
C                         OPOPAMM PSUB HA AHHOM AE,
C                         EC DGMAS(1)=2, TO TO HE EPBA TEPA
C                         BXOT PHAK MOE
C              DGMAS(2) - PXOT HAEHE TOKA MOE B MOMEHT TN+1,
C                         EC HOMEP APOCA = 1 ( DGMAS(1) ), TO
C                         TO HAEHE TOKA MOE, BCEHHOE HA
C                         PEEM AE,
C                         EC HOMEP APOCA = 2 ( DGMAS(1) ), TO
C                         TO HAEHE TOKA MOE, BCEHHOE HA
C                         PEE TEPA AHHOO AA
C                         BXOT  J-APAMETP (CT.TOKA CHTET.CXEM)
C              DGMAS(3) - G-APAMETP (POBOMOCT CHTETECKO CXEM)
C              DGMAS(4) - = 0.
C              DGMAS(5) - = 0.       P  E    E  P  B
C
C----------------------------------------------------------------------
C              INTMAS - MACCB  BXCOT (200) EMEHTOB,
C                       B OPOPAMME  H E    C  O     E T C 
C                                       ===============================
C----------------------------------------------------------------------
C
C   OPOPAMM: HET
C   -------------
C
C----------------------------------------------------------------------
       DIMENSION URMAS(*), STMAS(*), DGMAS(*), INTMAS(*)
       COMMON /PROF1/ MASPRT(200)
       EQUIVALENCE ( NWTR ,MASPRT(13) )
       EQUIVALENCE ( NWTRE,MASPRT(14) )
       EQUIVALENCE ( TMAX ,MASPRT(43) )
C
C
C----------------------------------------------------------------------
       TN = STMAS(1)
       H0 = STMAS(2)
       I = STMAS(3)
       U = STMAS(4)
       T0 = URMAS(3)
       IF(URMAS(2).EQ.0.AND.DGMAS(1).NE.1) RETURN
       DGMAS(1) = URMAS(2)
       DGMAS(4) = 0.
       DGMAS(5) = 0.
       IF(TN.LT.T0) GO TO 102
C----------------------------------------------------------------------
       K=URMAS(4)
C--->  K - HOMEP BA HEHEHOCT
C======================================================================
C--> K=1        I = A * U**3
C          A - KOHCTAHTA, BBOTC URMAS(5)
C
C
C    B CCKE APAMETPOB PSUB  OCE CTAHAPTHX APAMETPOB CET:
C    KOHCTAHT AHATECKX BPAEH  HEHEHX EMEHTOB
C
C----------------------------------------------------------------------
      PI=3.141593
C======================================================================
C     AC AHATECKX BPAEH  HEHEHX EMEHTOB
 101  GO TO (1),K
C----------------------------------------------------------------------
   1  A = URMAS(5)
      DGMAS(2) = 0.
      DGMAS(3) = A * U**2
C----------------------------------------------------------------------
      RETURN
 102  DGMAS(2) = 1.E-6
      DGMAS(3) = 1.E-6
      RETURN
      END

       SUBROUTINE SEQMAX( EYY, IK, IA, EYYV, EYYI )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA: SEQMAX( EYY, IK, IA, EYYV, EYYI )
C   ------------
C   HAHAEHE:        OPAHBAET HAEH EYY - AMT
C   ----------         PABEEO CHAA:
C
C                      "A" PA BPMET:
C                       =====================
C                        P IA > 0   EYY > 0 --> OPAHEHE "CBEPX"
C                              EC EYY > EYYV, TO EYY=EYYV;
C
C
C                      "K" PA BPMET:
C                       =====================
C                        P IK > 0   EYY < 0 --> OPAHEHE "CBEPX"
C                            EC  -EYY > EYYV, TO EYY=-EYYV;
C
C
C                      "A" PA HBEPTPET:
C                       =====================
C                        P IA > 0   EYY < 0 --> OPAHEHE "CH"
C                              EC -EYY > EYYI, TO EYY=-EYYI;
C
C
C                      "K" PA HBEPTPET:
C                       =====================
C                        P IK > 0   EYY > 0 --> OPAHEHE "CH"
C                              EC EYY > EYYI, TO EYY=EYYI;
C
C   METO:             CTAHOBT PEM  MEHT EYY
C   ------             COACHO EYYV  EYYI
C
C   APAMETP:         EYY - MEHEMOE HAPEHE PABEH,
C   ---------          IK - TOK "K" P,
C                      IA - TOK "A" P,
C                      EYYV - OPAHEHE "CBEPX" (BPMEHE),
C                      EYYI - OPAHEHE "CH"  (HBEPTPOBAHE).
C
C   OPOPAMM:   HET
C   -------------
C   PMEAHE:
C   -----------
C
CZZZ-------------------------------------------------------------------
       REAL IA, IK
C
       IF ( IA .GT. 0.0 .AND. EYY .GT. 0.0 .AND. EYY .GT. EYYV )
     *            EYY = EYYV
       IF ( IA .GT. 0.0 .AND. EYY .LT. 0.0 .AND. -EYY .GT. EYYI )
     *            EYY = -EYYI
       IF ( IK .GT. 0.0 .AND. EYY .LT. 0.0 .AND. -EYY .GT. EYYV )
     *            EYY = -EYYV
       IF ( IK .GT. 0.0 .AND. EYY .GT. 0.0 .AND. EYY .GT. EYYI )
     *            EYY = EYYI
       RETURN
       END

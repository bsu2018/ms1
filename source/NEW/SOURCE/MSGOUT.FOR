         SUBROUTINE MSGOUT( MSG )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C     OPOPAMMA : MSGOUT ( MSG )
C     ------------
C     HAHAEHE :  BBO PACPEHHOO TEKCTOBOO COOEH HA NWTR
C    ------------
C     METO:        BOP HOMEPOB  BXOHOO MACCBA MSG  EAT.
C     -----
C     APAMETP:   MACCB MSG => MSG(1) - KOECTBO AHTX O
C     ---------                  (HOMEPOB COOEH, KOTOPX HEOXOMO
C                                 BBECT) B MSG(2) ... MSG( MSG(1) );
C                                MSG(J) => HOMEP COOEH;
C    OPOPAMM:  COMMON / PROF1 /
C    ------------
C    PMEAHE: 1. EATEHO OECET H CTPOK COOEH HE
C    ----------     OEE 80 CMBOOB;
C                2. B TEKE BEPC  PCMOTPEHHO HE OEE MMSG
C                   COOEH [ 100 ];
C                3. PEAOBAHO MAXMSG COOEH  [ 33 ];
C                4.  O AC HOMEPA COOEH B MSG(*):
C                   I = MSG(1); MSG(I+2) = HOMEP; MSG(1) = I + 1;
C
CZZZ-------------------------------------------------------------------
         DIMENSION MSG(1)
         COMMON /PROF1/ MASPRT(200)
        DATA MAXMSG/35/, MMSG/100/
C             *******************
         NWTR = MASPRT(13)
         NWTRE = MASPRT(14)
         MAX = MSG(1)
         IF ( MAX .EQ. 0 ) RETURN
         IF (MAX .LT. 0  .OR.  MAX .GT. 100 ) GO TO 667
         IMAX = MAX + 1
         DO 666 I=2,IMAX
         IF ( MSG(I) .GT. MAXMSG ) WRITE(NWTR,668) MSG(I)
C----------------------------------------------------------------------
         IF ( MSG(I) .EQ. 1 ) WRITE(NWTR,1)
         IF ( MSG(I) .EQ. 2 ) WRITE(NWTR, 2)
         IF ( MSG(I) .EQ. 3 ) WRITE(NWTR, 3)
         IF ( MSG(I) .EQ. 4 ) WRITE(NWTR, 4)
         IF ( MSG(I) .EQ. 5 ) WRITE(NWTR, 5)
         IF ( MSG(I) .EQ. 6 ) WRITE(NWTR, 6)
         IF ( MSG(I) .EQ. 7 ) WRITE(NWTR, 7)
         IF ( MSG(I) .EQ. 8 ) WRITE(NWTR, 8)
         IF ( MSG(I) .EQ. 9 ) WRITE(NWTR, 9)
         IF ( MSG(I) .EQ.10 ) WRITE(NWTR, 10)
         IF ( MSG(I) .EQ.11 ) WRITE(NWTR, 11)
         IF ( MSG(I) .EQ.12 ) WRITE(NWTR, 12)
         IF ( MSG(I) .EQ.13 ) WRITE(NWTR, 13)
         IF ( MSG(I) .EQ.14 ) WRITE(NWTR, 14)
         IF ( MSG(I) .EQ.15 ) WRITE(NWTR, 15)
         IF ( MSG(I) .EQ.16 ) WRITE(NWTR, 16)
         IF ( MSG(I) .EQ.17 ) WRITE(NWTR, 17)
         IF ( MSG(I) .EQ.18 ) WRITE(NWTR, 18)
         IF ( MSG(I) .EQ.19 ) WRITE(NWTR, 19)
         IF ( MSG(I) .EQ.20 ) WRITE(NWTR, 20)
         IF ( MSG(I) .EQ.21 ) WRITE(NWTR, 21)
         IF ( MSG(I) .EQ.22 ) WRITE(NWTR, 22)
         IF ( MSG(I) .EQ.23 ) WRITE(NWTR, 23)
         IF ( MSG(I) .EQ.24 ) WRITE(NWTR, 24)
         IF ( MSG(I) .EQ.25 ) WRITE(NWTR, 25)
         IF ( MSG(I) .EQ.26 ) WRITE(NWTR, 26)
         IF ( MSG(I) .EQ.27 ) WRITE(NWTR, 27)
         IF ( MSG(I) .EQ.28 ) WRITE(NWTR, 28)
         IF ( MSG(I) .EQ.29 ) WRITE(NWTR, 29)
         IF ( MSG(I) .EQ.30 ) WRITE(NWTR, 30)
         IF ( MSG(I) .EQ.31 ) WRITE(NWTR, 31)
         IF ( MSG(I) .EQ.32 ) WRITE(NWTR, 32)
         IF ( MSG(I) .EQ.33 ) WRITE(NWTR, 33)
         IF ( MSG(I) .EQ.34 ) WRITE(NWTR, 34)
         IF ( MSG(I) .EQ.35 ) WRITE(NWTR, 35)
C
C----------------------------------------------------------------------
666      CONTINUE
         MSG(1) = 0
         RETURN
667      CONTINUE
C!!!-------------------------------------------------------------------
         WRITE(NWTRE, 500) MAX
500      FORMAT(' *** O. CO P BOBE MSGOUT, MSG(1)=', I12)
         CALL SUBERR(1)
         RETURN
668      FORMAT (' *** COOEHE # ', I4, ' HE PEAOBAHO ***')
C######################################################################
1        FORMAT(' *** HE OOHAHO HAAO OECKO CTPOK ***')
C---> 2
2        FORMAT(' *** BBOMOE CO CKOM BEKO ***'/
     =          ' *** BOMOHO - POCK PAETEHOO POEA ***')
C--->3
3        FORMAT(' *** BBOMOE CO COEPT HE OCTM CMBO ***')
C----->4
4        FORMAT(' *** T MAKPOMOE OOEH  HE PEAOBAH ***')
C-----> 5
5        FORMAT(' *** HE OPEEEH HOMEP MAKPOMOE ***'/
     =  ' *** OCAHE CCKA "" [  ]  PHT HEBOMOHO ***')
C-----> 6
6        FORMAT(' *** HE OCTM HOMEP  OOHAEH A ***')
C---->  7
7        FORMAT(' *** HE OPEEEH HOMEP MAKPOMOE ***'/
     = ' *** OCAHE CCKA "" [ APAMETP ] PHT HEBOMOHO ***')
C----->8
8        FORMAT(' *** AHHO MAKPOMOE APAMETP HE HH ***')
C---->9
9        FORMAT(' *** B OECKO CTPOKE OCAH KAAHA HE BC HOPM
     =A ***')
C---->10
10       FORMAT(' *** HAPEHA CTPKTPA CCKA "" - OAETC "MAS" **
     =*')
C---->11
11       FORMAT(' *** HET "<" B CCKE APAMETPOB [ "MAS" ] ***')
C----->12
12     FORMAT(' *** HAPEHA CTPKTPA CCKA "" - OAETC "FT" ***')
C------>13
13       FORMAT(' *** BCTPOEHA HK "FT" ?? HE PEAOBAHA ***')
C------>14
14    FORMAT(' *** B CCKE "" OCAH "FT" HET OPAHTE ">" ***')
C------>15
15       FORMAT(' *** B TEKEM CCKE MHOO APAMETPOB ***')
C---->16
16       FORMAT(' *** CCOK FT # "<......>" - MA ***')
C---->17
17       FORMAT(' *** HET "<" B CCKE APAMETPOB [ "FT" ] ***')
C---->18
18       FORMAT(' *** HAPEHA CTPKTPA CCKA "" - OAETC "PSUB" *
     =**')
C---->19
19       FORMAT(' *** HET "<" B CCKE APAMETPOB "PSUB" ***')
C---->20
20       FORMAT(' *** HEOAHH KOHE OECKO CTPOK - MAO AHHX
     =B CCKE ***')
C---->21
21       FORMAT(' *** HEOOHAHO HAAO OCCKA ***' / ' *** HET "V",
     = "U", "I", "GAMMA", "OMEGA", "MM", "MBH" ***')
C--->22
22       FORMAT(' *** CCOK "APOC BBOA" OO - CEKAETC ***')
C--->23
23       FORMAT(' *** OTMEEHHOO A B E HET ***')
C--->24
24       FORMAT(' *** OTMEEHHOO EMEHTA E HE COEPT ***')
C---->25
25       FORMAT(' *** OTMEEHHOE "HAEHE" HE OCTMO ***')
C----> 26
26       FORMAT(' *** OTMEEHH EMEHT, APOEHHX HA BBO BEH,
     = HE COEPT ***')
C--->27
27       FORMAT(' *** HE OPEEEH HOMEP BEHA  ***')
C--->28
28       FORMAT(' *** HOMEP CB OE HOMEPA BEHA  ***')
C--->29
29       FORMAT(' *** CB C BEHOM, MEM O HOMEP ***')
C--->30
30       FORMAT(' *** T CB OOEH  ***')
C--->31
31       FORMAT(' *** CB HE OPEEEHA ***')
C--->32
32       FORMAT(' *** BBOM XAPAKTEPCTK BEHA, HE BBE OCAH',
     *                ' CBE *** ' )
C--->33
33       FORMAT(' *** HAPEH OPOK CEOBAH APAMETPOB  ***')
C--->34
34       FORMAT(' *** OTMEEHH     #SPOOL ***')
C---> 35
35    FORMAT(' *** OTMEEHH ""    #SPOOL ***')
C
C
C
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
         END

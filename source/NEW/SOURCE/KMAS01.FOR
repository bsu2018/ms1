       REAL FUNCTION KMAS01( X, TMAS )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA-HK  KMAS01( X, TMAS )
C   --------------------
C   HAHAEHE:        BCEHE HAEH F(X), AAHHO TAHO
C   ----------         KOEHTAM OHOMA:
C
C                      F(X) = A0 + A1* X + A2* X**2 + A3* X**3 + ...
C
C                      OCKAETC AAHE OO KOECTBA
C                       KO. OHOMA;
C
C                      *** KOPOBKA B OCAH APAMETPOB ***
C
C                      MAS < A0 A1 ..... A_N >;
C
C
C   METO:     CXEMA OPHEPA;
C   -----
C   APAMETP:  KMAS01 - HAEHE F(X) P
C   ---------   X      - TOKA OPEEEH F(X);
C               TMAS - MACCB AHHX ( PAMEPHOCT N  COB ):
C   MACCB  --> N   A0  A1  A2 ..........................   T..
C   COBO # --> 1    2  3   4  ..........................   T..
C
C
C   OPOPAMM:      HET
C   ------------
C   PMEAHE:   1.  B TMAS BCE B REAL*4
C   ----------        ********************
C
C
C
CZZZ-------------------------------------------------------------------
       DIMENSION TMAS(1)
CVVVVV REAL KMAS01
       N  = IFIX( TMAS(1) )
C-->  CXEMA OPHEPA...
       NP1 = N+1
       I = NP1
       SP = 0.0
10     CONTINUE
       SP = SP * X  + TMAS(I)
       I = I - 1
       IF ( I .GE. 2 ) GO TO 10
       KMAS01 = SP
       RETURN
       END

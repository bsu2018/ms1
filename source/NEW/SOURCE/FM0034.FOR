         SUBROUTINE FM0034( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EBPA  1989 .)------------
C   OPOPAMMA:   FM0034( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C   HAHAEHE:    HACTPOKA MAKPOMOE TPAHCOPMATOPOB
C  ------------    *************************************
C     KO-BO O-K  T     M MOE     OOHAEH B AHEEM
C
C     K = 6        36      2-X OM. 3-X AH.    TP6
C                          TPAHCOPMATOP
C     K = 9        39      3-X OM. 3-X AH.    TP9
C                          TPAHCOPMATOP
C
C
C                  BBO O OCAH:
C
C            # T 3@  @ @ ..   SH UH PXX IXX PKZ1 PKZ2 ..UKZ1 UKZ2
C            ..R1 R2 ..W1 W2 ..K
C
C           OKEH: <HAAO OMOTOK> <KOH OMOTOK>
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOE.
C
C   METO:         PAMETKA OE RMSW O MOE;
C   -----
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO HE POBOTC;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C
C
C    EPEMEHHO ACT DR<*> = D<*> - D1 + 2
C                        $$$$$$$$$$$$$$$$$$$$$
C
C
CZZZ-------------------------------------------------------------------
       COMMON /PROF1/ MASPRT(200)
       DIMENSION MLM(2)
       INTEGER BA0T, TIP
       INTEGER DR1, DR2, DR3, DR4, DR5
       REAL IH, IXX, LCT,L1,L2
       DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
       EQUIVALENCE ( RN, N )
       DATA MLM /110,120 /
C-->  HA  T ?
       NEL = BA0T(1)
       TIP = BA0T(2)
       NWTR = MASPRT(13)
       IF( TIP .EQ. 36  .OR.  TIP .EQ. 39 ) GO TO 1
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(14)
         WRITE(NWTRE, 500) TIP, NEL
500      FORMAT(' *** O. CO B FM0034: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C----------------------------------------------------------------------
C    TPEXAH BXOMOTOH TPAHCOPMATOP (T # 36)
C    TPEXAH TPEXOMOTOH TPAHCOPMATOP (T # 39)
C----------------------------------------------------------------------
1        CONTINUE
C-->  K MOE
       K = TIP - 30
       K1 = K / 3 - 1
C-->  HA EPEMEHHO ACT
       LM = MLM(K1)  -  ( 8+2*K)
       DO 200 I=1, LM
200    RMSW(I) = 0.0
C-->  AEM  "K"
       N = K
       RMSW(1) = RN
C----------------------------------------------------------------------
C--> PEM APAMETPOB...
C  X KO-BO...
       KPAR = 3 *( K + 2)
C----------------------------------------------------------------------
C*** KOHTPO HA OOTEHOCT !!!
C----------------------------------------------------------------------
       DO 201  I=1, 10
       IF( PAR0T(I) .LE. 0.0 ) GO TO 9999
201    CONTINUE
C===>  PCBABAEM AHHE...
       SH = PAR0T(1)/3.
       UH = PAR0T(2)
       IH = SH/UH
       PXX = PAR0T(3)/3.
       IXX = PAR0T(4)*IH/100.
       PKZ = PAR0T(5)/3.
       UKZ = PAR0T(6)*UH/100.
       R1 = PAR0T(7)
       R2 = PAR0T(8)
       W1 = PAR0T(9)
       W2 = PAR0T(10)
C===>  PACTBAEM HKTBHOCT...
       XXX = SQRT(UH**2*IXX**2-PXX**2)/IXX**2
       A = PXX/IXX**2 - R1
       RKZ = PKZ/IH**2
       XKZ = SQRT(UKZ**2*IH**2 - PKZ**2)
     * /IH**2
       AKT= RKZ - R1
       R2 = R2*W1**2/W2**2
C ******************************************
C==>   BHMAHE EC PBEEHHE HAEH
C ******************************************
C==>  BCTC PEHO
       XL2 = XKZ/2.
       XL1 = XL2
       B = XXX - XL1
       RCT = B**2/A + A
       XLCT = RCT*A/B
       PI = 3.1415926
       L1 = XL1/(100.*PI)
       LCT = XLCT/(100.*PI*W1**2)
       RCT = RCT/W1**2
       L2= XL2/(100.*PI)*W2**2/W1**2
       R2 = R2*W2**2/W1**2
C==> AKOH PACET...
       DO 210   I =1,3
       RMSW(1 + I) = R1
       RMSW(4 + I) = R2
       RMSW(7 + I) = L1
       RMSW(10 + I) = L2
       RMSW(13 + I) =W1
       RMSW(16 + I) = W2
       RMSW(19 + I) = RCT
       RMSW(22 + I) = LCT
 210   CONTINUE
C----------------------------------------------------------------------
C--> PACTAEM MATP  X
       IDR3 = 3*K + 2
       A = RMSW(IDR3)
       B = RMSW(IDR3 + 1)
       C = RMSW(IDR3 + 2)
       T = A + B + C
C ---> AOHEM X ...
       RMSW(IDR3 + 6) = A*(B+C)/T
       RMSW(IDR3 + 7) =- A*B/T
       RMSW(IDR3 + 8) =- A*C/T
       RMSW(IDR3 + 9) =- B*A/T
       RMSW(IDR3 +10) = B*(C+A)/T
       RMSW(IDR3 +11) =- C*B/T
       RMSW(IDR3 +12) =- C*A/T
       RMSW(IDR3 +13) =- C*B/T
       RMSW(IDR3 +14) = C*(B+A)/T
C----------------------------------------------------------------------
C      PACCTABEM HAAHE HAEH TOKJU
C----------------------------------------------------------------------
       RMSW(89) = IXX*SQRT(2.)
       N   = IFIX(PAR0T(11))
       RMSW(90) = RN
       RETURN
C----------------------------------------------------------------------
9999   CONTINUE
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(14)
         WRITE(NWTRE, 501) TIP, NEL
501      FORMAT(' *** O. CO B FM0034: AHHE HE OOTEH      **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
       RETURN
CCC    DEBUG INIT
       END

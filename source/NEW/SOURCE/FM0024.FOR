         SUBROUTINE FM0024( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT EKAP 1987 .)------------
C
C
C   OPOPAMMA:  FM0024( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C
C   HAHAEHE:    HACTPOKA MAKPOMOEE CEX TOB:
C  ------------
C                +-----+--------------------------------------------+
C                I T I              M MOE                    I
C                I-----I--------------------------------------------I
C                I 15  I   PABEM HAPEHEM CTOHK TOKA    I
C                I-----I--------------------------------------------I
C                I 16  I   PABEM HAPEHEM CTOHK HAP-  I
C                I     I   EH                                    I
C                I-----I--------------------------------------------I
C                I 200 I   EAH OEPAOHH CTE         I
C                I-----I--------------------------------------------I
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOEE KAAHHX TOB.
C
C   METO:         BCETC  ACBAETC B MSW BC HEOXOMA
C   -----          HOPMA, HEOXOMA  OCEEO
C                  PACETA MAKPOMOE. AATPOBAHO K METO
C                  HEPA - BO.
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW - O SPM  SRAB;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C
C   PMEAHE:    1. KOHTPO HE POBOTC;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C
C                  RMSW T # 15 [ TH ]
C                  ----------------------
C
C             ++--------+---------+---------++
C             II        I         I         II
C             II   1   I   2    I    G    II
C             II        I         I         II
C             ++--------+---------+---------++       LM = 3
C             II   1    I    2    I     3   II       ******
C             ++--------+---------+---------++
C             II  11    I   12    I    13   II
C             ++--------+---------+---------++
C
C
C                  RMSW T # 16 [ HH ]
C                  ----------------------
C
C      ++--------+---------+---------+---------+----------+
C      II        I         I         I         I          I
C      II   1   I   2    I   GBH   I    K    I G=GBH*K  I
C      II        I         I         I         I          I
C      ++--------+---------+---------+---------+----------+    LM = 5
C      II   1    I    2    I     3   I    4    I    5     I    ******
C      ++--------+---------+---------+---------+----------+
C      II  11    I   12    I    13   I   14    I   15     I
C      ++--------+---------+---------+---------+----------+
C
C
C                  RMSW T # 200 [ EAH OEPAOHH CTE ]
C                  ----------------------
C
C             ++--------+---------+---------++
C             II        I         I         II
C             II   K    I   GBX   I  GBX   II
C             II        I         I         II
C             ++--------+---------+---------++       LM = 3
C             II   1    I    2    I     3   II       ******
C             ++--------+---------+---------++
C             II  13    I   14    I    15   II
C             ++--------+---------+---------++
C
C
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
         EQUIVALENCE ( RN , N )
         INTEGER BA0T, TIP
         REAL L
         TIP = BA0T(2)
         NEL = BA0T(1)
         NWTR = MASPRT(14)
         IF( TIP .EQ. 15 ) GO TO 10
         IF( TIP .EQ. 16 ) GO TO 20
         IF( TIP .EQ. 200) GO TO 30
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(13)
         WRITE(NWTRE, 500) TIP, NEL
500      FORMAT(' *** O. CO B FM0024: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C######################################################################
C######################################################################
10       CONTINUE
C---> CTOHK TOKA, PABEM HAPEHEM
         N = IFIX( PAR0T(1) )
         RMSW(1) = RN
         N = IFIX( PAR0T(2) )
         RMSW(2) = RN
         G = PAR0T(3)
         RMSW(3) = G
C---> HA RMSW MOE
         LM = 3
         RETURN
C######################################################################
20       CONTINUE
C---> CTOHK HAPEH, PABEM HAPEHEM
         N = IFIX( PAR0T(1) )
         RMSW(1) = RN
         N = IFIX( PAR0T(2) )
         RMSW(2) = RN
         R = PAR0T(3)
         IF( R .LE. 0.0 ) GO TO 25
         GBH = 1./R
21       RMSW(3) = GBH
         AK= PAR0T(4)
         RMSW(4) = AK
         G = AK * GBH
         RMSW(5) = G
C---> HA RMSW MOE
         LM = 5
         RETURN
25       WRITE(NWTR, 521) NEL
521    FORMAT(//' *** BHTPEHHE COPOTBEHE CTOHKA HAPEH ***'/
     =          ' *** EMEHTA HOMEP ', I10,  ' PABHO H         ***'/
     =          ' *** PHMAETC PABHM 1.0E-32  [OM]             ***')
         GBH = 1.0E+32
         GO TO 21
C######################################################################
30       CONTINUE
C---> EAH OEPAOHH CTE
         AK= PAR0T(1)
         RMSW(1) = AK
         RBX = PAR0T(2)
         IF( RBX .LE. 0.0 ) GO TO 35
         GBX = 1./ RBX
31       RMSW(2) = GBX
         RBIX = PAR0T(3)
         IF( RBIX .LE. 0.0 ) GO TO 37
         GBIX = 1./ RBIX
32       RMSW(3) = GBIX
C---> HA RMSW MOE
         LM = 3
         RETURN
35       WRITE(NWTR, 531) NEL
531    FORMAT(//' *** BXOHOE COPOTBEHE OEPAOHHOO CTE***'/
     =          ' *** EMEHTA HOMEP ', I10,  ' OTPATEHO       ***'/
     =          ' *** PHMAETC PABHM ECKOHEHOCT             ***')
         GBX = 0.0
         GO TO 31
37       WRITE(NWTR, 532) NEL
532    FORMAT(//' ***BXOHOE COPOTBEHE OEPAOHHOO CTE***'/
     =          ' *** EMEHTA HOMEP ', I10,  ' PABHO H         ***'/
     =          ' *** PHMAETC PABHM 1.E-32 [OM]               ***')
         GBIX = 1.0E+32
         GO TO 32
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
         END

         SUBROUTINE FM0012( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C
C
C   OPOPAMMA:  FM0012( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C
C   HAHAEHE:    HACTPOKA MAKPOMOEE CEX TOB:
C  ------------
C                  T     M MOE     OOHAEH B AHEEM
C
C                  63      3-X AHA ..C        ES3
C                              CHCOAHO
C
C
C              EMEHT KOPETC:
C              -------------------
C
C               # T 63  K0 KA KB KC  R EMAX OMEGA FAZ0;
C
C     K0 - O E, KA - AA A, KB - AA B, KC - AA C;
C
C---> BBOHE HAEH: TOK: K AM KA, KB, KC, OT A K0;
C                              *                   **
C
C
C                  13      "MEPTE"
C                           HAPEH             V3
C
C
C              EMEHT KOPETC:
C              -------------------
C
C               # T 13  K1 K2 K3;
C
C   B CCOK BECEHHX HAEH OMEAET HAPEHE OB K1, K2, K3;
C
C
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOEE KAAHHX TOB.
C
C   METO:         BCETC  ACBAETC B MSW BC HEOXOMA
C   -----          HOPMA, HEOXOMA  OCEEO
C                  PACETA MAKPOMOE. AATPOBAHO K METO
C                  HEPA - BO.
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW - O SPM  SRAB;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C
C   PMEAHE:    1. KOHTPO HE POBOTC;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C
C                  RMSW T # 13 [ MEPTE HAPEH ]
C                  ----------------------------------------
C
C                  LM = 0, XPAHTC TOKO ;
C                  LM<OHA> = 8+3=11;
C
C
C                  RMSW TA # 63 [ CTOHK SIN HAPEH - 3- A]
C                  -----------------------------------------
C                    ( U1 = EM * SIN( OMEGA * T  +  FAZ0 )     LM = 8
C                    ( U2 = EM * SIN( OMEGA * T  +  FAZ0 - 120 )
C                    ( U3 = EM * SIN( OMEGA * T  +  FAZ0 + 120 )
C                             SPM                 SRAB
C             ++--------+------+--------+------++------+---+---+-----+
C             II HA.  I HA.I        I FAZ0 II      IJESIJESI PE  I
C   :     II  1/R   I EM/R I OMEGA  I  P   II JESA I   I   I E  I
C  K0         II        I      I        I PA. II      I B I C I PB  I
C    K1       ++--------+------+--------+------++------+---+---+-----+
C      K2     II   1    I  2   I   3    I  4   II  5   I 6 I 7 I  8  I
C         K3  ++--------+------+--------+------++------+---+---+-----+
C  9 10 11 12 II  13    I  14  I  15    I  16  II  17  I18 I19 I 20  I
C             ++--------+------+--------+------++------+---+---+-----+
C
C
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
         INTEGER BA0T, TIP
         REAL L
         TIP = BA0T(2)
         NEL = BA0T(1)
         NWTR = MASPRT(14)
         IF (TIP .EQ. 63 )   GO TO 63
         IF (TIP .EQ. 13 )   GO TO 13
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(13)
         WRITE(NWTRE, 500) TIP, NEL
500      FORMAT(' *** O. CO B FM0012: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C######################################################################
C######################################################################
13       CONTINUE
          LM = 0
C---> MEPTE
        RETURN
C----------------------------------------------------------------------
63     CONTINUE
       LM = 8
C######################################################################
C######################################################################
C---> SIN CT. HAP.
         R = PAR0T(1)
         EM = PAR0T(2)
         OMEGA = PAR0T(3)
         FAZA = PAR0T(4) * 3.141592654 / 180.0
         IF ( R .EQ. 0.0 ) GO TO 60
C
61       RMSW(1) = 1.0 / R
         RMSW(2) = EM / R
         RMSW(3) = OMEGA
         RMSW(4) = FAZA
         RMSW(5) = 0.0
         RMSW(6) = 0.0
         RMSW(7) = 0.0
         RMSW(8) = 0.0
C
C
         RETURN
60       WRITE (NWTR,505) NEL
         R = 1.0E-32
         GO TO 61
C----------------------------------------------------------------------
505      FORMAT(' *** BHTPEHHE COPOTBEHE CTOHKA HAPEH ***'/
     =          ' *** EMEHTA HOMEP ', I10,  ' PABHO H         ***'/
     =          ' *** PHMAETC PABHM 1.0E-32  [OM]             ***')
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
         END

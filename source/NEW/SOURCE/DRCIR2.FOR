         SUBROUTINE DRCIR2( BA0, KNOT, PAR0, MSWALL )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:   DRCIR2( BA0, KNOT, PAR0, MSWALL )
C   ------------
C   HAHAEHE:    OPMPOBAHE CCKA MSWALL
C  ------------
C                  ( 3- ACT PEKTB #E )
C   METO:         CM. / GETNEW;
C   -----
C   APAMETP:     CM. GETNEW;
C   ---------
C
C   OPOPAMM:  1. COMMON PROF1
C   -------------  2. GETMSW
C   PMEAHE:    1. B CAE CEHOO OPMPOBAH MSWALL
C   ----------        BOAETC A FMSWAL
C                  2. OPOHEE CM. INPUT0
C                  3. MACCB MSWALL OEH T  HPOBAHH;
C
CZZZ-------------------------------------------------------------------
         COMMON/PROF1/ MASPRT(200)
         DIMENSION BA0(1), PAR0(1), KNOT(1), MSWALL(1)
         INTEGER BA0, FMSWAL
         EQUIVALENCE ( MASPRT(13),  NWTR )
         EQUIVALENCE ( MASPRT(14),  NWTRE )
         EQUIVALENCE ( MASPRT(74),  FMSWAL )
         FMSWAL = 0
         CALL GETMSW( BA0, KNOT, PAR0, MSWALL, IER )
         FMSWAL = 1
         IF ( IER    .NE. 0   ) WRITE( NWTR, 100 )
         IF ( IER    .NE. 0   ) FMSWAL = 0
100     FORMAT(//' *** OECKE OK OCAH E - E CPACABAET
     =C ***'//)
C23456
         RETURN
         END

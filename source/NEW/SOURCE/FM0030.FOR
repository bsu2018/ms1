       SUBROUTINE FM0030(BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT  MAPTA  1988 .)------------
C   OPOPAMMA: FM0030(BA0T, KNOTT, PAR0T, RMSW, LM )
C   -------------
C   HAHAEHE:  HACTPOKA MAKPOMOEE HEHX HAMECKX BEHEB:
C   ----------
C              +-----+-----------------------------------------------+
C              I     I                                   I  EPEA-  I
C              I T I       HAMEHOBAHE BEHA          I  TOHA   I
C              I     I                                   I  HK  I
C              I-----I-----------------------------------I-----------I
C              I     I  POOPOHAHO-CMMPEE      I           I
C              I 318 I       (  - PETOP )           I     K     I
C              I     I                                   I           I
C              I-----I-----------------------------------I-----------I
C              I     I  POOPOHAH CMMATOP        I           I
C              I 324 I       KBAPATOB                   I           I
C              I     I                                   I           I
C              I-----I-----------------------------------I-----------I
C              *******************************************************
C                PHAK TIP # 4 -> O. MAKPOMOE.
C                ( DM0035)
C                      OPMPET "EPEMEHH" ACT RMSW OCAH
C                      HA:  T 318:==> +1(VAR)
C
C----------------------------------------------------------------------
C              EMEHT OCBAETC:
C
C       # T 3XX  0 
C              MAS < NNAPR  H1 K1 K1  H2 K2 K2  ... >
C              MAS < NGARM  W  A0  A1  FI1  A2  FI2... >
C
C      E
C          NNAPR  -   KOECTBO HAPEH CXEM , KOTOPE
C                     COTC B KAECTBE BXOHX CHAOB;
C          HI,KI- HOMEPA OB, HAPEHE ME KOTOPM
C                     COETC B KAECTBE BXOHOO CHAA;
C          KI       - KOEHT POOPOHAHOCT I-TOO
C                     HAPEH;
C          NGARM  -   KOECTBO EMEHTOB APMOHECKOO PA
C                     OCBAEO AAHH CHA, OABAEM
C                     HA BXO PETOPA (C ETOM OCT. COCTAB.);
C          W    -     ACTOTA OCHOBHO APMOHK (  );
C          A0,A1,...- AMT COOTBETBX APMOHK CHAA;
C          FI1,FI2...-HA. A COOTBETBX APMOHK CHAA;
C----------------------------------------------------------------------
C
C   METO:         PAMETKA OE RMSW O MOE
C   -----
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW P FGMSW = 0;
C                  LM - HA BCEO MSW OCAH      P FGMSW = 1;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO B MHMAHOM O"EME;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C
C              OPMAT RMSW T # 318, #324:
C              ----------------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0030    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I  FQ - A HOBE HA. BCEH
C            I              I      I   (  CHXPO. MAKPO. P.TIP # 4 )
C    11      I     2        I  FM  I  NNAPR-KOECTBO HAP., CO-
C            I              I      I        EMX KAK BXOHE CHA;
C    12      I     3        I  FM  I  H1 - E HAAA;
C    13      I     4        I  FM  I  K1 - E KOHA;
C    14      I     5        I  FM  I  K1   - KOEHT POOPOHA-
C            I              I      I         HOCT HAPEH;
C  9+I*3     I     I*3      I  FM  I  HI -      -"-
C 10+I*3     I   1+I*3      I  FM  I  KI -      -"-
C 11+I*3     I   2+I*3      I  FM  I  KI   -      -"-
C .......    I   .......    I  ... I  ..........................
C12+NNAPR*3  I  3+NBXOD*3   I  FM  I  NGARM -KOECTBO APMOHK, C.
C            I              I      I          OCAH A. CHAA
C            I              I      I         (C ETOM OCT. COCTAB.);
C 13+NNAPR*3 I  4+NNAPR*3   I  FM  I  W  -   ACTOTA OCH. APMOHK();
C 14+NNAPR*3 I  5+NNAPR*3   I  FM  I  A0  -  AMTA OCT. COCTAB-
C            I              I      I         E A. CHAA;
C 15+NNAPR*3 I  6+NNAPR*3   I  FM  I  A1  -  AMTA 1-O APMOHK
C            I              I      I         AAHHOO CHAA;
C 16+NNAPR*3 I  7+NNAPR*3   I  FM  I  FI1 -  HA. AA 1-O APMOHK;
C 17+NNAPR*3 I  8+NNAPR*3   I  FM  I  A2  -  AMTA 2-O APMOHK;
C 18+NNAPR*3 I  9+NNAPR*3   I  FM  I  FI2 -  HA. AA 2-O APMOHK;
C .......    I   .......    I  ... I  ..........................
C----------------------------------------------------------------------
C
C  OA HA MSW --> LM<BC> = 12 + 3*NNAPR + 2*NGARM
C
C  HA EPEMEH. ACT --> LM<RMSW> = 3 + 3*NNAPR + 2*NGARM
C
CZZZ-------------------------------------------------------------------
       COMMON /PROF1/ MASPRT(200)
       DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
       EQUIVALENCE( NWTR, MASPRT(13) )
       EQUIVALENCE( NWTRE, MASPRT(14) )
       EQUIVALENCE( FGMSW, MASPRT(87) )
       EQUIVALENCE( NMKNOT,MASPRT(7) )
       EQUIVALENCE ( RN  ,   N  )
       INTEGER  TIP, BA0T, FGMSW
       NEL = BA0T(1)
       TIP = BA0T(2)
       NTIP= TIP - 317
       IF ( NTIP .EQ. 1 ) GO TO 1
       IF ( NTIP .EQ. 7 ) GO TO 1
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 501 ) NEL, TIP
501    FORMAT(/' *** O. CO B FM0030 --> HOMEP TA HE HA ***'/
     =  ' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
1      CONTINUE
       GO TO (100),NTIP
C----------------------------------------------------------------------
C---> POOPOHAHO CMMPEE BEHO   ( T  # 318 )
C---> POOPOHAH CMMATOP KBAPATOB ( T  # 324 )
C----------------------------------------------------------------------
100   CONTINUE
C--> OPEEEHE KOHCTAHT
       NNAPR = 0
       IF( PAR0T(2) .GT. 0.0 ) NNAPR = IFIX ( PAR0T(2) )
       INGARM = 4 + 3*NNAPR
       NGARM  = 0
       IF( PAR0T(INGARM) .GT. 0.0 ) NGARM = IFIX ( PAR0T(INGARM) )
       IF ( FGMSW .EQ. 0 ) GO TO 1000
C----------------------------------------------------------------------
C-->  TOHT LM <O >
       LM = 12 + 3*NNAPR + 2*NGARM
       RETURN
C######################################################################
1000   CONTINUE
C-->  PHT  TO MOHO COPMPOBAT..
       LM = 3 + 3*NNAPR + 2*NGARM
       DO 2 I=1,LM
2      RMSW(I) = 0.0
C---> POBEPM  PABHO  BBEEH AHHE?
       NNAPR1 = (IFIX ( PAR0T(1) ) - 1 ) / 3
       IF ( NNAPR .EQ. NNAPR1 ) GO TO 3
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 502 ) NEL, TIP
502    FORMAT(/' *** OKA B FM0030 -->HEPABHO AAHO KOECTBO BX
     =OOB***'/' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
3      CONTINUE
       NGARM1 = (IFIX( PAR0T(INGARM-1) ) - 1) / 2
       IF ( NGARM .EQ. NGARM1 ) GO TO 4
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 503 ) NEL, TIP
503    FORMAT(/' *** OKA B FM0030 -->HEPABHO AAHO KO-BO APMOH
     =K  ***'/' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
4      CONTINUE
C----------------------------------------------------------------------
C---->  AOMHM HOMEPA OB.
       N = NNAPR
       RMSW(2) = RN
       IPAR0T = 2
       IRMSW = 2
       IF( NNAPR .EQ. 0 ) GO TO 50000
          DO 5 I = 1 , NNAPR
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          NYZN  = IFIX ( PAR0T(IPAR0T) )
          IF( NYZN .LT. 0 .OR. NYZN .GT. NMKNOT ) GO TO 9999
          N = NYZN
          RMSW(IRMSW) = RN
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          NYZK = IFIX ( PAR0T(IPAR0T) )
          IF( NYZK .LT. 0 .OR. NYZK .GT. NMKNOT ) GO TO 9999
          N = NYZK
          RMSW(IRMSW) = RN
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          RMSW(IRMSW) =  PAR0T(IPAR0T)
5      CONTINUE
50000  CONTINUE
C----------------------------------------------------------------------
C---->  AOMHM AMT  A APMOHK ----------------------------
       IPAR0T = IPAR0T + 2
       IRMSW = IRMSW + 1
       N = NGARM
       RMSW(IRMSW) = RN
       IF( NGARM .EQ. 0 ) GO TO 60000
       IPAR0T = IPAR0T + 1
       IRMSW = IRMSW + 1
       RMSW(IRMSW) = PAR0T(IPAR0T)
       IPAR0T = IPAR0T + 1
       IRMSW = IRMSW + 1
       RMSW(IRMSW) = PAR0T(IPAR0T)
       I1 = NGARM-1
       IF( I1 .LT. 1 ) GO TO 7
          DO 6 I = 1 , I1
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          RMSW(IRMSW) = PAR0T(IPAR0T)
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          RMSW(IRMSW) = PAR0T(IPAR0T) * 3.1416 / 180.
6      CONTINUE
60000  CONTINUE
7      CONTINUE
       RETURN
C######################################################################
9999   CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 504 ) NEL, TIP
504    FORMAT(/' *** OKA B FM0030 -->HEPABHO AAH BXOHE AHH
     =E   ***'/' *** HA BXO EMEHTA ', I5, ' HOMEP TA ', I5,' ***'/
     =         ' *** OAETC HAPEHE C HECECTBX OB
     =    *** ')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
       RETURN
       END

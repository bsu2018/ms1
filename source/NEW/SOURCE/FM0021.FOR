       SUBROUTINE FM0021(BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT   OKTP 1987 .)-----------
C   OPOPAMMA: FM0021(BA0T, KNOTT, PAR-T, RMSW, LM )
C   -------------
C   HAHAEHE:  HACTPOKA MAKPOMOEE HEHEHX HAMECKX BEHEB:
C   ----------
C   +-----+-------------------------------+------------------------+
C   I     I                               I         EPEA-        I
C   I T I       HAMEHOBAHE BEHA      I         TOHA         I
C   I     I                               I         HK        I
C   +-----+-------------------------------+------------------------+
C   I     I                               I             I Y    *   I
C   I     I                               I             I     *    I
C   I     I        HEHEHOE BEHO       I             I*  *      I
C   I 320 I  (EPEATOHA HK EO    I      ------*+------->  I
C   I     I  AAHHO HAOPOM TOEK)       I         *   I       X  I
C   I     I                               I    *  *     I          I
C   I     I                               I             I          I
C   +-----+-------------------------------+------------------------+
C   I     I                               I            !Y          I
C   I     I                               I      !--<--*----!----- I
C   I     I                               I      !     !2  !      I
C   I     I                               I      !     !    !    X I
C   I 321 I       PEEHOE BEHO          I -----*-----I----*----- I
C   I     I                               I      !X1   !    !X2    I
C   I     I                               I      !     !    !      I
C   I     I                               I------!-----*-->-!      I
C   I     I                               I            !1         I
C   +-----+-------------------------------+------------------------+
C   I     I                               I !X         *      *    I
C   I     I                               I !    *                 I
C   I     I                               I !*                  T  I
C   I     I                               I *--------------------- I
C   I 322 I  BEHO "CTOE AABAHE"  I !                     I
C   I     I                               I !              *      *I
C   I     I                               I !TAU     *             I
C   I     I                               I !<->!*              T  I
C   I     I                               I *---*----------------- I
C   +-----+-------------------------------+------------------------+
C   I     I                               I                        I
C   I 323 I       MHOTE              I  = K * X1 * X2 *...   I
C   I     I                               I                        I
C   +-----+-------------------------------+------------------------+
C   I     I                               I                        I
C   I 325 I    KOPEH KBAPATH          I  = SQRT(X)            I
C   I     I                               I                        I
C   +-----+-------------------------------+------------------------+
C                PHAK TIP # 4 -> O. MAKPOMOE.
C                ( DM0025, LM0007 )
C                      OPMPET "EPEMEHH" ACT RMSW OCAH
C
C                      HA:  T 320:==>+1<VAR>
C                      HA:  T 321:==> 10
C                      HA:  T 322:==>+1<VAR>
C                      HA:  T 323:==>+1<VAR>
C                      HA:  T 325:==> 6
C----------------------------------------------------------------------
C              EMEHT OCBAETC:
C
C       # T 320  0   #  #CM  P
C              MAS < NT  X1  Y1  X2  Y2 ... >
C
C      E
C          # ,#CM - HOMEP EMEHTA  HOMEP TOKA EMEHTA ,
C                     COEMOO B KAECTBE BXOHOO CHAA;
C          P      -   PHAK BA APOKCMA:
C                     P = 1 - KCOHO-HEHA ;
C                     P = 2 - KBAPATHA ;
C          NT     -   KOECTBO  TOEK EPEATOHO HK
C                     AAHHO TAHO;
C          X1 Y1  -   COOTBETCTBEHHO ACCA  OPHATA TOK
C           T..     EPEATOHO HK
C          ------------------------------------------------------------
C
C          # T 321  0   #  #CM  X1  X2  1  2
C
C         E:
C          # ,#CM - HOMEP EMEHTA  HOMEP TOKA EMEHTA ,
C                     COEMOO B KAECTBE BXOHOO CHAA;
C          X1,X2  -   ACC TOEK EPECEEH EPEATOHO HK
C                     C OC ACC.
C          1,2  -   OPHAT TOEK EPECEEH EPEATOHO HK
C                     C OC OPHAT.
C          ------------------------------------------------------------
C
C          # T 322  0   #  #CM  TAU  HA.C.
C
C         E:
C          # ,#CM - HOMEP EMEHTA  HOMEP TOKA EMEHTA ,
C                     COEMOO B KAECTBE BXOHOO CHAA;
C          TAU    -   OCTOHHA BPEMEH AEPK;
C          HA.C. - HAEHE BXOHOO CHAA P T=[0,TAU];
C          ------------------------------------------------------------
C
C          # T 323  0   K
C              MAS < NBXOD  #1 #CM1  #2 #CM2   ... >;
C
C         E
C          K        - KOEHT POOPOHAHOCT POBEEH
C                     BXOHX TOKOB;
C          NBXOD  -   KOECTBO TOKOB EMEHTOB CXEM , KOTOPE
C                     COTC B KAECTBE BXOHX CHAOB;
C          #I,#CMI- HOMEP EMEHTA  HOMEP TOKA EMEHTA ,
C                     COEMOO B KAECTBE BXOHOO CHAA;
C          ------------------------------------------------------------
C
C          # T 325  0   #  #CM
C
C         E:
C          # ,#CM - HOMEP EMEHTA  HOMEP TOKA EMEHTA ,
C                     COEMOO B KAECTBE BXOHOO CHAA;
C----------------------------------------------------------------------
C
C   METO:         PAMETKA OE RMSW O MOE
C   -----
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW P FGMSW = 0;
C                  LM - HA BCEO MSW OCAH      P FGMSW = 1;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO B MHMAHOM O"EME;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C
C                      OPMAT RMSW T # 320
C                 ------------------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0021    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I  FQ - A HOBE HA. BCEH
C            I              I      I   (  CHXPO. MAKPO. P.TIP # 4 )
C    11      I     2        I  DM  I  NMIN - HOMEP AE MEHE
C            I              I      I         TOK  T=TN+
C    12      I     3        I  FM  I  #  - HOMEP EMEHTA;
C    13      I     4        I  FM  I  #CM  - HOMEP TOKA EMEHTA #1;
C    14      I     5        I  LM  I  ATOKM  - AP B TOKN/N1 HAEH
C            I              I      I           TOKA HOMEP #CM
C            I              I      I           EMEHTA #;
C    15      I     6        I  FM  I  P   - PHAK BA APOKCMA
C    16      I     7        I  FM  I  NT  - KOECTBO  TOEK
C            I              I      I        EPEATOHO HK
C    17      I     8        I  FM  I  X1  - ACCA 1- TOK
C            I              I      I        EPEATOHO HK
C   ....     I    .....     I      I  .....
C    16+NT   I     7+NT     I  FM  I  X(NT) - ACCA NT-O  TOK
C            I              I      I          EPEATOHO HK
C    16+NT+1 I     7+NT+1   I  FM  I  Y1    - OPHATA 1-O TOK
C            I              I      I          EPEATOHO HK
C    ......  I    ......    I      I .......
C    16+2NT  I     7+2NT    I  FM  I  Y(NT) - OPHATA NT-O TOK
C            I              I      I          EPEATOHO HK
C----------------------------------------------------------------------
C  OA HA MSW --> LM<BC> = 16 + 2 * NT
C
C  HA EPEMEH. ACT --> LM<RMSW> = 7 + 2 * NT
C----------------------------------------------------------------------
C
C
C
C                      OPMAT RMSW T # 321
C                 ------------------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0021    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I  FQ - A HOBE HA. BCEH
C            I              I      I   (  CHXPO. MAKPO. P.TIP # 4 )
C    11      I     2        I  DM  I         PEEPB
C    12      I     3        I  FM  I  #  - HOMEP EMEHTA;
C    13      I     4        I  FM  I  #CM  - HOMEP TOKA EMEHTA #1;
C    14      I     5        I  LM  I  ATOKM  - AP B TOKN/N1 HAEH
C            I              I      I           TOKA HOMEP #CM
C            I              I      I           EMEHTA #;
C    15      I     6        I  FM  I  X1  - (CM. PC.)
C    16      I     7        I  FM  I  X2  - (CM. PC.)
C    17      I     8        I  FM  I  Y1  - (CM. PC.)
C    18      I     9        I  FM  I  Y2  - (CM. PC.)
C    19      I     10       I  DM  I  PAR - APAMETP, OPEE
C            I              I      I        COCTOHE BX. CHAA
C            I              I      I        P T=TN.
C            I              I      I        PAR=0 - IBX=Y1
C            I              I      I        PAR=1 - IBX=Y2
C----------------------------------------------------------------------
C  OA HA MSW --> LM<BC> = 19
C
C  HA EPEMEH. ACT --> LM<RMSW> = 10
C----------------------------------------------------------------------
C
C
C                      OPMAT RMSW T # 322
C                 ------------------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0021    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I  FQ - A HOBE HA. BCEH
C            I              I      I   (  CHXPO. MAKPO. P.TIP # 4 )
C    11      I     2        I  DM  I         PEEPB
C    12      I     3        I  FM  I  #  - HOMEP EMEHTA;
C    13      I     4        I  FM  I  #CM  - HOMEP TOKA EMEHTA #1;
C    14      I     5        I  LM  I  ATOKM  - AP B TOKN/N1 HAEH
C            I              I      I           TOKA HOMEP #CM
C            I              I      I           EMEHTA #;
C    15      I     6        I  FM  I  TAU - OCTOHHA BPEMEH AEPK;
C    16      I     7        I  FM  I  HA.C.- HAAHE COB;
C    17      I     8        I  FM  I  NT  - KO-BO TOEK BXOHOO CH.
C    18      I     9        I  DM  I  IPAR- KAATE;
C    19      I     10       I  DM  I  T1  - BPEM;
C    20      I     11       I  DM  I  T2  - BPEM;
C   ....     I    .....     I .... I .............................
C   18+NT    I    9+NT      I  DM  I  TNT - BPEM;
C   18+NT+1  I    9+NT+1    I  DM  I  IBX1- BXOHO CHA P T=T1
C   18+NT+2  I    9+NT+2    I  DM  I  IBX2- BXOHO CHA P T=T2
C   ....     I    .....     I .... I .............................
C   18+2*NT  I    9+2*NT    I  DM  I  IBXNT-BXOHO CHA P T=TNT
C----------------------------------------------------------------------
C  OA HA MSW --> LM<BC> = 18 + 2*NT
C
C  HA EPEMEH. ACT --> LM<RMSW> = 9 + 2*NT
C----------------------------------------------------------------------
C
C
C              OPMAT RMSW T # 323:
C              ----------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0021    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I  FQ - A HOBE HA. BCEH
C            I              I      I   (  CHXPO. MAKPO. P.TIP # 4 )
C    11      I     2        I  FM  I  NBXOD-KOECTBO TOKOB, CO-
C            I              I      I        EMX KAK BXOHE CHA;
C    12      I     3        I  FM  I  #1 - HOMEP EMEHTA;
C    13      I     4        I  FM  I  #CM1 - HOMEP TOKA EMEHTA #1;
C    14      I     5        I  LM  I  ATOKM1 - AP B TOKN/N1 HAEH
C            I              I      I           TOKA HOMEP #CM1
C            I              I      I           EMEHTA #1;
C .......    I   .......    I  ... I  ..........................
C  9+I*3     I     I*3      I  FM  I  #I -      -"-
C 10+I*3     I   1+I*3      I  FM  I  #CMI -      -"-
C 11+I*3     I   2+I*3      I  LM  I  ATOKMI -    -"-
C .......    I   .......    I  ... I  ..........................
C 12+3*NBXOD I  3+3*NBXOD   I  FM  I  K    - KOEHT POOPOHA-
C            I              I      I         HOCT;
C----------------------------------------------------------------------
C  OA HA MSW --> LM<BC> = 12 + 3*NBXOD
C
C  HA EPEMEH. ACT --> LM<RMSW> = 3 + 3*NBXOD
C----------------------------------------------------------------------
C
C
C
C
C                      OPMAT RMSW T # 325
C                 ------------------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0021    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I  FQ - A HOBE HA. BCEH
C            I              I      I   (  CHXPO. MAKPO. P.TIP # 4 )
C    11      I     2        I  DM  I         PEEPB
C    12      I     3        I  FM  I  #  - HOMEP EMEHTA;
C    13      I     4        I  FM  I  #CM  - HOMEP TOKA EMEHTA #1;
C    14      I     5        I  LM  I  ATOKM  - AP B TOKN/N1 HAEH
C            I              I      I           TOKA HOMEP #CM
C            I              I      I           EMEHTA #;
C    15      I     6        I      I         PEEPB
C----------------------------------------------------------------------
C  OA HA MSW --> LM<BC> = 15
C
C  HA EPEMEH. ACT --> LM<RMSW> = 6
CZZZ-------------------------------------------------------------------
       COMMON /PROF1/ MASPRT(100)
       DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
       EQUIVALENCE( NWTR, MASPRT(13) )
       EQUIVALENCE( NWTRE, MASPRT(14) )
       EQUIVALENCE( FGMSW, MASPRT(87) )
       EQUIVALENCE( H0   , MASPRT(30) )
       EQUIVALENCE ( RN  ,   N  )
       INTEGER  TIP, BA0T, FGMSW
       NEL = BA0T(1)
       TIP = BA0T(2)
       NTIP= TIP - 319
       IF ( NTIP .GE. 1 .AND. NTIP .LE. 4 ) GO TO 1
       IF ( NTIP .EQ. 6                   ) GO TO 1
500    CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 501 ) NEL, TIP
501    FORMAT(/' *** O. CO B FM0021 --> HOMEP TA HE HA ***'/
     =  ' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
1      CONTINUE
       GO TO (100,200,300,400,500,600),NTIP
C######################################################################
100   CONTINUE
C----------------------------------------------------------------------
C---> HEHEHOE BEHO ( T  # 320 )
C----------------------------------------------------------------------
C--> OPEEEHE KOHCTAHT
       NT = IFIX ( PAR0T(5) )
       IF ( FGMSW .EQ. 0 ) GO TO 1000
C----------------------------------------------------------------------
C-->  TOHT LM <O >
       LM = 16 + 2*NT
       RETURN
C######################################################################
1000   CONTINUE
C-->  PHT  TO MOHO COPMPOBAT..
       LM = 7 + 2*NT
       DO 102 I=1,LM
102    RMSW(I) = 0.0
       N = 1
       RMSW(2) = RN
C---> POBEPM  PABHO  BBEEH AHHE?
       NT1 = (IFIX ( PAR0T(4) ) - 1 ) / 2
       IF ( NT .EQ. NT1 ) GO TO 103
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 502 ) NEL, TIP
502    FORMAT(/' *** OKA B FM0021 -->HEPABHO AAHO KOECTBO TO
     =EK ***'/' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
103    CONTINUE
       NELBX = IFIX ( PAR0T(1) )
       IF ( NEL .LE. NELBX ) GO TO 9999
C----------------------------------------------------------------------
C---->  AOMHM # , #CM    P
       N = IFIX( PAR0T(1) )
       RMSW(3)=RN
       N = IFIX( PAR0T(2) )
       RMSW(4)=RN
       N = IFIX ( PAR0T(3) )
       RMSW (6) = RN
C---->  AOMHM CO TOEK EPEATOHO XAPAKTEPCTK ---------
       N = NT
       RMSW(7) = RN
       IF( NT.EQ. 0 ) GO TO 105
C---->  AOMHM KOOPHAT TOEK EPEATOHO XAPAKTEPCTK ---------
          DO 104 I = 1 , NT
          RMSW(7+I)   = PAR0T( 4 + 2*I )
          RMSW(7+NT+I)= PAR0T( 5 + 2*I )
104    CONTINUE
105    CONTINUE
C----> POBEPM,PABHO  BBEEH AHHE ?
       DO 106 I=2,NT
       IF ( RMSW(6+I) .LE. RMSW(7+I) ) GO TO 106
C----------------------------------------------------------------------
       WRITE(NWTRE, 505 ) NEL, TIP
505    FORMAT(/' *** OKA B FM0021-->OPHAT AAH HE B BOPACAEM
     =OPKE ***'/' *** HOMEP EMEHTA',I5,' HOMEP TA',I5,' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
106    CONTINUE
       RETURN
C######################################################################
200   CONTINUE
C----------------------------------------------------------------------
C---> PEEHOE BEHO ( T  # 321 )
C----------------------------------------------------------------------
C-->  PHT  TO MOHO COPMPOBAT..
       LM = 10
       DO 212 I=1,LM
212    RMSW(I) = 0.0
C---> POBEPM  PABHO  BBEEH AHHE?
       X1 =  PAR0T(3)
       X2 =  PAR0T(4)
       Y1 =  PAR0T(5)
       Y2 =  PAR0T(6)
       IF ( X1 .LT. X2  .AND. Y1 .LT. Y2 ) GO TO 213
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 512 ) NEL, TIP
512    FORMAT(/' *** OKA B FM0021 -->HEPABHO AAH APAMETP
     =***'/' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
213    CONTINUE
       NELBX = IFIX ( PAR0T(1) )
       IF ( NEL .LE. NELBX ) GO TO 9999
C----------------------------------------------------------------------
C---->  AOMHM # , #CM , X1 , X2 , Y1 , Y2.
       N = NELBX
       RMSW(3) = RN
       N = IFIX( PAR0T(2) )
       RMSW(4) = RN
       RMSW(6) = X1
       RMSW(7) = X2
       RMSW(8) = Y1
       RMSW(9) = Y2
       RETURN
C######################################################################
300    CONTINUE
C----------------------------------------------------------------------
C--> BEHO "CTOE AABAHE" ( T # 322 )
C----------------------------------------------------------------------
C-->  PHT  POBEPT AHHE.
       TAU = PAR0T(3)
       IF ( TAU .GE. 0. ) GO TO 314
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 513 ) NEL, TIP
513   FORMAT(/' *** OKA B FM0021 -->HEPABHO AAHA BPEM.AEPKA
     =***'/' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
       CALL SUBERR(TIP)
C!!!-------------------------------------------------------------------
314    CONTINUE
       NT = 2 * ( IFIX( TAU / H0 ) + 1 )
       IF ( NT .LE. 100 ) GO TO 315
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 514 ) NEL, TIP
514   FORMAT(/' *** OKA B FM0021 -->CKOM OA BPEMEH. AEPKA
     =***'/' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
       CALL SUBERR(TIP)
C!!!-------------------------------------------------------------------
315    CONTINUE
       IF ( FGMSW .EQ. 0 ) GO TO 3000
C----------------------------------------------------------------------
C-->  TOHT LM <O >
       LM = 18 + 2 * NT
       RETURN
C######################################################################
3000   CONTINUE
       LM = 9 + 2 * NT
       DO 316 I=1,LM
316    RMSW(I) = 0.0
       NELBX = IFIX ( PAR0T(1) )
       IF ( NEL .LE. NELBX ) GO TO 9999
C----------------------------------------------------------------------
C---->  AOMHM # , #CM , TAU , HU
       N = NELBX
       RMSW(3) = RN
       N = IFIX( PAR0T(2) )
       RMSW(4) = RN
       RMSW(6) = PAR0T(3)
       RMSW(7) = PAR0T(4)
       N = NT
       RMSW(8) = RN
       N = 19
       RMSW(9) = RN
       RETURN
C######################################################################
400    CONTINUE
C----------------------------------------------------------------------
C-->  MHOTE ( T # 323 )
C----------------------------------------------------------------------
C--> OPEEEHE KOHCTAHT
       NBXOD = IFIX ( PAR0T(3) )
       IF ( FGMSW .EQ. 0 ) GO TO 4000
C----------------------------------------------------------------------
C-->  TOHT LM <O >
       LM = 12 + 3*NBXOD
       RETURN
C######################################################################
4000   CONTINUE
C-->  PHT  TO MOHO COPMPOBAT..
       LM = 3 + 3*NBXOD
       DO 420 I=1,LM
420    RMSW(I) = 0.0
       RMSW(3+3*NBXOD) = PAR0T(1)
C---> POBEPM  PABHO  BBEEH AHHE?
       NBXOD1 = ( IFIX( PAR0T(2)) - 1 ) / 2
       IF ( NBXOD .EQ. NBXOD1 ) GO TO 422
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 502 ) NEL, TIP
       CALL SUBERR(TIP)
C!!!-------------------------------------------------------------------
422    CONTINUE
C---> AOMHM HOMEPA BXOHX TOKOB.
       N = NBXOD
       RMSW(2) = RN
       IPAR0T = 3
       IRMSW = 2
          DO 425 I = 1 , NBXOD
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          N = IFIX ( PAR0T(IPAR0T) )
          RMSW(IRMSW) = RN
          IPAR0T = IPAR0T + 1
          IRMSW = IRMSW + 1
          N = IFIX ( PAR0T(IPAR0T) )
          RMSW(IRMSW) = RN
          IRMSW = IRMSW + 1
425    CONTINUE
       RETURN
C######################################################################
600   CONTINUE
C----------------------------------------------------------------------
C---> KOPEH KBAPATH (T  # 325 )
C----------------------------------------------------------------------
C-->  PHT  TO MOHO COPMPOBAT..
       LM = 6
       DO 612 I=1,LM
612    RMSW(I) = 0.0
C---> POBEPM  PABHO  BBEEH AHHE?
       NELBX = IFIX ( PAR0T(1) )
       IF ( NEL .LE. NELBX ) GO TO 9999
C----------------------------------------------------------------------
C---->  AOMHM # , #CM
       N = NELBX
       RMSW(3) = RN
       N = IFIX( PAR0T(2) )
       RMSW(4) = RN
       RETURN
C######################################################################
9999   CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 555 ) NEL, TIP,NELBX
555    FORMAT(/' *** OKA B FM0021 -->HEPABHO AAH BXOHE AHH
     =E   ***'/' *** HA BXO EMEHTA ', I5, ' HOMEP TA ', I5,' ***',
     =         ' *** OAETC TOK EMEHTA, MEEO O OPKOB
     =HOMEP ( # ' ,I3,' )  *** ')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
       RETURN
       END

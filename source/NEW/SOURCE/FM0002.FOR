         SUBROUTINE FM0002( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:  FM0002( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C   HAHAEHE:    HACTPOKA MAKPOMOE:
C  ------------
C                  T     M MOE     OOHAEH B AHEEM
C
C                   8        O (HE P. BEHT)    D
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOE.
C
C   METO:         PAMETKA OE RMSW O MOE "EAH K";
C   -----
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO HE POBOTC;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C                  RMSW TA # 8 [ EAH O ]
C                  --------------------------------
C                                                             LM = 4
C                                                             ******
C
C                    SRAB
C             +------+--------+-----+-------+
C             I COCT.I BPEM  I PE  I PE    I         KD = 0 BEHT
C             I TEK. I EPEK.I E  I  E   I                AKPT;
C             I "KD" I "TX"  .I PB  I   PB  I
C             +------+--------+-----+-------+         KD = 1 BEHT
C             I  1   I   2    I  3  I   4   I                OTKPT;
C             +------+--------+-----+-------+
C             I  11  I   12   I 13  I  14   I         TX - BOMOHOE
C             +------+--------+-----+-------+              BPEM KOM-;
C
C
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
         INTEGER BA0T, TIP
         TIP = BA0T(2)
         NEL = BA0T(1)
         NWTR = MASPRT(13)
         IF ( TIP .EQ. 8 ) GO TO 1
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(14)
         WRITE(NWTRE, 500) TIP, NEL
500      FORMAT(' *** O. CO B FM0002: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C######################################################################
C######################################################################
1        CONTINUE
C---> OHEHE BCEX OE RMSW  OA
         LM = 4
         DO 100 I=1,LM
100      RMSW(I) = 0.0
         RETURN
         END

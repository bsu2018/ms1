         SUBROUTINE FM0009( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EBPA 1989 .)------------
C   OPOPAMMA:  FM0009( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C   HAHAEHE:    HACTPOKA MAKPOMOE:
C  ------------
C                  T     M MOE     OOHAEH B AHEEM
C
C                  10       "MOCT APOHOBA"          TM
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOE.
C
C   METO:         PAMETKA OE RMSW O MOE "MOCT APOHOBA"
C   -----
C
C          CB C BHEHE CPEO:
C
C            AA "A"       AA "B"     AA  "C"
C              ! I             ! I          ! I
C              ! V             ! V          ! V
C   +--< V1 ]--*---------------!------------!----< V4 ]----+
C   !           <-- IV1        !            !   <-- IV4    !
C   !                          !            !              !
C   *--< V2 ]------------------*------------!----< V5 ]----*
C   !           <-- IV2                     !   <-- IV5    !
C   !                                       !              !
C   +--< V3 ]-------------------------------*----< V6 ]----*
C   !           <-- IV3                                    !
C   !                                            <-- IV6   !
C   !    ---> TOK_A                         ----> TOK_K    !
C   +---------*                                 *----------+
C     "AHO"  +                                 - "KATO"
C
C
C
C     KO-BO OB --- 5
C
C     BBOHE HAEH- 17 T. :
C      TOK A-A, TOK A-B, TOK A-C, TOK AHOA, TOK KATOA,
C      TOK V1, V2, V3, V4, V5, V6,
C      C V1, V2, V3, V4, V5, V6;
C
C   KOPOBKA:
C
C    # T 10   1 2 3 4 5
C                MAS < KOD [#] >;
C
C E:  KOD = 0 EC MOCT HE PABEM, TOA # MOHO HE KOPOBAT;
C
C       KOD = 1 EC MOCT PABEM, TOA --->
C                # - HOMEP EMEHTA E, BMC C  MOCTA
C                      B TOKN/N1 COEPATC: MMCIU - 6 TK.
C                                            HXCIU - 6 TK.
C                B COOTBETCTB CO CXEMO  HMEPAE BEHTE
C
C     COETC: FM0009, LM0011, DM0013;
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO B MHMAHOM O"EME;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. PEAA  KOD  = 0  , KOD  = 1
C                     P OKAX --> PHMAETC KOD  = 0;
C                     HAEH APAMETPOB ---->
C
C              OPMAT MSW # TA 10 [ MOCT APOHOBA  ]:
C              -------------------------------------------
C               "OCTOHHA" ACT
C               ******************
C
C  +-------+--------+--------+--------+------+------+--------+----+
C  I HOMEP I HOMEP  I HOMEP  I  P. I KO I KO. I AP.   I PE I
C  I EME I TA   I /    I TA   I ECT I BB. I HA.  I E I
C  I HTA   I MOE I PACETAI (PE.) I BO   I HA I "TOKN" I PB I
C  I       I        I DM.....I       .I OBI EH.I "TOKN1"I    I
C  +-------+--------+--------+--------+------+------+--------+----+
C  I  1    I   2    I   3    I    4   I   5  I  6   I   7    I 8  I
C  +-------+--------+--------+--------+------+------+--------+----+
C
C
C              EPEMEHHA ACT MSW:
C              *********************
C
C  COBO     COBO     OOHAEH  OECKOE HAEHE
C  OT        OT
C  HAAA    EPEMEH.
C  MSW       ACT         BEHTE:
C                       :
C  9 - AA "A"           V1 --> <09> - <12>
C 10 - AA "B"           V2 --> <10> - <12>
C 11 - AA "C"           V3 --> <11> - <12>
C 12 - "A"- AHO          V4 --> <13> - <09>
C 13 - "K" - KATO        V5 --> <13> - <10>
C                         V6 --> <13> - <11>
C
C    14       1   [FM]  1 PHAK PABEH --> 0 HET,
C                                                1 - PABEHE O C
C    15       2   [FM]  NPB - #., AEO C
C    16       3   [LM]  ANPB - APEC #. B MSWALL
C    17       4   [LM]  AUYP - APEC B TOKN/N1 HAEH C
C    18       5   [ ]    PEEPB
C    19       6   [ ]    PEEPB
C    20-25    7-12    MSWX[1] V1-> #O.; KD  HX  MMCIU  HXCIU  . KD
C    26-31   13-18    MSWX[2] V2-> #O.; KD  HX  MMCIU  HXCIU  --
C    32-37   19-24    MSWX[3] V3-> #O.; KD  HX  MMCIU  HXCIU  --
C    38-43   25-30    MSWX[4] V4-> #O.; KD  HX  MMCIU  HXCIU  --
C    44-49   31-36    MSWX[5] V5-> #O.; KD  HX  MMCIU  HXCIU  --
C    50-55   37-42    MSWX[6] V6-> #O.; KD  HX  MMCIU  HXCIU  ---
C
C  LM<O> = 55;      LM<OKAH>=42
C
C
C
C               PAO MACCB TA  MSWX TA # A9 (  T. #10)
C             -------------------------------------
C
C
C  +-------+--------+--------+--------+------+-------+
C  I HOMEP I  "KD"  I "HX"   I MMCIU  I  HX  I A  I
C  I O - I P-  I BPEM  I KO PI CIU  I       I
C  I EME I HAK CO-I PE-OI M I M-CAI "KD"  I
C  I HTA   I CTOHI KOM-I  CA    I      I       I
C  +-------+--------+--------+--------+------+-------+
C  I  1    I   2    I   3    I    4   I   5  I       I
C  +-------+--------+--------+--------+------+-------+
C
C   MMCIU    HXCIU OPEEET MOHTOP MAKPOMOE BEHT......
C
C
C    BEHT OTKPT ==>  KD=1;;;;BEHT AKPT ==>  KD=0;
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
         INTEGER BA0T, TIP
         EQUIVALENCE ( NUM, RNUM )
         TIP = BA0T(2)
         NEL = BA0T(1)
         NWTR = MASPRT(13)
         IF ( TIP .EQ. 10 ) GO TO 1
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(14)
         WRITE(NWTRE, 500) TIP, NEL
500      FORMAT(' *** O. CO B FM0009: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C######################################################################
1        CONTINUE
C---> OHEHE BCEX OE RMSW  BEHT  (INTEGER)
         LM = 42
         NUM = 0
         DO 100 I=1,LM
100      RMSW(I) = RNUM
C===> PHT CXO. APAMETP:
C-->  KOTPO
         KKM = PAR0T(1)
C-->  B PABEH..
         PMAS = PAR0T(2)
         IF ( PMAS .EQ. 0.0 ) GO TO 2
         IF ( PMAS .EQ. 1.0 ) GO TO 3
C----------------------------------------------------------------------
         WRITE(NWTR,4) NEL, PMAS
4        FORMAT(' *** T PABEH "C"  EMEHTA #', I5, ' ***',
     =          ' *** PABH ', F5.0,' OOEH  --> PHMAETC',
     =    ' PABHM H ***')
         GO TO 2
C----------------------------------------------------------------------
2        CONTINUE
C-->  PMAS = 0   HE PAB. BEHT.....
C MMCIU=11,  HXCIU=0.0
         NUM=11
         DO 201 I=1,6
         JMSW =  (I-1)*6 + 10
         RMSW(JMSW) = RNUM
         RMSW(JMSW+1) = 0.0
201      CONTINUE
         GO TO 5
C######################################################################
3        CONTINUE
C---> PMAS = 1 --> ECT PABEHE (HOMEP -TA)
         NUM=INT(PAR0T(3))
         RMSW(2) = RNUM
C--> PHAK PABEH....
         NUM = 1
         RMSW(1) = RNUM
         GO TO 5
C----------------------------------------------------------------------
5         CONTINUE
          RETURN
          END

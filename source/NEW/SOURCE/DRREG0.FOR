       SUBROUTINE DRREG0 ( KAR, DAT, MSWALL , TOKN , VALLN )
CXXX****KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)*************
C*                                                                     *
C*  OPOPAMMA :   DRREG0 ( KAR, DAT, MSWALL , TOKN , VALLN )        *
C*  ------------                                                       *
C*                                                                     *
C*  HAHAEHE :  * OPAOTK PEKTB #PEM *                      *
C*  ----------      PEM CCKA  AAHE "HAAHX" AHHX          *
C*                  TOKOB  HAPEH COACHO CCKA:                *
C*                  #PEM                                             *
C*                  "V" Z "" K1 K2....;                               *
C*                  "V" Z "" "BCE";                                   *
C*                  "I" Z "" N1[.S1]...N2...N3[.BCE];                 *
C*                  "GAMMA" Z "" N1 N2...;                            *
C*                  "OMEGA" Z "" N1 N2...;                            *
C*                  "I" Z "" "BCE";                                   *
C*                   #BCE                                              *
C*           *** TO HE PEAOBAHO - TOKO OHEHE -               *
C*                                                                     *
C*  METO :         BBO  PAOP AHHX , KOHTPO  AC           *
C*  -----           B TOKN  VALLN COACHO HA B MSWALL.          *
C*                                                                     *
C*  APAMETP :    KAR,DAT - OCAHE CTPOK PEKTB;                *
C*  ---------       MSWALL - CCOK PABX COB EMEHTOB;        *
C*                    TOKN - "TOK" EMEHTOB;                         *
C*                   VALLN - "HAPEH" EMEHTOB.                   *
C*                                                                     *
C*  OPOPAMM :   1.PROF1 , SERVZ1                                   *
C*  -----------     2.INPUT2                                           *
C*                                                                     *
C*  PMEAHE:     ACTHA PEAA                               *
C* ------------     ( MAKET POPAMM ).                               *
C*                                                                     *
CZZZ********************************************************************
      COMMON / PROF1 / MASPRT(200)
      DIMENSION KAR(1),DAT(1),MSWALL(1),TOKN(1),VALLN(1)
      EQUIVALENCE ( NNTOK , MASPRT(46) )
      EQUIVALENCE ( FMSWAL , MASPRT(74) )
      EQUIVALENCE ( NMKNOT , MASPRT(7) )
      INTEGER FMSWAL
C-->
      EQUIVALENCE ( NWTR , MASPRT(13) )
      EQUIVALENCE ( NRDR , MASPRT(12) )
      IF( FMSWAL.EQ.0 ) WRITE (NWTR,1)
 1    FORMAT(/' *** HAPEH OPOK CEOBAH PEKTB.',
     *' PEKTBA HOPPETC ***'/)
      IF( FMSWAL.EQ.0 ) RETURN
C-->   AHEHE E BCKOO...
C----------------------------------------------------------------------
      DO 100 I=1,NNTOK
      TOKN(I)=0.0
 100  CONTINUE
C----------------------------------------------------------------------
      DO 200 I=1,NMKNOT
      VALLN(I)=0.0
 200  CONTINUE
C----------------------------------------------------------------------
C
       WRITE(NWTR,3000)
3000   FORMAT(/' *** PEKTBA #PEM - OKA  HE PEAOBAHA ***'  )
       WRITE(NWTR,3004)
3004  FORMAT(' *** HAAHE HAEH BCEX EPEMEHHX CTAHABBATC',
     =' PABHM 0.0 ***'/)
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
      RETURN
      END

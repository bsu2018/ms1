       SUBROUTINE READST( KAR, IK, IKMAX, NRDRX, DSNAME, KFIZ, KODEOF )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA: READST( KAR, IK, IKMAX, NRDRX, DSNAME, KFIZ, KODEOF )
C   ------------
C   HAHAEHE:  BBO CTPOK CXOHX AHHX  / INPUT0,
C   ----------   EPE KAHA NRDRX B OPMATE 80A1;
C                 CTAHOBKA IK=1  IKMAX=72 <--- BCEA;
C   METO:       OEBEH
C   -----
C   APAMETP:   KAR - MACCB BBEEHHO HOPMA ( HE OEE 80 );
C   ---------    IK, IKMAX - PAH "OEHO" HOPMA;
C                NRDRX - KAHA BBOA;
C                DSNAME - MACCB C DIM *(11) - M AA BBOA
C                          POPAMM CE. OCBAH;
C                KODEOF = 0  -  CTPOKA BBEEHA;
C                       = 1  -  CTA END OF FILE HA NRDRX;
C                KFIZ = 1  - "AKT" BBOA -KO CTPOK COBEPEH;
C                     = 0  - -K CTPOK HE BBO;
C
C   OPOPAMM:   HET;
C   ------------
C   PMEAHE:   KOHTPO HE POBOTC;
C   ----------
C
CZZZ-------------------------------------------------------------------
       DIMENSION KAR(1), DSNAME(1)
       COMMON /PROF1/ MASPRT(200)
       EQUIVALENCE (MASPRT(127), NZRDR)
       DATA KOD/0/, K80/80/
       KODEOF = 0
C******IF ( KOD .EQ. 1 ) GO TO 99
C----------------------------------------------------------------------
       READ(NRDRX,1, END=99 ) (KAR(I), I=1,K80)
       NZRDR = NZRDR + 1
1      FORMAT(80A1)
       IK = 1
       IKMAX = 72
       KFIZ = 1
       RETURN
99     CONTINUE
       KODEOF = 1
       KOD = 1
       RETURN
       END

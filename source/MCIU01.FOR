       INTEGER FUNCTION MCIU01( TN, H0, HX, TMAS )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA_HK MCIU01( TN, H0, HX, TMAS )
C   ---------------------
C   HAHAEHE:        "EHEPATOP"  "BEPKATOP" MCOB PABEH
C  -----------         C  MOE PAB. BEHT  EAH K.
C                         BAPAHT # 01:
C                         -------------
C
C
C
C                         I<-HX->I        :<-HX->:
C                         $      $        :      $
C                       **********        :      ******
C                       **********        :      ******
C                       **********        :      ******
C  T0*--------I---------**********-----I--$------******----------------
C    I        I         I        I     I         I
C    I<-TZAD->I         I<-TIMP->I     I         I
C    I        I<-TALFA->I        I     I<-TALFA->I
C             I                        I
C             I<---------TCIU--------->I<-----------TCIU--------
C
C  B OCAH E COETC CE. AC:
C
C   ... MAS < 1 TZAD  TCIU  TALFA  TIMP  TKCIU  DTALFA >
C
C  BOMOHO KOPOBAHE:
C
C   ... MAS < 0 >, TO COOTBETCTBET:
C
C       TZAD = 360 . PA.
C       TCIU = 60  . PA.
C       TAFLA= 30  . PA.
C       TIMP = 10  . PA.
C       TKCIU=  0  . PA.
C       DTALFA= 0  . PA.
C
C      EC COOBAH CE. OOHAEH:
C
C
C      TZAD  - BPEM AEPK BKEH "C"    ( . PA. )
C      TCIU  - EPO OBTOPEH CHAOB "C"  ( . PA. )
C      TALFA - AEPKA EPEHEO POHTA CHAA
C              "C" OT HAAA OEPEHOO EPOA
C              OBTOPEH                          ( . PA. )
C      TIMP - TEHOCT MCA "C"          ( . PA. )
C      TKCIU - EPE CKOKO EPOOB OBTOPEH
C              CHAA "C" MEHT TALFA HA
C      DTALFA - EH - A MEHEH TALFA       ( . PA. )
C
C              - B CAE, KOA TEKEE HAEHE TALFA - "AEPKA
C                EPEHEO POHTA C" CTAHET OE TCIU - "EPOA
C                OBTOPEH C" ( TO BOMOHO P TKCIU  DTALFA
C                HE PABHM H ), POBOTC #CTAA#
C                                               ------------
C                MCA "C":
C                ---------------
C                                            ******
C                                            ******
C                                            ******
C                                            ******
C   *----------------------*-----------------******--------------------
C   I(TGO)                 I(TGO+TCIU)       I
C   I                      I                 I
C   I                      I                 I
C   I<-------( TGO + TALFA<TEKEE> )------->I
C   I                      I                 I
C------------<HOBOE TZAD>->I                 I
C                          I<-<HOBOE TALFA>->I
C                          I     PABHOE      I
C                              <AAHHOE>
C                              < TALFA  >
C
C
C              TGO - BPEM HAAA OEPEHOO EPOA C,  KOTOPOO
C                    AKCPOBAHA CTA TALFA<TEKEE> >= TCIU
C              TALFA = TALFA<AAHHOE> + [ NP/KCIU ] * DTALFA -
C                   TEKEE HAEHE "AAPKA EPEHEO POHTA";
C              NP = [ (TN-TZAD) / TCIU ] - HOMEP EPOA "C"
C                   ( P NP < 0, NP = 0 );
C
C              #CTAA# AKAET B CEEM:
C
C               TZAD <HOBOE>    = TGO + TCIU
C
C               TALFA < HOBOE > = TALFA <AAHHOE>
C
C               NP < HOBOE >    = 0 ( CPACBAETC).
C
C               #EHEPPETC COOEHE HA NWTRE KAHA....#
C
C
C
C
C   METO:     "BOCTAHOBEHE" HK "C"(T)  KOHTPO
C   -----      HA "C" P T = TN;
C   APAMETP: TN - BPEM OPEEEH HA MCA "C";
C   ---------  H0 - "OKPECHOCT" OPEEEH C
C              HX - "A" OT TN O POHTA MCA "C"(CM. PC.)
C
C
C              TMAS - "OTPEOK" MACCBA MSWALL - OCAHE "C":
C
C  +------+------+-------+------+-------+--------+
C  I TZAD I TCIU I TALFA I TIMP I TKCIU I DTALFA I
C  +------+------+-------+------+-------+--------+
C  I  1   I  2   I   3   I  4   I   5   I    6   I
C  +------+------+-------+------+-------+--------+
C
C  HAEHE HK:  MCIU01 = 00  --> "C" HET B [TN, TN+H0];
C  ****************                     HX = 0.0;
C                     MCIU01 = 01  --> B [TN, TN+H0] -> EPEH POHT
C                                      MCA "C", HX = HTEPBA OT
C                                      TN O EPEHEO POHTA "C";
C                     MCIU01 = 10 -->  B [TN, TN+H0] -> AH POHT
C                                      MCA "C", HX = HTEPBA
C                                      OT TN O AHEO POHTA MCA;
C                     MCIU01 = 11 -->  B [TN, TN+H0] "C" BKEH,
C                                      COCTOHE HE MEH, HX=0.0;
C
C                !!!!!  P H0 > TIMP --->  BOMOHA OPEP "C" !!!!!
C                      PEOOAETC, TO H0 < TIMP
C
C
C   OPOPAMM:      PROF1
C   -------------
C
C   PMEAHE:        1. KOHTPO HE POBOTC;
C  -----------         2. MC ECT, EC:
C                         .... <= TN <= .....
CZZZ-------------------------------------------------------------------
       COMMON /PROF1/ MASPRT(200)
       EQUIVALENCE ( NWTRE, MASPRT(14) )
       DIMENSION TMAS(*)
100     CONTINUE
       MCIU01 = 0
         HX = 0.0
C-->  BOCTAHOBM MC....
C  NP - HOMEP EPOA "C", TGO -  BPEM HAAA EPOA "C",
C        !!! BOCTAHOBEHE O TN !!!
C
         TZAD = TMAS(1)
         TCIU = TMAS(2)
         TALFA = TMAS(3)
         TIMP = TMAS(4)
         KCIU = IFIX( TMAS(5) )
         DTALFA = TMAS(6)
C---->
         RNP = 0.0
         NP = 0
         T1 = TN - TZAD
         IF ( T1 .LT. 0.0 ) GO TO 1
         RNP = T1/ TCIU
         NP = INT( RNP )
         RNP = FLOAT( NP )
1        CONTINUE
C-->  BPEM HAAA TEK. "C"
         TGO = TZAD + RNP * TCIU
C-->  CMEEHE BHTP EPOA "C" ..
         IF ( KCIU .EQ. 0  .OR.  DTALFA .EQ. 0.0 ) GO TO 2
C-->  TALFA MEHAC...
         IALFA = NP / KCIU
         TALFA = TALFA + FLOAT(IALFA) * DTALFA
C----------------------------------------------------------------------
C-->  HE HHO  CTAPOBAT "C" ?
        IF ( TALFA .LT. TCIU ) GO TO 2
C--->  CTAA...
        TMAS(1) = TGO + TCIU
C---->
        WRITE(NWTRE, 3 ) TN, TGO, TMAS(1)
3      FORMAT(' *** P TN=', G12.5, ' BOHEHA "CTAA"',
     =        ' CHAA "C" ***'/
     =        ' *** TGO = ', G12.5, ', HOBOE BPEM TZAD = ', G12.5,
     =        ' ***' )
       GO TO 100
2        CONTINUE
C--->  TI1 - EPEH POHT, TI2 - AH POHT...
         TI1 = TGO + TALFA
         TI2 = TI1 + TIMP
C--->  KAKO HTEPBA...
C  M1 - HAEHE "C"  TN,
C  M2 - HAEHE "C"  TN + H0;
         M1 = 0
         M2 = 0
         TN1 = TN + H0
         IF ( TI1 .LE. TN  .AND.  TN .LT. TI2  )  M1 = 1
         IF ( TI1 .LE. TN1  .AND.  TN1 .LE. TI2  )  M2 = 1
C--->   M1 = 0  M2 = 0  -->  MCA HET...
         IF ( M1 .EQ. 0  .AND.  M2 .EQ. 0 ) RETURN
C--> ECM M1  M2 --> 1,  MC HE "KOH"....
         MCIU01 = 11
         IF ( M1 .EQ. 1  .AND.  M2 .EQ. 1 ) RETURN
C-->  OCTM, TO M1 = 0,  -->  EPEH POHT...?
         MCIU01 = 01
         HX = TI1 - TN
C-->  TAK  TO?
         IF ( M1 .EQ. 0 ) RETURN
C-->  HE TAK....
         MCIU01 = 10
         HX = TI2 - TN
         RETURN
         END

       SUBROUTINE APFSEE ( II , R , INDEX ,DATAR , KINDEX )
CXXX****KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)*************
C*                                                                     *
C*  OPOPAMMA :  APFSEE ( II , R , INDEX ,DATAR , KINDEX )          *
C*  ------------                                                       *
C*                                                                     *
C*  HAHAEHE :    OPEEEHE CEHHOO HAEH EHTKATOPA.    *
C*  ----------                                                         *
C*                                                                     *
C*  METO :         OCK COOTBETCTB B TAE MEH EHTKATOPOB  *
C*  -----           C OMO OCEOBATEHOO EPEOPA.              *
C*                                                                     *
C*  APAMETP :     II - M EHTKATOPA .                          *
C*  ---------       R - HAEHE EHTKATOPA .                      *
C*                  INDEX - MACCB MEH EHTKATOPOB .              *
C*                  DATAR - MACCB CEHHX HAEH EHTKATOPOB .*
C*                  KINDEX - PAMEPHOCT  INDEX  DATAR .              *
C*                                                                     *
C*  OPOPAMMA :  HET , ( SUBERR(1) , COMMON : PRINTN )              *
C*  ------------                                                       *
C*                                                                     *
CZZZ********************************************************************
C*
       DIMENSION INDEX(1) , DATAR(1)
C!!!-------------------------------------------------------------------
       COMMON / PRINTN / NW6
C!!!-------------------------------------------------------------------
       DO 1 J=1,KINDEX
       IF ( II .NE. INDEX(J) ) GO TO 1
       R = DATAR(J)
       RETURN
 1     CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NW6,10)
 10    FORMAT(1X,'***** CO B APFSEE.HAEHE EHT. HE HAEHO.')
       CALL SUBERR(1)
C!!!-------------------------------------------------------------------
       RETURN
       END

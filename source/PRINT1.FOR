         SUBROUTINE PRINT1( MSWALL, RMSWALL )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:  PRINT1( MSWALL )
C   ------------
C   HAHAEHE:    KOHTPOHA EAT MACCBA
C   ----------     PABX COB MAKPOMOEE
C
C
C   METO:         EAT OT MSWALL(1) O MSWALL( MSWALL(3)+10 )
C   -----
C
C   OPOPAMM: COMMON PROF1;
C  -------------
C
C  PMEAHE:
C  -----------
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION MSWALL(*), RMSWALL(*)
         EQUIVALENCE ( NWTRE     , MASPRT(14) )
C
         IMAX = MSWALL(3)
C
         IM1 = IMAX + 10
C--------------------------------------------------------------------
C       
C--------------------------------------------------------------------
         WRITE(NWTRE,1) IM1
1        FORMAT( 1X, 128('*')/'    MACCB MSWALL OT [ 1 O ',I5,' ]')
         WRITE(NWTRE,2) ( MSWALL(I), I=1,IM1 )
2        FORMAT(1X, 10I10/ )
         WRITE(NWTRE,3) IM1
3        FORMAT( 1X, 128('*')/'    MACCB RMSWALL OT [ 1 O ',I5,' ]')
         WRITE(NWTRE,4) ( RMSWALL(I), I=1,IM1 )
4        FORMAT(1X, 10E10.4/ )
         WRITE(NWTRE,5)
5        FORMAT( 1X, 128('*') )
         RETURN
         END

       SUBROUTINE FM0032(BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT    MA    1988 .)-----------
C   OPOPAMMA: FM0032(BA0T, KNOTT, PAR0T, RMSW, LM )
C   -------------
C   HAHAEHE:  HACTPOKA MAKPOMOE MKPOPOECCOPHO CCTEM,
C   ----------   OCTPOEHHO HA AE MKPOPOECCOPA CEP
C                K1801BM1(2,3), MEEO CCTEM KOMAH PM "DEC".
C                ******************************************************
C                PHAK TIP # 5 -> O. MAKPOMOE.
C                ( DM0036,LM0013 )
C                      OPMPET "EPEMEHH" ACT RMSW OCAH
C
C                 HA  MSW  ==>+56
C----------------------------------------------------------------------
C
C        OPAHA AMT B PEAHO MKPOPOECCOPHO CCTEME
C         B MOEPE EE POPAMME (MACCB MEM  MEM1)
C        -------------------------------------------------------
C
C        OPAHA AMT B PEAHO MKPOPOECCOPHO CCTEME
C
C               COBO
C           AT      AT                      AT
C        *---------+---------*               *---------*
C        ! CTAP ! MA ! 000000        ! MA ! 000000
C        ! CTAP ! MA ! 000002        ! CTAP ! 000001
C        ! CTAP ! MA ! 000004        ! MA ! 000002
C        ! CTAP ! MA ! 000006        ! CTAP ! 000003
C        ! CTAP ! MA ! 000010        ! MA ! 000004
C        ! CTAP ! MA ! 000012        ! CTAP ! 000005
C        ! CTAP ! MA ! 000014        ! MA ! 000006
C        ! ................. ! ......        ! CTAP ! 000007
C        ! ................. ! ......        ! ....... ! ......
C        ! ................. ! ......        ! ....... ! ......
C        ! CTAP ! MA ! 003774        ! MA ! 003776
C        ! CTAP ! MA ! 003776        ! MA ! 003777
C        *---------+---------*               *---------*
C
C
C        OPAHA AMT B MOEPE POPAMME
C            (MACCB   MEM    MEM1 )
C     *-------------------------------------*
C     ! COBO C APECOM 000000              ! 0001
C     ! CTAP AT COBA C APECOM 000000 ! 0002
C     ! COBO C APECOM 000002              ! 0003
C     ! CTAP AT COBA C APECOM 000002 ! 0004
C     ! COBO C APECOM 000004              ! 0005
C     ! CTAP AT COBA C APECOM 000004 ! 0006
C     ! COBO C APECOM 000006              ! 0007
C     ! CTAP AT COBA C APECOM 000006 ! 0008
C     ! COBO C APECOM 000010              ! 0009
C     ! CTAP AT COBA C APECOM 000010 ! 0010
C     ! ................................... ! ....
C     ! ................................... ! ....
C     ! ................................... ! ....
C     ! COBO C APECOM 003776              ! 2047
C     ! CTAP AT COBA C APECOM 003776 ! 2048
C     *-------------------------------------*
C
C
C
C
C----------------------------------------------------------------------
C              EMEHT OCBAETC:
C
C          # T 380  0   # #CM
C                          FTAKT  ADRPPTI  ADRPRTO ADRPROG  P  ;
C
C         E
C          # #CM- HOMEP EMEHTA  HOMEP TOKA ,KO-P OKAET
C                   K MKPOPOECCOP ,KAK BXOHO CHA  ;
C          FTAKT  - TAKTOBA ACTOTA MKPOPOECCOPHO CCTEM ;
C          ADRPRTI- APEC OPTA BBOA  B BOCMHOM BE ;
C          ADRPRTO- APEC OPTA BBOA B BOCMHOM BE ;
C          ADRPROG- APEC EK O (B BOCMHOM BE), AE
C                   KOTOPOO HE PAMEATC H PABA POPAMMA
C                   H AHHE ;
C          P   -    A OTAOHO EAT:
C                   P=0 - OTAOHA EAT OTMEHETC ,
C                   P=1 - OTAOHA EAT APEAETC - OCE
C                         BOHEH KAOO AA POPAMM B BXOHO
C                         HAOP AHHX BBOTC COEPMOE  POH
C                         CC    OPTA BBOA-BBOA C APECOM ADRPORT;
C----------------------------------------------------------------------
C
C   METO:         PAMETKA OE RMSW O MOE
C   -----
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW P FGMSW = 0;
C                  LM - HA BCEO MSW OCAH      P FGMSW = 1;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------
C
C   PMEAHE:    1. KOHTPO B MHMAHOM O"EME;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HE PBOTC OPMAT RMSW:
C
C
C                   OPMAT RMSW T # 380:
C                  -----------------------
C+-----------I--------------I------I-----------------------------------
C  # COBA   I    # COBA B I KTO  I
C OT HAAA  I    OTPEKE   IOPE.I
C BCEO MSW  I    RMSW B    I      I       OOHAEHE - HAEHE
C OCAH   I    FM0032    I      I
C------------I--------------I------I-----------------------------------
C     9      I     0        I      I HOMEP A ( BCEA 0 )
C----------------------------------------------------------------------
C    10      I     1        I  DM  I TX - BPEM BOMOHOO EPEKEH;
C    11      I     2        I  DM  I KX - KAATE MEHEH COCTOH
C            I              I      I      OPTA  B/B HA AE ;
C    12      I     3        I  FM  I TTAKT - EPO TAKTOBO ACTOT ;
C    13      I     4        I  FM  I P - A OTAOHO EAT:
C            I              I      I     P=0 - OTAOHA EAT OTK.
C            I              I      I     P=1 - OTAOHA EAT BK.
C    14      I     5        I  FM  I           PEEPB
C    15      I     6        I  FM  I           PEEPB
C    16      I     7        I  DM  I KX - KAATE MEHEH COCTOH
C            I              I      I      OPTA B/B HA AE ;
C    17      I     8        I  DM  I TT - TEKEE BPEM ;
C    18      I     9        I  DM  I R0 - COEPMOE POH R0 P T=TT ;
C    19      I    10        I  DM  I R1 - COEPMOE POH R1 P T=TT ;
C    20      I    11        I  DM  I R2 - COEPMOE POH R2 P T=TT ;
C    21      I    12        I  DM  I R3 - COEPMOE POH R3 P T=TT ;
C    22      I    13        I  DM  I R4 - COEPMOE POH R4 P T=TT ;
C    23      I    14        I  DM  I R5 - COEPMOE POH R5 P T=TT ;
C    24      I    15        I  DM  I R6 - COEPMOE POH R6 P T=TT ;
C    25      I    16        I  DM  I R7 - COEPMOE POH R7 P T=TT ;
C    26      I    17        I  DM  I FLC-HAEHE AA "C" CC P T=TT
C    27      I    18        I  DM  I FLV-HAEHE AA "V" CC P T=TT
C    28      I    19        I  DM  I FLZ-HAEHE AA "Z" CC P T=TT
C    29      I    20        I  DM  I FLN-HAEHE AA "N" CC P T=TT
C    30      I    21        I  DM  I FLT-HAEHE AA "T" CC P T=TT
C    31      I    22        I  DM  I FLIO-HAEHE AA "I/O" CC
C            I              I      I             P T=TT ;
C    32      I    23        I  DM  I IPORT  -COEPMOE OPTA BBOA
C    33      I    24        I  DM  I IPORT1 -COEPMOE OPTA BBOA
C            I              I      I             P T=TT ;
C    34      I    25        I  FM  I ADRPROG-MAKCMAH APEC O ;
C    35      I    26        I  FM  I ADRPRTO-APEC OPTA BBOA;
C    36      I    27        I  FM  I ADRPRTI-APEC OPTA BBOA ;
C    37      I    28        I  DM  I TO - CTAPOE BPEM ;
C    38      I    29        I  DM  I R0 - COEPMOE POH R0 P T=TO ;
C    39      I    30        I  DM  I R1 - COEPMOE POH R1 P T=TO ;
C    40      I    31        I  DM  I R2 - COEPMOE POH R2 P T=TO ;
C    41      I    32        I  DM  I R3 - COEPMOE POH R3 P T=TO ;
C    42      I    33        I  DM  I R4 - COEPMOE POH R4 P T=TO ;
C    43      I    34        I  DM  I R5 - COEPMOE POH R5 P T=TO ;
C    44      I    35        I  DM  I R6 - COEPMOE POH R6 P T=TO ;
C    45      I    36        I  DM  I R7 - COEPMOE POH R7 P T=TO ;
C    46      I    37        I  DM  I FLC-HAEHE AA "C" CC P T=TO
C    47      I    38        I  DM  I FLV-HAEHE AA "V" CC P T=TO
C    48      I    39        I  DM  I FLZ-HAEHE AA "Z" CC P T=TO
C    49      I    40        I  DM  I FLN-HAEHE AA "N" CC P T=TO
C    50      I    41        I  DM  I FLT-HAEHE AA "T" CC P T=TO
C    51      I    42        I  DM  I FLIO-HAEHE AA "I/O" CC
C            I              I      I             P T=TO ;
C    52      I    43        I  DM  I IPORT  -COEPMOE OPTA BBOA
C    53      I    44        I  DM  I IPORT1 -COEPMOE OPTA BBOA
C            I              I      I             P T=TO ;
C    54      I    45        I  FM  I  # - HOMEP EMEHTA;
C    55      I    46        I  FM  I  #CM - HOMEP TOKA EMEHTA # ;
C    56      I    47        I  LM  I  ATOKM - AP B TOKN/N1 HAEH
C            I              I      I           TOKA HOMEPA #CM ;
C----------------------------------------------------------------------
C  OA HA MSW --> LM<BC> = 56
C
C  HA EPEMEH. ACT --> LM<RMSW> = 47
C
C   BHMAHE OABK*** :HAAH APEC CTEKA B MEM-MACCBE 2046
C   ----------
C               OBAB LAN  11/06/89.
C
CZZZ-------------------------------------------------------------------
       COMMON /PROF1/ MASPRT(100)
       COMMON /MEMER/ MEM(2048), MEM1(2048)
       DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
       EQUIVALENCE( NWTR, MASPRT(13) )
       EQUIVALENCE( NWTRE, MASPRT(14) )
       EQUIVALENCE( FGMSW, MASPRT(87) )
       EQUIVALENCE( H0   , MASPRT(30) )
       EQUIVALENCE ( RN  ,   N  )
       INTEGER  TIP, BA0T, FGMSW, ADPRTI,ADPRTO, ADPROG
       NEL = BA0T(1)
       TIP = BA0T(2)
       IF (  TIP .EQ. 380 ) GO TO 1
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 501 ) NEL, TIP
501    FORMAT(/' *** O. CO B FM0032 --> HOMEP TA HE HA ***'/
     =  ' *** HOMEP EMEHTA ', I5, ' HOMEP TA ', I5, ' ***'/)
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
1      CONTINUE
C----------------------------------------------------------------------
C --->       MKPOPOECCOPHA CCTEMA HA AE MKPOPOECCOPA
C --->       CEP K1801BM1(2,3)
C----------------------------------------------------------------------
         IF ( FGMSW.EQ. 0 ) GOTO 11
C--> TOHT LM <O>
         LM = 56
         RETURN
C----------------------------------------------------------------------
 11      CONTINUE
C-->  PHT  TO MOHO COPMPOBAT..
       LM = 47
       DO 10 I=1,LM
10     RMSW(I) = 0.0
C----------------------------------------------------------------------
C---> BBEEM AHHE
           N = IFIX ( PAR0T(1) )
        NCM  =  IFIX ( PAR0T(2) )
       FTAKT = PAR0T(3)
       ADPRTI = IFIX ( PAR0T(4) )
       ADPRTO = IFIX ( PAR0T(5) )
       ADPROG = IFIX ( PAR0T(6) )
       P =  PAR0T(7)
C---> POBEPM  AOMHM AHHE.
C
C--> POBEPM HOMEP # ..
       IF ( N .GE. NEL ) GO TO 9999
       RMSW( 45) = RN
       N = NCM
       RMSW(46) = RN
C---> POBEPM ACTOT.
       IF( FTAKT .GT. 0.0 ) GO TO 15
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 551 ) NEL, TIP
551    FORMAT(/' *** OKA B FM0032 --> ACTOTA HE OOTEHA  ***'/
     =         ' *** EMEHT  ',   I5,'      HOMEP TA ',   I5,' ***')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C----------------------------------------------------------------------
15     CONTINUE
       RMSW(3) = 1./FTAKT
C--> POBEPM APEC OPTA BBOA
C--> POBEPM PABHOCT AAH KAOO PAPA APECA
CCCC   WRITE (6,*) ADPRTI
       CALL ADECQD(ADPRTI, IADD, I0, I1, I2, I3, I4, I5, IER )
       IF( IER .NE. 0 ) GO TO 20
       IF( I0  .LT. 0 .OR. I0 .GT. 7 ) GO TO 20
       IF( I1  .LT. 0 .OR. I1 .GT. 7 ) GO TO 20
       IF( I2  .LT. 0 .OR. I2 .GT. 7 ) GO TO 20
       IF( I3  .LT. 0 .OR. I3 .GT. 7 ) GO TO 20
       IF( I4  .LT. 0 .OR. I4 .GT. 7 ) GO TO 20
       IF( I5  .LT. 0 .OR. I5 .GT. 7 ) GO TO 20
       IF( ADPRTI .LT. 160000 .OR. ADPRTI .GT. 177777 ) GO TO 20
C--> AOMHM..
       N = IADD
       RMSW(27) = RN
       GO TO 22
20     CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 552 ) NEL, TIP
552    FORMAT(/' *** OKA B FM0032 --> HEPABHO AAH APEC  ***'/
     =         ' *** OPTA BBOA                           ***'/
     =         ' *** EMEHT  ',   I5,'      HOMEP TA ',   I5,' ***')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C----------------------------------------------------------------------
22     CONTINUE
C--> POBEPM APEC OPTA BBOA
C--> POBEPM PABHOCT AAH KAOO PAPA APECA
       CALL ADECQD(ADPRTO, IADD, I0, I1, I2, I3, I4, I5, IER )
       IF( IER .NE. 0 ) GO TO 21
       IF( I0  .LT. 0 .OR. I0 .GT. 7 ) GO TO 21
       IF( I1  .LT. 0 .OR. I1 .GT. 7 ) GO TO 21
       IF( I2  .LT. 0 .OR. I2 .GT. 7 ) GO TO 21
       IF( I3  .LT. 0 .OR. I3 .GT. 7 ) GO TO 21
       IF( I4  .LT. 0 .OR. I4 .GT. 7 ) GO TO 21
       IF( I5  .LT. 0 .OR. I5 .GT. 7 ) GO TO 21
       IF( ADPRTO .LT. 160000 .OR. ADPRTO .GT. 177777 ) GO TO 21
C--> AOMHM..
       N = IADD
       RMSW(26) = RN
       GO TO 25
21     CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 553 ) NEL, TIP
553    FORMAT(/' *** OKA B FM0032 --> HEPABHO AAH APEC  ***'/
     =         ' *** OPTA BBOA                           ***'/
     =         ' *** EMEHT  ',   I5,'      HOMEP TA ',   I5,' ***')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C----------------------------------------------------------------------
 25    CONTINUE
C--> POBEPM MAKCMAH APEC O
C--> POBEPM PABHOCT AAH KAOO PAPA APECA
       CALL ADECQD(ADPROG, IADPRG, I0, I1, I2, I3, I4, I5, IER )
       IF( IER .NE. 0 ) GO TO 30
       IF( I0  .LT. 0 .OR. I0 .GT. 7 ) GO TO 30
       IF( I1  .LT. 0 .OR. I1 .GT. 7 ) GO TO 30
       IF( I2  .LT. 0 .OR. I2 .GT. 7 ) GO TO 30
       IF( I3  .LT. 0 .OR. I3 .GT. 7 ) GO TO 30
       IF( I4  .LT. 0 .OR. I4 .GT. 7 ) GO TO 30
       IF( I5  .LT. 0 .OR. I5 .GT. 7 ) GO TO 30
       IF( ADPROG .LT. 0 .OR. ADPROG .GT. 3777 ) GO TO 30
C--> AOMHM..
       N = IADPRG
       RMSW(25) = RN
C----------------------------------------------------------------------
C---> CTAHABBAEM HAA. HAEHE KAATE CTEKA
       RMSW(15) = RN
       GO TO 35
30     CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 5522) NEL, TIP
5522   FORMAT(/' *** OKA B FM0032 --> HEPABHO AAH        ***'/
     =         ' *** MAKCMAH APEC O                       ***'/
     =         ' *** EMEHT  ',   I5,'      HOMEP TA ',   I5,' ***')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C----------------------------------------------------------------------
35     CONTINUE
C--> POOM..
       N = IFIX(P)
       RMSW(4) = RN
C======================================================================
C--> TEEP BBEEM B O MKPOPOECCOPHO CCTEM PAB
C--> POPAMM B BOCMPHOM KOE, A ATEM PEOPAEM EE B
C--> ECTH OPM.
C======================================================================
C--> OHM
       DO 80 I=1,2048
       MEM1(I) = 0
80     MEM(I)  = 0
C--> NPROG - KAHA BBOA POPAMM.
       NPROG = 22
C--> BBOM
C***   READ(NPROG,600,END=100) (MEM1(I),I=1,2048)
       DO 400 I=1,2048
       READ(NPROG,600,END=100) MEM1(I)
600    FORMAT(8X,I6)
C      IF(MEM1(I).EQ.0)  I1=I1+1
C      IF(I1.GT.0) GOTO 100
400    CONTINUE
       I = 2049
100    CONTINUE
       WRITE (6,600) I
       WRITE (6,600) (MEM1(K),K=1,I)
       IK = I - 1
       IF( (2*IK-1) .GT. IADPRG ) GO TO 200
C--> EPEBOM B ECTH OPM
       DO 150 I=1,IK
       KQ = MEM1(I)
       CALL ADECQD(KQ, KD, KQ0, KQ1, KQ2, KQ3, KQ4, KQ5, IER)
       IF( IER. NE. 0 ) GO TO 210
       IF( KQ0  .LT. 0 .OR. KQ0 .GT. 7 ) GO TO 210
       IF( KQ1  .LT. 0 .OR. KQ1 .GT. 7 ) GO TO 210
       IF( KQ2  .LT. 0 .OR. KQ2 .GT. 7 ) GO TO 210
       IF( KQ3  .LT. 0 .OR. KQ3 .GT. 7 ) GO TO 210
       IF( KQ4  .LT. 0 .OR. KQ4 .GT. 7 ) GO TO 210
       IF( KQ5  .LT. 0 .OR. KQ5 .GT. 7 ) GO TO 210
C--> AOMHM BCE COBO
       MEM(2*I-1) = KD
C--> AOMHM CTAP AT
       MEM(2*I) = KD/256
150    CONTINUE
CCCC   WRITE (6,*) RMSW(15),RMSW(3)
       RETURN
200    CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 554 ) NEL, TIP
554    FORMAT(/' *** OKA B FM0032 --> PABA POPAMMA    ***'/
     =         ' *** AHMAET OACT AMT OE AAHHO      ***'/
     =         ' *** EMEHT  ',   I5,'      HOMEP TA ',   I5,' ***')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C----------------------------------------------------------------------
       RETURN
210    CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 555 ) I, NEL, TIP
555    FORMAT(/' *** OKA B FM0032 --> OOHA KOMAHA B      ***'/
     =         ' *** POPAMME  MKPOPOECCOPA B CTPOKE',I4,' ***'/
     =         ' *** EMEHT  ',   I5,'      HOMEP TA ',   I5,' ***')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C----------------------------------------------------------------------
CCCC   WRITE (6,*) RMSW,MSW
       RETURN
C######################################################################
9999   CONTINUE
C!!!-------------------------------------------------------------------
       WRITE(NWTRE, 556 ) NEL, TIP
556    FORMAT(/' *** OKA B FM0032 -->HEPABHO AAH BXOHE AHH
     =E   ***'/' *** HA BXO EMEHTA ', I5, ' HOMEP TA ', I5,' ***',
     =         ' *** OAETC TOK EMEHTA, MEEO O OPKOB
     =HOMEP   *** ')
C!!!-------------------------------------------------------------------
       CALL SUBERR(TIP)
C######################################################################
       RETURN
CCCCC  DEBUG INIT
       END

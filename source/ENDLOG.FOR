         SUBROUTINE ENDLOG( LOGYTR )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:   ENDLOG( LOGYTR )
C   ------------
C   HAHAEHE:    HAXOHEHE  PAMETKA "AOBX" OB BCEX
C   ----------     HEHTHX P. HA MECTO "AOBX" ACBAETC
C                  PHAK "-1" -  OCHOBHO E. HEBE  HE
C                  MEHTC;
C   METO:         CKAHPOBAHE LOGYTR, P LOGYTR(I) = I -> "AOB";
C   -----
C   OPOPAMM:  1. COMMON PROF1
C   ------------
C   PMEAHE:    1. P FTEST >= 2 EAT LOGYTR HA NWTRE
C   ----------        OCE POBEEH MOKA;
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION LOGYTR(1)
         EQUIVALENCE ( KNOT6M, MASPRT(58) )
         EQUIVALENCE ( NSYS,   MASPRT(26) )
         EQUIVALENCE ( FTEST,  MASPRT(60) )
         EQUIVALENCE ( NWTRE,  MASPRT(14) )
         INTEGER FTEST
         DO 100 I = KNOT6M, NSYS
         IF ( LOGYTR(I) .LE. 0 ) GO TO 100
C---> I - B E HEHTHX
C    MOET TO "AOB" ?
         IF ( LOGYTR(I) .EQ. I ) LOGYTR(I) = -1
100      CONTINUE
         IF ( FTEST .LT. 2 ) RETURN
C!!!-------------------------------------------------------------------
         WRITE(NWTRE, 2 )
2        FORMAT(' ***FTEST_2: MACCB K.. OB C OTMEEHHM "AOBM"
     = AM ***')
         WRITE(NWTRE, 1 ) ( I, LOGYTR(I), I=1, NSYS)
1        FORMAT(           5( 2X,   'LOG(', I4, ')=', I4 ) )
C!!!-------------------------------------------------------------------
         RETURN
         END

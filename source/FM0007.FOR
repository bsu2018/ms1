         SUBROUTINE FM0007( BA0T, KNOTT, PAR0T, RMSW, LM )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA:  FM0007( BA0T, KNOTT, PAR0T, RMSW, LM )
C   ------------
C
C   HAHAEHE:    HACTPOKA MAKPOMOEE:
C  ------------
C                  T     M MOE     OOHAEH B AHEEM
C
C                  71      "HEH. COPOTBEHE"     RN
C
C                  72      "HEH. HKTBHOCT"     LN
C
C                  720     "HEH. HKTBHOCT" LN AAHHA EPE
C                                                 L_ . OT I_TOKA;
C
C       HEHEHOCT EMEHTA AAETC TAHO, EPE MAS <...>:
C
C         "RN" ==>  BOT-AMEPHA X-KA:
C
C             MAS < I1 U1  I2 U2  ... ...   I_N U_N >;
C
C         "LN" ==> BEEP-AMEPHA X-KA: ( T #72 ):
C
C             MAS < I1 1  I2 2  ... ...   I_N _N >;
C
C         "LN" ==> EPE L_. OT TOKA:
C
C             MAS < I1 L_1 I2 L_2 .....           >;
C
C                  POBOT OPMPOBAHE "EPEMEHHO" ACT MSW
C                  MAKPOMOE.
C
C   METO:         PAMETKA OE RMSW O MOE, CAH PAOEHE
C   -----          TAHO AAHHX HEHEHX ABCMOCTE.
C
C   APAMETP:     BA0T, KNOTT, PAR0T - "OTPEK" HOPMAOHHX
C  ----------      MACCOBOB BA0, KNOT, PAR0 ( CM. INPUT0  GETMSW );
C                  RMSW - "EPEMEHHA" ACT MSW ( "OTPEOK" MSWALL );
C                  LM - HA "EPEMEHHO" ACT RMSW P FGMSW = 0;
C                  LM - HA BCEO MSW OCAH      P FGMSW = 1;
C
C   OPOPAMM:  1.COMMON PROF1
C   ------------   2. SPLIN0
C
C   PMEAHE:    1. KOHTPO B MHMAHOM O"EME;
C   -----------    2. BCE HE OPEEEME O RMSW AHTC;
C                  3. HEOXOMO KOPOBAT HE MEHEE BX TOEK BAX.
C                     AAHHA Y=F(X) HE OHA MET POBOH
C                     PABH H --> HAE CO P PACETE.......!!!!!
C
C                  4. HE PBOTC OPMAT RMSW:
C
C                  RMSW TA # 71, 72  720 [ HEHEHE R  L ]:
C                  ----------------------------------------------
C                                                        LM=<EPEMEHHA>
C                                                          =+1<VAR>
C                                                  (OT KO-AB TOEK BAX)
C                    SPM
C +-----+----+-----------------------+
C I  G  I  J I  XY_MAS               I
C I     I    I MACCB TOEK BAX.     I    MACB "B", "C", "D" -->
C I     I    I N,(X,Y).."B","C","D"  I    CAH PAOEHE BAX:
C +-----+----+-----------------------+    CM. / SPLIN0;
C I  1  I 2  I 3 ... 3+(5*N+1)       I
C +-----+----+-----------------------+
C I  11 I 12 I 13 ...12+(5*N+1)      I
C +-----+----+-----------------------+
C
C   N -> KOECTBO TOEK BAX.
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         DIMENSION BA0T(*), KNOTT(*), PAR0T(*), RMSW(*)
         EQUIVALENCE( FGMSW, MASPRT(87) )
         EQUIVALENCE( N,       RN       )
         INTEGER BA0T, TIP, FGMSW
         TIP = BA0T(2)
         NEL = BA0T(1)
         NWTR = MASPRT(13)
         IF ( TIP .EQ. 71  .OR.  TIP .EQ. 72  .OR. TIP.EQ.720) GO TO 1
C!!!-------------------------------------------------------------------
         NWTRE = MASPRT(14)
         WRITE(NWTRE, 501) TIP, NEL
501      FORMAT(' *** O. CO B FM0007: HOMEP TA HE OCBAETC **
     =*'/' *** HOMEP TA ', I10, ' EMEHT HOMEP: ', I10, '***')
C!!!-------------------------------------------------------------------
         CALL SUBERR(1)
C######################################################################
C######################################################################
1        CONTINUE
C---> TO HAO?
       IF ( FGMSW .EQ. 0 ) GO TO 1000
C--> APOC "TOHT H MSW"
C      LM:=8 + 2<HOMEPA OB> + 2<G-J APAMETP> + 5*N+1<MACCB XYMAS>
C          N := KO-BO TOEK BAX (X,Y)
C
           NMAS = IFIX( PAR0T(1) )
           N  = NMAS/2
           LM = 8 + 2 +2 + 5*N + 1
C----------------------------------------------------------------------
         RETURN
1000     CONTINUE
C---> OPMATPOBAT RMSW <OTPEOK>;
C-->  CKOKO CE...
       NMAS=IFIX( PAR0T(1) )
C--> CKOKO TOEK..
       N = NMAS/2
C-->  OCTMO,  TO ?
       IF ( N .GE. 2 ) GO TO 1001
C-->  PAOTAT HE...
       NWTR = MASPRT(13)
       WRITE(NWTR,500) NEL
500    FORMAT(/' ***   EMEHTA # ', I5, ' MAO TOEK  B.A.X.',
     = ' ***'/ ' *** PACET PEPBAETC ***'//)
       call STOPP(28)
1001   CONTINUE
       LM = 2 + 5*N + 1
       DO 1002 I=1,LM
1002   RMSW(I) = 0.0
C-->  PHT MACCB...
       RMSW(3) = RN
C       MSW(3) = N
C
       DO 1003 I=1, NMAS
       RMSW(3+I) = PAR0T(1+I)
1003   CONTINUE
C--->  HAEM CAH PAOEHE....
       CALL SPLIN0( RMSW(3) )
C++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
       RETURN
       END

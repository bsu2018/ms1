       SUBROUTINE ADECIS( KD, ADPROG, REG, IADRS, IDATA, T, IER )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT    MA    1988 .)-----------
C   OPOPAMMA: ADECIS( KD, ADPROG, REG, IADRS, IDATA, T, IER )
C   -------------
C   HAHAEHE:  OCK O KO APECA  KD ( BXPAPHOE Q-CO )
C   ----------   APECA  AHHOO B O   B  POH .
C                (  KOMAH, PAOTAX CO COBAM )
C                ******************************************************
C
C   METO:         EPEKOPOBKA, OPEEEHE METOA APECA  OCK.
C   -----
C
C   APAMETP:  BXOHE :
C  ----------      KD     - KO APECA ;
C                  REG(8) - MACCB PECTPOB MKPOPOECCOPA ;
C
C               BXOHE :
C                  IADRS  - APEC  CKOMOO AHHOO :
C                           EC IADRS >= 0 - APEC EK AMT;
C                           EC IADRS <  0 - APEC PECTPA .
C                                             O APEC OPTA B/B .
C                  IDATA  - CO, XPAHEEC O APEC 'IADRS';
C                  T      - BPEM BOHEH KOMAH ;
C                  IER    - KAATE OK:
C                           IER=0  -  OOK HE OHAPEHO ;
C                           IER=1  -  OOH KO APECA ,
C                                     O BXO A PEE O ;
C                           IER=2  -  OEHH APEC EK AMT
C                                     'IADRS' HE ETH .
C
C   OPOPAMM:  1. HET.
C   ------------
C
C   PMEAHE:    1. HET.
C   -----------
C
CZZZ-------------------------------------------------------------------
       COMMON /MEMER/ MEM(2048), MEM1(2048)
       INTEGER  REG(8), ADPROG
       IER = 0
C--> KAKO METO APECA ?
       METOD = KD/8 + 1
       NREG  = KD - 8*(METOD - 1) + 1
       GO TO (100, 200, 300, 400, 500, 600, 700, 800 ), METOD
       GO TO 9990
C######################################################################
C
C               PMA  APECA
C
C######################################################################
100    CONTINUE
C--> PECTPOB METO APECA.
C----------------------------------------------------------------------
       IADRS = - NREG
       IDATA = REG(NREG)
       T = 0.
       RETURN
C----------------------------------------------------------------------
300    CONTINUE
C--> ABTOHKPEMEHTH METO APECA
C----------------------------------------------------------------------
       IADRS = REG(NREG)
       T = 1.6E-6
       REG(NREG) = REG(NREG) + 2
       GO TO 900
C----------------------------------------------------------------------
500    CONTINUE
C--> ABTOEKPEMEHTH METO APECA
C----------------------------------------------------------------------
       REG(NREG) = REG(NREG) - 2
       IADRS = REG(NREG)
       T = 2.4E-6
       GO TO 900
C----------------------------------------------------------------------
700    CONTINUE
C--> HEKCH METO APECA
C----------------------------------------------------------------------
       INDEX = MEM( REG(8) + 1)
       REG(8) = REG(8) + 2
       IADRS = REG(NREG) + INDEX
       T = 4.8E-6
       GO TO 900
C######################################################################
C
C            KOCBEHHA  APECA
C
C######################################################################
200    CONTINUE
C--> KOCBEHHO-PECTPOB METO APECA
C----------------------------------------------------------------------
       IADRS = REG(NREG)
       T = 1.6E-6
       GO TO 900
C----------------------------------------------------------------------
400    CONTINUE
C--> KOCBEHHO-ABTOHKPEMEHTH METO APECA
C----------------------------------------------------------------------
       IADRS = MEM( REG(NREG)+1)
       T = 4.0E-6
       REG(NREG) = REG(NREG) + 2
       GO TO 900
C----------------------------------------------------------------------
600    CONTINUE
C--> KOCBEHHO-ABTOEKPEMEHTH METO APECA
C----------------------------------------------------------------------
       REG(NREG) = REG(NREG) - 2
       IADRS = MEM( REG(NREG)+1)
       T = 4.8E-6
       GO TO 900
C----------------------------------------------------------------------
800    CONTINUE
C--> KOCBEHHO-HEKCH METO APECA
C----------------------------------------------------------------------
       INDEX = MEM( REG(8) + 1)
       REG(8) = REG(8) + 2
       IAD   =  REG(NREG)  + INDEX
       IADRS = MEM(IAD+1)
       T = 7.2E-6
C----------------------------------------------------------------------
900    CONTINUE
C--> POBEPM APEC  AOMHM AHHOE.
       IF( IADRS .GE. 57344  .AND. IADRS .LT. 65536 ) GO TO 8888
       IF( IADRS .LT. 0      .OR.  IADRS .GT. ADPROG) GO TO 9990
       IDATA = MEM(IADRS+1)
       IF( MOD(IADRS,2) .GT. 0 ) GO TO 9999
       RETURN
C----------------------------------------------------------------------
8888   CONTINUE
C--> OPATC K OPT B/B.
       IADRS = - IADRS
       RETURN
C----------------------------------------------------------------------
9990   CONTINUE
       IER = 1
       RETURN
C----------------------------------------------------------------------
9999   CONTINUE
C--> CEAHO  KOMAH , PAOTAX C ATAM
       IDATA = MEM(IADRS)
       IADRS = IADRS - 1
       IER = 2
       RETURN
C----------------------------------------------------------------------
       END

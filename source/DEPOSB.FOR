       SUBROUTINE DEPOSB(NALL, KK, GK, JK, YALL, JALL )
CXXX----KOMEKC "MS1" --> BEPC 1.0  (OT EKAP 1986 .)------------
C   OPOPAMMA: DEPOSB(NALL, KK, GK, JK, YALL, JALL )
C   ------------
C   HAHAEHE:   OCECTBEHE "AKTA" OEMEHTHOO BKAA G-J
C   ----------    APAMETPOB  "OHX" MATP POBOHOO MHOOO.
C                 B  MATP  YALL  JALL OE CCTEM PABHEH;
C
C                      GK - "OHA" MATPA OBX POBOMOCTE,
C                      JK - ---> CTOHKOA TOKA;
C                 NALL - MACCB CCKA HOMEPOB OB OKEH,
C                        ( KK - TK );
C
C                 [I] = [GK] * [V]  + [JK]  <---- "MOE" MHOOO.
C
C              E [I] - BEKTOP-CTOE (PAMEPHOCT KK) - TOK
C                        "BTEKAE"  BO BCE BETB  B MHOOOCHKA;
C                  [V] - BEKTOP CTOE ( KK ) - OBX HAPEH
C                        HA AX OKEH  MHOOOCHKA;
C                      - HOMEPA OB OKEH COEPATC B MACCBE
C
C                        NALL  -  HOMEPA OB OKEH;
C
C         CXEMA "BKAA":           #NALL#
C         *************                        T
C                               I               I
C                               I .[ + GK ]     I #NALL#
C       [ YALL ] = [ YALL ] +   I               I
C                               I               I
C                               L               J
C                                             .
C                                           T
C                                I           I
C       [ JALL ] = [ JALL ]  -   I [ + JK ]  I
C                            #   I           I
C                            $   L           J
C  !!! BHMAHE $$$$$$$$$$$$$$   !!!!!#MHC#!!!!!!!!!!!!!!!!!!!!!!!!!!
C
C   METO:     CM. BE;
C   -----
C   APAMETP: CM. BE;
C   ---------
C   OPOPAMM:  PROF1;
C   ------------
C
C   PMEAHE:  1. B V.M= 2.* - HE AKOBAHHOE XPAHEHE;
C   ----------   2. P NALL(I) = 0 ->
C                   "BKA" HE EAETC;
C                3. YALL(I,J)   --> EPE IDY(I,J)=NDALL*(J-1)+I
C                   GK    (I,J) --> EPE IDG(I,J)=KK  *(J-1)+I
C
C
CZZZ-------------------------------------------------------------------
       COMMON/PROF1/ MASPRT(100)
       EQUIVALENCE( NDALL, MASPRT(23) )
       DIMENSION NALL(1), GK(1), JK(1)
       DIMENSION YALL(1), JALL(1)
       REAL JK,     JALL
C--
       IDY(I,J) = NDALL * (J-1) + I
       IDG(I,J) = KK    * (J-1) + I
C--->  BKA  CTOHKOB...
       DO 1 I=1, KK
C--> PEAHE HOMEPA B CCTEME *_ALL
       K = NALL(I)
C          ********
       IF ( K .NE. 0 ) JALL(K) = JALL(K) - JK(I)
1      CONTINUE
C######################################################################
C--->  BKA B YALL
       DO 2 I = 1, KK
         K = NALL(I)
C            ********
         IF ( K .EQ. 0 ) GO TO 2
           DO 3 J = 1, KK
             M = NALL(J)
C                ********
C            IF ( M .EQ. 0 ) GO TO 3
C--->        BKABAEM...
             IJ = IDG(I,J)
             KM = IDY(K,M)
             YALL( KM ) = YALL( KM ) + GK( IJ )
3          CONTINUE
2      CONTINUE
       RETURN
       END

         SUBROUTINE DM0028 ( MSW, RMSW, YALL, JALL, LOGYTR,
     *                       TOKN, TOKN1, VALLN, VALLN1, KOD,
     *                         MSWALL, RMSWAL, ADRMSW )
CXXX----KOMEKC "MS1" --> BEPC 1.1  (OT EKAP 1987 .)------------
C   OPOPAMMA:  DM0028 ( MSW, RMSW, YALL, JALL, LOGYTR,
C   ------------             TOKN, TOKN1, VALLN, VALLN1, KOD,
C    *                         MSWALL, RMSWAL, ADRMSW )
C
C   HAHAEHE:    B ABCMOCT OT HAEH AA ZAPROS [MASPRT(28)]:
C   -----------    BOHET CEE HK:
C
C                  A) ZAPROS = 1
C                     ----------
C                  OPMPOBAHE BKAOB B YALL MATP POBOMOCTE 
C                  JALL - BEKTOP CTOHKOB TOKA HAEH APAMETPOB
C                  KBBAEHTHX  G - J BETBE  MOE EMEHTOB E:
C                +-----+--------------------------------------------+
C                I T I              M MOE                    I
C                I-----I--------------------------------------------I
C                I 15  I   PABEM HAPEHEM CTOHK TOKA    I
C                I-----I--------------------------------------------I
C                I 16  I   PABEM HAPEHEM CTOHK HAP-  I
C                I     I   EH                                    I
C                I-----I--------------------------------------------I
C                I 200 I   EAH OEPAOHH CTE         I
C                I-----I--------------------------------------------I
C                  ***************************************************
C                  B) ZAPROS = 2
C                     ----------
C                  BCEHE HAEH TOKOB B BETBX B T(N+1) MOMEHT
C                  BPEMEH   CXO  G  J APAMETPOB  HAEH
C                  OBX HAPEH HA EMEHTE PACTAHHX  T(N+1)
C                     AC (KCA) TX HAEH B "TOK T(N+1)";
C
C                  C) ZAPROS = 3
C                     **********
C                     HE OEPBAETC ( PEEPB );
C
C                  D) ZAPROS = 4
C                     **********
C                     HE OEPBAETC;
C
C   METO:         CKPETA PABHEH COCTOH MOE AHHOO
C   -----          TA HA OCHOBE METOA HEPA BO:
C                  Y(N+1) = Y(N) + H1*F(N+1) + H2*F(N).
C
C   APAMETP:     MSW, RMSW - PABEE COBO COCTOH MAKPOMOE
C   ---------      ( "OTPEOK" MACCB MSWALL -> C.M. / GETMSW );
C                  YALL - MATPA P0BOMOCT AHAPEMO E;
C                  JALL - BEKTOP CTOHKOB TOKA AHAPEMO E;
C                  VALLN - BEKTOP OBX HAPEH AHAPEMO E:
C                          P  T  =  TN;
C                  VALLN1- BEKTOP OBX HAPEH AHAPEMO E:
C                           P  T  =  TN  + H0;
C                  TOKN    TOKN1 - BEKTOPA EPEMEHHX "COCTOH"
C                                   EMEHTA P  TN    TN + H0;
C                                   PAMEPHOCT CM. MASPRT(46)=NNTOK;
C                  LOGYTR - BEKTOP PHAKOB K.. OB:
C                  COETC B MAKPOMOEX C PHAKOM TA = 5;
C                  PAMEPHOC MACCBOB P "ALL" OPEETC:
C                   MAX_DIMENSION ---> NDALL = MASPRT(23)
C                   TEKA_PAMEPHOCT ---> NSYS = MASPRT(26)
C                  KOD - APAMETP OPATHO CB C MOHTOPOM DM....
C                   ( COETC  MAKPOMOEE "EAHX" KE)
C
C
C
C   OPOPAMM:  1. COMMON PROF1, PROF2, PROF3;
C   ------------             DEPOST;
C
C   PMEAHE:  1.CTPKTP MSW COBA MAKPOMOE AHHOO TA CM. HE
C   ----------   2. P ZAPROS ^=   1   2 - COOEHE  XO;
C
C
C               "OCTOHHA" ACT MSW TA # 15,16,200
C                --------------------------------------
C
C  +-------+--------+--------+--------+------+------+-------+---+
C  I HOMEP I HOMEP  I HOMEP  I  P. I KO I KO. IAP.   IPE I
C  I EME I TA   I /    I  TA  I ECT I BB. IHA.  IE I
C  I HTA   I MOE I PACETAI  MOEI BO   I HA I"TOKN" IPB I
C  I       I        I DM.....I        I OBI EH.I"TOKN1"I   I
C  +-------+--------+--------+--------+------+------+-------+---+
C  I  1    I   2    I   3    I    4   I   5  I  6   I   7   I 8 I
C  +-------+--------+--------+--------+------+------+-----------+
C
C
C
C                  RMSW T # 15 [ TH ]
C                  ----------------------
C
C             ++--------+---------+---------++
C             II        I         I         II
C             II   1   I   2    I    G    II
C             II        I         I         II
C             ++--------+---------+---------++       LM = 3
C             II   1    I    2    I     3   II       ******
C             ++--------+---------+---------++
C             II  11    I   12    I    13   II
C             ++--------+---------+---------++
C
C
C                  RMSW T # 16 [ HH ]
C                  ----------------------
C
C      ++--------+---------+---------+---------+----------+
C      II        I         I         I         I          I
C      II   1   I   2    I   GBH   I    K    I G=GBH*K  I
C      II        I         I         I         I          I
C      ++--------+---------+---------+---------+----------+    LM = 5
C      II   1    I    2    I     3   I    4    I    5     I    ******
C      ++--------+---------+---------+---------+----------+
C      II  11    I   12    I    13   I   14    I   15     I
C      ++--------+---------+---------+---------+----------+
C
C
C                  RMSW T # 200 [ EAH OEPAOHH CTE ]
C                  ----------------------
C
C             ++--------+---------+---------++
C             II        I         I         II
C             II   K    I   GBX   I  GBX   II
C             II        I         I         II
C             ++--------+---------+---------++       LM = 3
C             II   1    I    2    I     3   II       ******
C             ++--------+---------+---------++
C             II  13    I   14    I    15   II
C             ++--------+---------+---------++
C
C
C
CZZZ-------------------------------------------------------------------
         COMMON /PROF1/ MASPRT(200)
         COMMON /PROF2/ MACTAB(800)
         COMMON /PROF3/ FUNTAB(50)
         DIMENSION MSW(*), RMSW(*), YALL(*), JALL(*), LOGYTR(*)
         DIMENSION VALLN(*), RMASPT(1), VALLN1(*), TOKN(*), TOKN1(*)
         REAL JALL
         EQUIVALENCE ( ZAPROS, MASPRT(28) )
         EQUIVALENCE ( NDALL,  MASPRT(23) )
         EQUIVALENCE ( TN,     MASPRT(29) )
         EQUIVALENCE ( H0,     MASPRT(30) )
         EQUIVALENCE ( H1,     MASPRT(32) )
         EQUIVALENCE ( H2,     MASPRT(33) )
         EQUIVALENCE ( FKOM,   MASPRT(34) )
         EQUIVALENCE ( TXG,    MASPRT(35) )
         EQUIVALENCE ( NTXG,   MASPRT(36) )
         EQUIVALENCE ( EPSKOM, MASPRT(39) )
         DIMENSION MSWALL(*), RMSWAL(*)
         INTEGER ADRMSW
         EQUIVALENCE ( ZEROCP, MASPRT(50) )
         INTEGER FKOM, ZAPROS, TIP
         EQUIVALENCE ( MASPRT(1), RMASPT(1) )
C===>    BCTPOEHHA HK OCBAH BXMEPHX MACCBOB:
         ID(I,J) = NDALL * (J-1)  +  I
C--->  KOHTPO TA
         TIP = MSW(2)
         IF ( TIP .NE. 15 .AND. TIP .NE. 16 .AND. TIP.NE.200)GO TO 9999
C---> BOP PEMA:
         GO TO ( 667, 666                 ), ZAPROS
C===> HAE OKA
         NWTRE = MASPRT(14)
         WRITE(NWTRE,9997) MSW(3), ZAPROS
9997     FORMAT( ' *** DM MOHTOP #', I4, ' APOC OCBAH OOEH
     = ***'/ ' *** ZAPROS = ', I5, ' - HOPPETC ***')
         RETURN
667      CONTINUE
C---------ZAPROS = 1---------------------------------------------------
C---> TO A T?
         IF ( TIP .EQ. 16 ) GO TO 20
         IF ( TIP .EQ. 200) GO TO 30
C######################################################################
C---> PABEM HAPEHEM CTOHK TOKA
C--->   OEMEHTH BKA:
C==> CBO  .
         K = MSW(9)
         L = MSW(10)
C==>  PABEO HAPEH
         M = MSW(11)
         N = MSW(12)
C===> KOEHT CB
         G = RMSW(13)
C======================================================================
         KOD = 0
C---->
         CALL DEPOSN( K, L, M, N, G, YALL )
C---->
         RETURN
C######################################################################
20      CONTINUE
C---> PABEM HAPEHEM CTOHK HAPEH
C--->   OEMEHTH BKA:
C==> CBO  .
         K = MSW(9)
         L = MSW(10)
C==>  PABEO HAPEH
         M = MSW(11)
         N = MSW(12)
C===> BHTPEHHEE COPOTBEHE  KOEHT CB
         GBH = RMSW(13)
         G   = RMSW(15)
C======================================================================
C-->   BKABAEM...
         KOD = 0
C---->
         CALL DEPOSN( K, L, M, N, G, YALL )
C---->
         CALL DEPOST( K, L, GBH, 0.0, YALL, JALL )
C
         RETURN
C######################################################################
30      CONTINUE
C---> EAH OEPAOHH CTE
C--->   OEMEHTH BKA:
C==> BXOHE  .
         M = MSW(9)
         N = MSW(10)
C==> BXOHE  .
         K = MSW(11)
         L = MSW(12)
C===> KOEHT CEH, BXOHA  BXOHA POBOMOCT.
         AK  = RMSW(13)
         GBX = RMSW(14)
         GBIX= RMSW(15)
C======================================================================
         KOD = 0
C-->  BOM BXOH E
         CALL DEPOST( M, N, GBX, 0.0, YALL, JALL )
C---> BOM BXOH E
         G = AK * GBIX
         CALL DEPOSN( K, L, M, N, G, YALL )
C---->
         CALL DEPOST( K, L, GBIX, 0.0, YALL, JALL )
         RETURN
C======================================================================
666      CONTINUE
C$$$$$$$$$$$$$$$$$$$ZAPROS = 2$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
C---> TO A T?
         IF ( TIP .EQ. 16 ) GO TO 40
         IF ( TIP .EQ. 200) GO TO 50
C######################################################################
C---> PABEM HAPEHEM CTOHK TOKA
C==>  PABEO HAPEH
         M = MSW(11)
         N = MSW(12)
C--->  HOB TOK:
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN1(M)
         VN = 0.0
         IF ( N .NE. 0 ) VN = VALLN1(N)
         UN1 = VM - VN
         TOKT = RMSW(13) * UN1
C--->     AC TOKOB
         ITOK = MSW(7)
         TOKN1( ITOK ) = TOKT
         KOD = 0
         RETURN
C######################################################################
40       CONTINUE
C---> PABEM HAPEHEM CTOHK HAPEH
C==>  PABEO HAPEH
         M = MSW(11)
         N = MSW(12)
C--->CBO .
         K = MSW(9)
         L = MSW(10)
C--->  HOBOE PABEE HAPEHE
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN1(M)
         VN = 0.0
         IF ( N .NE. 0 ) VN = VALLN1(N)
         UBX = VM - VN
C--->  HOBOE BXOHOE HAPEHE
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN1(K)
         VL = 0.0
         IF ( L .NE. 0 ) VL = VALLN1(L)
         UBIX = VK - VL
C---> HOB TOK
         TOKT = ( RMSW(14) * UBX + UBIX ) * RMSW(13)
C---> AC TOKA
         ITOK = MSW(7)
         TOKN1( ITOK ) = TOKT
         KOD = 0
         RETURN
C######################################################################
50       CONTINUE
C---> EAH OEPAOHH CTE
C==>  BXOHOO HAPEH
         M = MSW(9)
         N = MSW(10)
C---> BXOHOO HAPEH
         K = MSW(11)
         L = MSW(12)
C--->  HOBOE BXOHOE HAPEHE
         VM = 0.0
         IF ( M .NE. 0 ) VM = VALLN1(M)
         VN = 0.0
         IF ( N .NE. 0 ) VN = VALLN1(N)
         UBX = VM - VN
C--->  HOBOE BXOHOE HAPEHE
         VK = 0.0
         IF ( K .NE. 0 ) VK = VALLN1(K)
         VL = 0.0
         IF ( L .NE. 0 ) VL = VALLN1(L)
         UBIX = VK - VL
C---> HOB TOK
         TOKBX = UBX * RMSW(14)
         TOKBIX= ( RMSW(13) * UBX + UBIX ) * RMSW(15)
C---> AC TOKA
         ITOK = MSW(7)
         TOKN1( ITOK ) = TOKBX
         TOKN1( ITOK + 1 ) = TOKBIX
         KOD = 0
         RETURN
C$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$
9999     CONTINUE
         NWTRE = MASPRT(14)
         WRITE(NWTRE,9998) MSW(1), MSW(2)
9998     FORMAT( ' *** O. CO B DM0028 - APOC HA OCBAHE O
     =O TA ***'/
     =    ' *** EMEHT # ', I10, ' T MOE # ', I10, ' ***'//)
         CALL SUBERR(0)
         END
